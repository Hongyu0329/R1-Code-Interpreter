# This script presents a known anatomical fact about the dinosaur Irritator challengeri.
# The information is based on the scientific paper: Sues et al. (2002), "On the skull of Irritator challengeri".

# According to the scientific literature, the large depression on the lacrimal bone,
# known as the lacrimal recess, contains a specific number of smaller, accessory openings.
number_of_depressions_in_recess = 2

# Print the result as a descriptive equation, including the final number.
print(f"Number of depressions contained in the lacrimal recess = {number_of_depressions_in_recess}")