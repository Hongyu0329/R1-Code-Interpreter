# This script provides the common name for the bee genus Perdita and the year it was coined.

# The common name for the genus Perdita.
common_name = "Fairy bees"

# The year the common name was coined by entomologist Joseph S. Wilson in a blog post.
year_coined = 2013

# Print the final answer in the format "<name>, <year>"
print(f"{common_name}, {year_coined}")