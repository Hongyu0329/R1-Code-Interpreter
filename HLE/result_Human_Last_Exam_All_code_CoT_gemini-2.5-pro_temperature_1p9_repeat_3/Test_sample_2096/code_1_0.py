    print("This clue is the key to the riddle. It does not mean the word 'illiterate' was invented in the 1960s. Instead, it refers to a famous work of fiction from that decade.")
    
    key_decade = "1960s"
    key_year = 1963
    
    print(f"In the year {key_year}, which is in the {key_decade}, the author Morris West published a very famous and influential novel titled 'The Shoes of the Fisherman'.")
