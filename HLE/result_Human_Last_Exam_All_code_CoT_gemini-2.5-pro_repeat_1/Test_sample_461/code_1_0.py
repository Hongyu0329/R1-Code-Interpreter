def find_hydrocarbon_name():
    """
    This function identifies and prints the name of a polycyclic hydrocarbon
    named after an extinct creature.
    """
    # The creature is the Pterodactyl, a type of pterosaur that went extinct over 65 million years ago.
    # The molecule, a polycyclic hydrocarbon, was named for its shape resembling the creature.
    hydrocarbon_name = "Pterodactyladiene"
    print(f"The polycyclic hydrocarbon named after a creature extinct for over 65 million years is: {hydrocarbon_name}")

find_hydrocarbon_name()