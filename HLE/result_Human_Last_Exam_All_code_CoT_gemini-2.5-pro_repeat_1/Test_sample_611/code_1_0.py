    # The key is to determine the limiting behavior of V_n as n -> infinity.

    # 1. The process X_t is a complex Brownian motion conditioned to stay outside the unit disk.
    #    This conditioning introduces a radial outward drift, making the process transient.
    #    This means |X_t| -> infinity as t -> infinity, almost surely.

