# 1. Extract the numbers from the haiku.
# "Twice fifteen"
num1 = 2 * 15
# "An August tempest"
num2 = 8
# "A divine one"
num3 = 1

# 2. Formulate the equation and calculate the result.
# The language suggests subtracting the smaller numbers from the largest.
result = num1 - num2 - num3

# 3. Print the final equation, showing each number.
# The result, 21, corresponds to the 21st letter of the alphabet, 'U',
# which is shaped like a bay, answering the question in the title.
print(f"The equation from the haiku is:")
print(f"{num1} - {num2} - {num3} = {result}")