def find_borges_reference():
    """
    This function identifies and prints the novel and author
    Borges described with the given quotes.
    """
    author = "Juan Carlos Onetti"
    novel = '"El astillero" (The Shipyard)'

    print(f"The author Jorge Luis Borges referred to is: {author}")
    print(f"The novel is: {novel}")
    print("\nBorges described Onetti as a 'continuator (and simplifier) of Faulkner' and praised his work for having 'the intensity of a tiger and the variety that a chess duel can achieve.'")

if __name__ == "__main__":
    find_borges_reference()