# The chemical formula for a representative nanocar is C138H96.
# We are interested in the number of hydrogen atoms.
hydrogen_atoms = 96

# The prefix "perfluoro-" means all hydrogen atoms are replaced by fluorine atoms.
# Therefore, the number of fluorine atoms is equal to the number of hydrogen atoms.
fluorine_atoms = hydrogen_atoms

# As requested, here is the final equation showing the relationship
# between the number of atoms.
print("The final equation for the atom substitution is:")
print(f"Number of Fluorine Atoms = Number of Hydrogen Atoms")
print(f"{fluorine_atoms} = {hydrogen_atoms}")

# The final answer is the number of fluorine atoms.
print(f"\nA perfluoronanocar would contain {fluorine_atoms} fluorine atoms.")