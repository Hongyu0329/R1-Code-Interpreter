def find_bee_common_name_info():
    """
    This function stores and prints the common name for the bee genus Perdita
    and the year of its earliest verifiable publication.
    
    The research indicates:
    1. Common Name: The genus Perdita is most commonly known as "Fairy bees" due to their
       tiny size and often iridescent coloration.
    2. Year Coined: While the name may have been used colloquially earlier, its earliest
       verifiable appearance in a publication is in the "Kansas School Naturalist"
       (Volume 39, Number 3) in an article on pollination by the world-renowned bee expert
       Charles D. Michener in 1993.
    """
    
    # The common name for the genus Perdita (Smith, 1853)
    common_name = "Fairy bees"
    
    # The year the common name was first found in a verifiable publication
    year = 1993
    
    # Print the answer in the format "<name>, <year>"
    print(f"{common_name}, {year}")

find_bee_common_name_info()