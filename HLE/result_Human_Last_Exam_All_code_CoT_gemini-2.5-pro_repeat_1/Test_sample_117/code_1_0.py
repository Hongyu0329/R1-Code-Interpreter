def solve_pskov_succession():
    """
    This function determines the successor to Ivan Ivanovich Golovin in Pskov in 1700.
    """
    
    # The question is about the leadership of the city of Pskov in the year 1700.
    # This year marked the start of the Great Northern War.
    # After the Russian army's defeat at Narva in November 1700, Tsar Peter the Great
    # appointed a new commander to defend the northwestern border, with Pskov as the key base.
    
    # The previous voivode was Ivan Ivanovich Golovin.
    
    # The options are:
    # A. Boris Petrovich Sheremetev
    # B. Peter Matveevich Apraksin
    # C. Mikhail Mikhailovich Golitsyn
    # D. Vasily Borisovich Bukhvostov
    # E. Fyodor Yuryevich Romodanovsky
    
    # Historical records show that Field Marshal Boris Petrovich Sheremetev was appointed
    # commander-in-chief of the forces in the region and took command in Pskov in late 1700.
    # His role as military commander effectively made him the successor.
    
    answer_choice = "A"
    full_name = "Boris Petrovich Sheremetev"
    
    print(f"The person who became the next voivode (or more accurately, the military commander in charge) of Pskov in 1700 was:")
    print(f"{answer_choice}. {full_name}")

solve_pskov_succession()