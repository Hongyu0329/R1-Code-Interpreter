def get_liu_bans_evaluation():
    """
    This function stores and prints Liu Ban's exact evaluation of Zhang Ji's seven-character poems.
    The evaluation is taken from Liu Ban's "Zhongshan Shihua" (《中山诗话》).
    """
    # 刘攽 (Liu Ban) on 张籍 (Zhang Ji) 的 七言诗 (seven-character poems)
    evaluation_text = """刘攽在《中山诗话》中对张籍七言诗的评价原文是：
“张籍七言诗，处处有精绝之句，人以其乐府得名，其实七言长篇，纾徐尽意，当在乐府之上。”
"""

    # 解释：
    # 这段话的意思是：刘攽认为，张籍的七言诗里，到处都有精彩绝伦的诗句。
    # 虽然世人因为他的乐府诗而知晓他，但实际上，他内容详尽、意蕴悠长的七言长诗，成就应当在他的乐府诗之上。
    # 这是一个非常高的评价。

    print(evaluation_text)

# Execute the function to display the answer
get_liu_bans_evaluation()