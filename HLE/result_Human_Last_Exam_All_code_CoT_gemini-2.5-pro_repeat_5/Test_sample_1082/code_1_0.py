# Define the financial variables provided in the problem
selling_price = 50
water_cost = 10
energy_cost = 15

# Calculate the total cost of resources used
total_cost = water_cost + energy_cost

# Calculate the Sustainable Value Added (SVA)
# SVA = Revenue - Total Costs
sustainable_value_added = selling_price - total_cost

# Print the final equation with all the numbers
print(f"Sustainable Value Added = ${selling_price} (Revenue) - (${water_cost} (Water Cost) + ${energy_cost} (Energy Cost)) = ${sustainable_value_added}")