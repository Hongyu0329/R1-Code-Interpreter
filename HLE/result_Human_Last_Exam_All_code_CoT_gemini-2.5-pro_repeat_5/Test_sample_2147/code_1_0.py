def solve_milyukov_riddle():
    """
    This script identifies the term Pavel Milyukov used to denounce
    a cultural movement as a 'stagnant swamp'.
    """
    
    # Pavel Milyukov, a classical liberal, was critical of modernist art movements.
    # The "X-Y" in the question refers to the specific group he targeted with this phrase.
    
    x = "Decadents"
    y = "Modernists"
    
    full_term = f"{x}-{y}"
    
    print("Pavel Milyukov used the term 'stagnant swamp' to criticize what he saw as the decay in modern art.")
    print("The term represented by 'X-Y' is a compound name for the artists and thinkers of these movements.")
    print(f"In the term 'X-Y':")
    print(f"X = {x}")
    print(f"Y = {y}")
    print(f"Therefore, the full term is: {full_term}")

solve_milyukov_riddle()