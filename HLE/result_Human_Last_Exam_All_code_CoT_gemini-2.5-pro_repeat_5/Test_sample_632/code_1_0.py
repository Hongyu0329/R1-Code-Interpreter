def find_removed_feature():
    """
    This function identifies and prints the architectural feature removed
    from the Piazza della Rotonda around 1950.
    """
    # The feature was the tram line that circled the fountain in the square.
    # It was removed as part of a city-wide project to replace trams with buses.
    removed_feature = "The tram line"

    print(f"The unique architectural feature removed from the Piazza della Rotonda was: {removed_feature}")

find_removed_feature()