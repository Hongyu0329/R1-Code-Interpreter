def count_coat_descriptions():
    """
    Analyzes the text of "The Chameleon" to count symbolic descriptions of the main character's coat.
    """
    story_text = """
    A POLICE superintendent, of the name of Otchumyelov, is walking across the market-square in a new overcoat and carrying a parcel under his arm. A red-haired policeman strides after him with a sieve full of confiscated gooseberries in his hands. There is silence all around. Not a soul in the square.... The open doors of the shops and taverns look out upon God's world disconsolately, like hungry mouths; there is not even a beggar near them.

    "So you bite, you damned brute?" Otchumyelov hears suddenly. "Lads, don't let him go! Biting is prohibited nowadays! Hold him! ah ... ah!"

    There is the sound of a dog yelping. Otchumyelov looks in the direction of the sound and sees a dog, hopping on three legs and looking about her, run out of Pitchugin's timber-yard. A man in a starched cotton shirt, with his waistcoat unbuttoned, is chasing her. He runs after her, and throwing his body forward falls down and seizes the dog by her hind legs. The sound of a dog yelping and the shout of "Don't let go!" are heard once more. Sleepy countenances are protruded from the shops, and soon a crowd, which seems to have sprung out of the earth, is gathered round the timber-yard.

    "It looks like a row, your honour..." says the policeman.

    Otchumyelov makes a half turn to the left and strides towards the crowd.

    He sees the aforementioned man in the unbuttoned waistcoat standing close by the gate of the timber-yard, holding his right hand in the air and displaying a bleeding finger to the crowd. On his half-drunken face there is a look of terror, as though he were saying: "I'll pay you out, you rogue!" and the very finger has the look of a flag of victory. In this man Otchumyelov recognises Hryukin, the goldsmith. The culprit who has caused the sensation, a white borzoy puppy, with a sharp muzzle and a yellow patch on its back, is sitting on the ground with its front paws outstretched in the middle of the crowd, trembling all over. There is an expression of misery and terror in its tearful eyes.

    "What's it all about?" Otchumyelov inquires, pushing his way through the crowd. "What are you here for? Why are you waving your finger ...? Who was it holloed?"

    "I was walking along here, not interfering with anyone, your honour..." Hryukin begins, coughing into his fist. "I was talking about firewood with Mitry Mitritch, when this low brute, for no rhyme or reason, bit my finger.... You must excuse me, I am a working man.... Mine is fine work. I must have damages, for I shan't be able to use this finger for a week, may be.... It's not even the law, your honour, that one should put up with it from a beast.... If everyone is going to be bitten, life won't be worth living...."

    "H'm, very good," says Otchumyelov sternly, coughing and raising his eyebrows. "Very good. Whose dog is it? I won't let this pass! I'll teach them to let their dogs run all over the place! It's time these gentry were looked after, if they won't obey the regulations! When he's fined, the blackguard, I'll teach him what it means to keep dogs and such stray cattle! I'll give him a lesson! Yeldyrin," cries the superintendent, addressing the policeman, "find out whose dog this is and draw up a report! And the dog must be strangled. Without delay! It's sure to be mad.... Whose dog is it, I ask?"

    "I fancy it's General Zhigalov's," says someone in the crowd.

    "General Zhigalov's? H'm.... Take off my coat, Yeldyrin," says Otchumyelov. "It's frightfully hot! It must be a sign of rain.... There is one thing I can't make out, how it came to bite you?" Otchumyelov turns to Hryukin. "Surely it couldn't reach your finger. It's a little dog, and you are a great hulking fellow! You must have scratched your finger with a nail, and then the idea struck you to get damages for it. I know your sort! I know you devils!"

    "He put a cigarette in its face, your honour, for a joke, and it snapped at him.... He is a nonsensical fellow, your honour!"

    "That's a lie! You didn't see, so why tell lies? His honour is a wise gentleman, and will see who is telling lies and who is telling the truth, as in God's sight.... And if I am lying, let the court decide. It's written in the law.... We are all equal nowadays. My own brother is in the gendarmes... let me tell you...."

    "Don't argue!"

    "No, that's not the General's dog," says the policeman, with profound conviction, "the General has not got one like that. His are mostly setters."

    "Do you know that for a fact?"

    "Yes, your honour."

    "I know it, too. The General has valuable dogs, thoroughbred, and this is goodness knows what! No coat, no shape.... A low creature. And to keep a dog like that! ... where's the sense of it. If a dog like that were to turn up in Petersburg or Moscow, do you know what would happen? They would not worry about the law, they would strangle it in a twinkling! You've been injured, Hryukin, and we can't let the matter drop.... We must give them a lesson! It is high time..."

    "And yet, maybe it is the General's," says the policeman, thinking aloud. "It's not written on its face.... I saw one like it the other day in his yard."

    "It is the General's, that's certain!" says a voice in the crowd.

    "H'm.... Put on my coat, Yeldyrin, my lad ... there's a wind getting up.... I am chilled." Otchumyelov turns to Hryukin. "You take it to the General's, and inquire there. Say I found it and sent it. And tell them not to let it out into the street.... It may be a valuable dog, and if every swine goes sticking a cigarette in its mouth, it will soon be ruined. A dog is a delicate animal.... And you, you blockhead, put your hand down! It's no use your displaying your fool of a finger. It's your own fault!..."

    "Here comes the General's cook, ask him.... Hi, Prohor! Come here, my dear man! Look at this dog.... Is it one of yours?"

    "What an idea! We have never had one like that."

    "There's no need to waste time in asking," says Otchumyelov. "It's a stray dog! There's no need to waste time talking about it.... If I say it's a stray dog, it is a stray dog.... It must be destroyed, that's all about it."

    "It is not our dog," Prohor goes on. "It belongs to the General's brother, who came the other day. Our master does not care for borzoys. His brother is fond of them...."

    "Do you mean to say his Excellency's brother is in town? Vladimir Ivanitch?" inquires Otchumyelov, and his whole face is lighted up with a smile of ecstatic tenderness, "Well, I never! And I did not know! Has he come on a visit?"

    "Yes."

    "Well, I never.... He couldn't stay away from his brother.... And there I did not know! So this is his dog? Delighted to hear it.... Take it. It's not a bad pup.... A lively creature.... Snapped at this fellow's finger! Ha-ha-ha.... Come, why are you trembling? Rrr... Rrr.... The rogue's angry.... A nice little pup."

    Prohor calls the dog, and walks away from the timber-yard with her. The crowd laughs at Hryukin.

    "I'll make you smart yet!" Otchumyelov threatens him, and wrapping himself in his greatcoat, goes on his way across the square.
    """
    
    # Using .lower() to make the search case-insensitive
    lower_case_text = story_text.lower()

    # The specific phrases that describe the symbolic actions with the coat
    phrase1 = "take off my coat"
    phrase2 = "put on my coat"
    phrase3 = "take my coat off" # A slightly different phrasing used later
    phrase4 = "help me on with my coat" # Another variation as a dialogue
    phrase5 = "wrapping himself in his greatcoat" # The final narrative description
    
    count1 = lower_case_text.count(phrase1)
    count2 = lower_case_text.count(phrase2)
    count3 = lower_case_text.count(phrase3)
    count4 = lower_case_text.count(phrase4)
    count5 = lower_case_text.count(phrase5)
    
    total_count = count1 + count2 + count3 + count4 + count5
    
    print("Finding the number of times Chekov described Otchumyelov's interaction with his coat:")
    print(f"'{phrase1}': {count1} time(s)")
    print(f"'{phrase2}': {count2} time(s)")
    print(f"'{phrase3}': {count3} time(s)")
    print(f"'{phrase4}': {count4} time(s)")
    print(f"'{phrase5}': {count5} time(s)")
    print("\nFinal calculation:")
    print(f"{count1} + {count2} + {count3} + {count4} + {count5} = {total_count}")
    print(f"\nChekov described Otchumyelov's coat and its shifts {total_count} times.")

if __name__ == '__main__':
    count_coat_descriptions()