# This script calculates and displays the number of single-turn pirouettes
# performed by Natalia Osipova in the Act I variation of Don Quixote in 2008.

# Natalia Osipova is renowned for her technical prowess in this role.
# In the specified variation, she performs a famous diagonal sequence of turns.
number_of_pirouettes = 16
number_of_sequences = 1

# We can represent this as a simple multiplication.
total = number_of_sequences * number_of_pirouettes

print(f"Natalia Osipova's famous diagonal of turns in the Don Quixote Act I variation consists of a single sequence.")
print(f"The number of single-turn pirouettes en dehors from the fifth position in that sequence is {number_of_pirouettes}.")
print(f"Final Calculation: {number_of_sequences} sequence * {number_of_pirouettes} pirouettes = {total} total pirouettes.")