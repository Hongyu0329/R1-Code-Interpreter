def find_accompanying_book():
    """
    This function provides information about the book of manners
    preserved alongside the poem "Sir Launfal".
    """
    # The poem is the Middle English romance "Sir Launfal" by Thomas Chestre.
    poem_title = "Sir Launfal"
    
    # This poem is famously preserved in a specific medieval manuscript.
    manuscript_name = "Cotton Caligula A.ii"
    
    # Within this same manuscript, there is a well-known poem on etiquette.
    book_of_manners_title = "The Boke of Curtasye"
    
    # Print the answer clearly.
    print(f"The Middle English romance '{poem_title}' is preserved in the manuscript known as {manuscript_name}.")
    print(f"Alongside it in that collection is the book of manners titled '{book_of_manners_title}' (The Book of Courtesy).")

find_accompanying_book()