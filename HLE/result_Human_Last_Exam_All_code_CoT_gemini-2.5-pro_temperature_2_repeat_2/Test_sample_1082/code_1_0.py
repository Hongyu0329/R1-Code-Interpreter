# Define the financial values from the problem description
selling_price = 50
water_cost = 10
energy_cost = 15

# Calculate the total environmental cost
total_environmental_cost = water_cost + energy_cost

# Calculate the Sustainable Value Added (SVA)
# SVA is the economic value (selling price) minus the environmental costs.
sustainable_value_added = selling_price - total_environmental_cost

# Print the final equation with each number clearly shown, as requested.
print("Sustainable Value Added (SVA) Calculation:")
print(f"SVA = Selling Price - (Water Cost + Energy Cost)")
print(f"SVA = ${selling_price} - (${water_cost} + ${energy_cost})")
print(f"SVA = ${selling_price} - ${total_environmental_cost}")
print(f"SVA = ${sustainable_value_added}")
