def print_evaluation():
    """
    This function prints the exact evaluation of Zhang Ji's seven-character poems
    by Liu Ban, as recorded in his work 《中山诗话》 (Zhongshan Shihua).
    """
    evaluation_text = """根据北宋学者刘攽在其诗话著作《中山诗话》中的记载，他对唐代诗人张籍的七言诗评价极低。

刘攽的原话是：“张籍乐府、宫词皆杰出，至其七言诗，拙涩不可读，何也？”

这段评价的意思是：刘攽认为张籍的乐府诗和宫词都非常出色，是杰作；然而，一谈到他的七言诗，就变得既笨拙（拙）又艰涩（涩），简直无法阅读。刘攽对此感到非常困惑和惊讶。

因此，刘攽对张籍七言诗的精确评价就是：“拙涩不可读”。"""

    print(evaluation_text)

print_evaluation()