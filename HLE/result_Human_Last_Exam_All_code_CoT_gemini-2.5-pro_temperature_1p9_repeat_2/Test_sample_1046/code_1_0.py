    print("\n1. Initial Event: 'rare resistance mutations'")
    print("   This correctly identifies the starting point for resistance in a stable genome.")

    print("\n2. Overcoming a Hurdle: 'compensatory mutations'")
    print("   Resistance mutations can harm a bacterium's normal functions, reducing its fitness. Compensatory mutations fix this problem, allowing the resistant bacterium to grow strong.")

    print("\n3. The Accelerator: 'increased the fitness to a great extent'")
    print("   This is crucial. The mutant doesn't just survive; it becomes more fit than the original population, allowing it to rapidly take over.")

    print("\n4. A Powerful Bonus: 'led to cross-resistance'")
    print("   If one mutation provides resistance to multiple drugs, it is a huge evolutionary advantage. It will be strongly selected for, dramatically speeding up its spread through the population.")

    print("-" * 30)
    print("Conclusion: The combination of high fitness and multi-drug resistance from a single line of mutation creates a 'super-bug' that can proliferate so quickly that its overall rate of establishing resistance in the population can rival that of LGT.")
