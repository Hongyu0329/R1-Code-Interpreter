# This script will print the five-word Latin quote requested by the user.
# The quote is attributed by Tacitus to his father-in-law, Agricola, in a speech
# before the Battle of Mons Graupius.
# The quote is "incolumitas ac decus eodem loco".
# 1. 'incolumitas' means 'safety'
# 2. 'ac' means 'and'
# 3. 'decus' means 'glory' or 'honor'
# 4. 'eodem' means 'the same'
# 5. 'loco' means 'in place'
# This phrase, meaning "safety and glory are in the same place," thematically
# resembles the situation in chapter 4 of Cornelius Nepos's Life of Chabrias, where
# Chabrias chooses an honorable but dangerous course over a safe but shameful one.

latin_quote = "incolumitas ac decus eodem loco"
print(latin_quote)