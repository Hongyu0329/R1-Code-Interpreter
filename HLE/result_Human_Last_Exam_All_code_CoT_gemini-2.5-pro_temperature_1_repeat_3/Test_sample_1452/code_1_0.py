# The user wants to find the famous equation associated with Max Born.
# Max Born's most notable contribution is the Born rule, which provides a statistical
# interpretation of the quantum mechanical wavefunction.

# Name of the scientist
name = "Max Born"

# The Born rule equation. It states that the probability density (ρ)
# of finding a particle is the squared magnitude of its wavefunction (Ψ).
# The number 2 is part of the equation.
equation_latex = r"\( \rho = |\Psi|^2 \)"

# Print the name and the equation in the required format.
print(f"Name: {name}")
print(f"Equation: {equation_latex}")