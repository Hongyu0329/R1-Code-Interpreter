# Step 1: Define the values for the cuneiform numerals.
# 'сеп' is 7.
# '𒌋' is 10.
# '𒁹' is 1.

# Step 2: Translate the first term of the equation: 7 / (10 + 10 + 10)
numerator = 7
denominator = 30

# Step 3: Translate the second term of the equation: (10 + 10) + (1 + 1 + 1)
second_number = 23

# Step 4: Calculate the result of the full equation.
result = (numerator / denominator) - second_number

# Step 5: Print the equation in modern numbers and the final answer.
print(f"The equation is: {numerator} / {denominator} - {second_number}")
print(f"The result is: {result}")