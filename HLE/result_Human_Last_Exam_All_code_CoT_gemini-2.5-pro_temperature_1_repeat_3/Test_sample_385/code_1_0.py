def find_tacitus_quote():
    """
    This function stores and prints the famous five-word Latin quote from Tacitus.
    The quote is attributed to Agricola in a speech before the Battle of Mons Graupius.
    It is formatted in all lowercase and without punctuation as requested.
    """
