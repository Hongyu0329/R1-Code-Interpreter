import math

# The Gromov-Hausdorff distance between the interval [0,1] and the unit circle
# is given by the formula: pi / 2.

# The numbers in the final equation are:
pi_value = math.pi
denominator = 2

# Calculate the result
result = pi_value / denominator

print("The final equation for the distance is: pi / 2")
print(f"Value of pi: {pi_value}")
print(f"Value of the denominator: {denominator}")
print(f"The Gromov-Hausdorff distance is: {result}")