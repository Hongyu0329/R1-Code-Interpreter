# The common name for the bee genus "Perdita (Smith, 1853)" is "fairy bees".
# This name was proposed by the entomologist T.D.A. Cockerell in a 1926 publication.
# This script will print the name and the year it was coined.

common_name = "fairy bees"
year_coined = 1926

print(f"{common_name}, {year_coined}")