def solve_trilogy():
    """
    This function solves the three-part riddle to identify a trilogy of cities.
    """
    # City 1: From Irving Wallace's "The Miracle"
    city_1 = "Lourdes"

    # City 2: From Yuri Krasnokutsky's poem about Babyn Yar
    city_2 = "Kyiv"

    # City 3: From the analogue to Zagreb's "Dolac" market
    city_3 = "Paris"

    # Print the final trilogy in the specified format
    print(f"{{{city_1}, {city_2}, {city_3}}}")

solve_trilogy()