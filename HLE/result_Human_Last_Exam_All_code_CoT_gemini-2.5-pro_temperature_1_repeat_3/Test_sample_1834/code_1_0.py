import math

def solve_magnetic_field():
    """
    Calculates the magnitude of the magnetic field from two perpendicular wires.
    """
    # Point of interest
    x, y, z = 1, -1, 0

    print(f"This script calculates the magnitude of the magnetic field at the point P({x}, {y}, {z}).")
    print("The field is generated by two infinite wires carrying current I.")
    print("-" * 60)

    # --- Analysis for Wire 1 ---
    print("Step 1: Analyze the field from Wire 1 (on x-axis, current in +x direction).")
    
    # Perpendicular distance from P to the x-axis
    r1 = math.sqrt(y**2 + z**2)
    print(f"The perpendicular distance r1 from P to the x-axis is sqrt({y}^2 + {z}^2) = {r1:.1f}")
    
    # The magnitude B1 is (mu_0 * I) / (2 * pi * r1)
    print(f"The magnitude of its magnetic field, B1, is (mu_0 * I) / (2 * pi * {r1:.1f}).")
    
    # Direction of B1
    print("By the right-hand rule, with current in +x, the field at y=-1 is in the -z direction.")
    print("So, the magnetic field vector B1 = (0, 0, -mu_0*I / (2*pi)).")
    print("-" * 60)

    # --- Analysis for Wire 2 ---
    print("Step 2: Analyze the field from Wire 2 (on y-axis, current in +y direction).")
    
    # Perpendicular distance from P to the y-axis
    r2 = math.sqrt(x**2 + z**2)
    print(f"The perpendicular distance r2 from P to the y-axis is sqrt({x}^2 + {z}^2) = {r2:.1f}")
    
    # The magnitude B2 is (mu_0 * I) / (2 * pi * r2)
    print(f"The magnitude of its magnetic field, B2, is (mu_0 * I) / (2 * pi * {r2:.1f}).")
    
    # Direction of B2
    print("By the right-hand rule, with current in +y, the field at x=1 is also in the -z direction.")
    print("So, the magnetic field vector B2 = (0, 0, -mu_0*I / (2*pi)).")
    print("-" * 60)

    # --- Total Magnetic Field ---
    print("Step 3: Calculate the total magnetic field by vector addition (superposition).")
    print("B_total = B1 + B2")
    print("Since both vectors point in the same direction (-z), their magnitudes add up.")
    print("Magnitude |B_total| = |B1| + |B2|")
    print(f"|B_total| = (mu_0 * I) / (2 * pi * {r1:.1f}) + (mu_0 * I) / (2 * pi * {r2:.1f})")
    print("|B_total| = 2 * (mu_0 * I) / (2 * pi)")
    print("|B_total| = (mu_0 * I) / pi")
    print("-" * 60)

    # --- Final Answer ---
    print("Final equation for the magnitude of the magnetic field:")
    # Fulfilling the request to output numbers in the final equation by showing the coefficients.
    # The simplified form is (1 * mu_0 * I) / (1 * pi)
    print("Magnitude = (1 * mu_0 * I) / (1 * pi)")

solve_magnetic_field()