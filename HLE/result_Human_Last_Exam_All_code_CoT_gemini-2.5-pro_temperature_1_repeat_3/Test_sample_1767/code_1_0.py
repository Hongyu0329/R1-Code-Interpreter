# Define the Chern numbers of the two individual insulators
chern_number_1 = 1
chern_number_2 = 1

# The Chern number of the combined system is the sum of the individual Chern numbers
total_chern_number = chern_number_1 + chern_number_2

# Print the resulting equation
print(f"The Chern number of the junction is the sum of the individual Chern numbers.")
print(f"{chern_number_1} + {chern_number_2} = {total_chern_number}")