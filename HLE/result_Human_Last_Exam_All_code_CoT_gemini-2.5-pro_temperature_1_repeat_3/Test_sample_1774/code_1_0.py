# This script prints the move sequence for the checkmate.

print("The shortest path to checkmate the black king is in 3 moves.")
print("The mating sequence is as follows:")

# Move 1: White sacrifices the Queen to break the pin on the e5 knight.
print("1. Qxh7+ Kxh7")

# Move 2: White delivers a forcing double check. Black's king must move to h6.
print("2. Nxf7+ Kh6")

# Move 3: White delivers checkmate with a pawn.
print("3. g4#")

print("\nThe number of moves for White to mate is 3.")