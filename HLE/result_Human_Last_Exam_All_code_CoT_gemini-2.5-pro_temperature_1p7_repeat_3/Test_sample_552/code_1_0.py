# This script will print the name of the person whose lectures
# were cited by Albert Einstein in his famous 1905 paper on Brownian motion.

# The paper is titled "Über die von der molekularkinetischen Theorie der Wärme
# geforderte Bewegung von in ruhenden Flüssigkeiten suspendierten Teilchen"
# ("On the Motion of Small Particles Suspended in a Stationary Liquid, as
# Required by the Molecular-Kinetic Theory of Heat").

# In this paper, Einstein cites the work and lectures of a pioneering physicist
# in the field of statistical mechanics and the kinetic theory of gases.

cited_scientist = "Ludwig Boltzmann"

print(f"In his 1905 paper on Brownian motion, Albert Einstein cited the lectures of: {cited_scientist}")