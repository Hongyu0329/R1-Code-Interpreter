def find_french_equivalent():
    """
    This function provides the modern French equivalent for the ancient name "Odobeccus".
    """
    gaulish_name = "Odobeccus"
    # The name Odobeccus is a Latinized Frankish name.
    # The Germanic root 'Odo-' or 'Audo-' evolved into the French name 'Eudes'.
    french_equivalent = "Eudes"
    
    print(f"The Gaulish/Frankish name is: {gaulish_name}")
    print(f"Its modern French equivalent is: {french_equivalent}")

find_french_equivalent()