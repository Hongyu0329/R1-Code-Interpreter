def find_radical_name():
    """
    This function identifies the radical for the kanji 謄 and prints its name in Hepburn romanization.
    """
    # The character is 謄.
    # The radical is the left-side component: 言.
    # When the radical 言 (word) is on the left side (hen), its name is ごんべん.
    # The Hepburn romanization for ごんべん is "Gonben".
    radical_name = "Gonben"
    print(radical_name)

find_radical_name()