def find_prophets_verses():
    """
    This function identifies and prints the range of verses (bayt) in
    'Aqeedat al-'Awaam' where the names of the prophets are mentioned.
    """
    # The first verse that starts listing the names of the prophets.
    start_bayt = 28

    # The last verse that lists the names of the prophets.
    end_bayt = 31

    print("The names of the Prophets in the poem 'Aqeedat al-'Awaam' are mentioned from bayt:")
    print(start_bayt)
    print("to bayt:")
    print(end_bayt)

