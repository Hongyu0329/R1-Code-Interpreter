def find_location_of_defeat():
    """
    This function identifies and prints the current Vietnamese province
    where Ming General Mu Sheng's forces experienced their first major defeat
    during the Lam Sơn uprising.
    """
    battle = "Battle of Tốt Động – Chúc Động"
    year = 1426
    historical_location = "Tốt Động and Chúc Động, Chương Mỹ District"
    modern_province = "Hanoi"

    print(f"The first major defeat for the Ming army during the Lam Sơn uprising was the {battle} in {year}.")
    print(f"The battle took place at {historical_location}.")
    print(f"Today, this area is part of the city of {modern_province}, which is a province-level municipality in Vietnam.")
    print("\nTherefore, the current Vietnamese province is:")
    print(modern_province)

find_location_of_defeat()