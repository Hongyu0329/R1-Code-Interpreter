def get_bee_info():
    """
    This function provides the common name and the year it was coined for the genus Perdita.
    
    The common name "Fairy Bees" is widely used for this genus of tiny bees.
    The earliest prominent published reference leading to this name comes from a 1923
    paper by the entomologist T.D.A. Cockerell, where he described them as
    "The little fairy-like bees of the genus Perdita".
    """
    common_name = "Fairy Bees"
    year_coined = 1923
    
    print(f"{common_name}, {year_coined}")

get_bee_info()