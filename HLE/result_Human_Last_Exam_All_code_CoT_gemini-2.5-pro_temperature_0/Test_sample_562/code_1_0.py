def find_radical_name():
    """
    This function identifies the radical for the kanji 謄 and prints its name
    in Hepburn romanization.
    """
    kanji = "謄"
    # The radical for 謄 is 月, which is a variant of 肉 (niku, meat).
    # When it appears on the left side (hen), it is called Nikuzukihen.
    radical_name_hepburn = "Nikuzukihen"

    print(f"The radical (bushu) in the character '{kanji}' is '月'.")
    print("This is the 'nikuzuki' variant, derived from the character for meat (肉).")
    print("When it appears on the left side of a character, it is called a 'hen'.")
    print(f"The Hepburn transcription for the radical's name is: {radical_name_hepburn}")

find_radical_name()