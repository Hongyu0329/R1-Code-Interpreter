def find_dinosaur_hydrocarbon():
    """
    This function identifies and prints the name of a polycyclic hydrocarbon
    named after a creature that went extinct over 65 million years ago.
    """
    # The creature is a dinosaur. The polycyclic hydrocarbon is a biomarker
    # found in rocks from the Mesozoic Era, the age of dinosaurs.
    hydrocarbon_name = "Dino-sterane"
    
    print(f"The polycyclic hydrocarbon named after a creature extinct for over 65 million years is: {hydrocarbon_name}")

find_dinosaur_hydrocarbon()