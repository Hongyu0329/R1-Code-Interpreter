def solve_trilogy():
    """
    This function solves the three clues to identify three cities and prints them
    in the specified format.
    """
    # Clue 1: Irving Wallace's "The Miracle" is set in Lourdes, France.
    city_1 = "Lourdes"

    # Clue 2: The poem by Yuri Krasnokutsky poetically describes the Dnieper River
    # flowing through Kyiv, the capital of Ukraine.
    city_2 = "Kyiv"

    # Clue 3: The Dolac market in Zagreb is called the "Belly of Zagreb".
    # Its famous literary analogue, "The Belly of Paris" (Le Ventre de Paris),
    # refers to the Les Halles market in Paris.
    city_3 = "Paris"

    # Print the final trilogy in the required format.
    print(f"{{{city_1}, {city_2}, {city_3}}}")

solve_trilogy()
