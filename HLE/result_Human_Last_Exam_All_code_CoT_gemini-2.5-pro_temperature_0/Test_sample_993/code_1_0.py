def solve_paleoclimate_puzzle():
    """
    Determines the climatic conditions that lead to low 18O isotope levels in lake sulfate deposits.
    """
    print("Step 1: Connect sulfate 18O to lake water 18O.")
    print("The oxygen in sulfate deposits comes from the lake water. Therefore, low 18O in sulfate implies the lake water had low 18O.\n")

    print("Step 2: Analyze the effect of temperature and evaporation.")
    print("Evaporation favors the lighter 16O isotope, leaving the remaining water enriched in 18O.")
    print("To keep 18O levels low, we need to minimize evaporation. This points to a COLD climate.\n")

    print("Step 3: Analyze the effect of precipitation.")
    print("Precipitation, especially in cold regions, is depleted in 18O.")
    print("To get a strong low-18O signal, the lake needs significant input from this type of precipitation. This points to a WET climate.\n")

    print("Step 4: Analyze the effect of lake levels.")
    print("A wet climate (precipitation > evaporation) leads to HIGH lake levels.")
    print("High lake levels create a large water volume that is less susceptible to 18O enrichment from evaporation compared to a shallow lake.\n")

    print("Step 5: Synthesize the final answer.")
    print("The combination of factors leading to low 18O is a WET, COLD climate with HIGH lake levels.")
    print("This corresponds to choice G.")

solve_paleoclimate_puzzle()