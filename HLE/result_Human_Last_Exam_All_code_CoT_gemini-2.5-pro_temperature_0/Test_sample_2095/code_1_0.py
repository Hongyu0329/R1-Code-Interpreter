def liu_ban_evaluation_of_zhang_ji():
    """
    This function provides the exact evaluation of Zhang Ji's (张籍)
    seven-character poems by Liu Ban (刘攽) as recorded in his work
    "Zhongshan Shihua" (《中山诗话》).
    """
    evaluation = """根据北宋刘攽在其《中山诗话》中的记载，他对张籍诗歌的评价如下：

原文：“张籍乐府、宫词皆杰出，七言诗亦多佳句，但其格不甚高。”

这段话中，刘攽对张籍七言诗的直接评价是“亦多佳句，但其格不甚高”。
这可以理解为：
1.  优点 (亦多佳句): 张籍的七言诗中有很多优秀的、写得很好的诗句。
2.  缺点 (但其格不甚高): 尽管有好句子，但诗歌的整体风格、气韵和格局不算特别高超，未能达到像李白、杜甫那样的顶尖水平。"""
    print(evaluation)

