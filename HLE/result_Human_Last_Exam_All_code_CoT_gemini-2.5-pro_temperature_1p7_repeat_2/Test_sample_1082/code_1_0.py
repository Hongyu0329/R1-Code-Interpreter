# Step 1: Define the variables from the problem description.
selling_price = 50
cost_water = 10
cost_energy = 15

# Step 2: Calculate the total costs.
total_cost = cost_water + cost_energy

# Step 3: Calculate the Sustainable Value Added (SVA).
# SVA is calculated as Revenue - Total Costs.
sva = selling_price - total_cost

# Step 4: Print the final equation with each number.
# The result demonstrates the calculation: Revenue - Cost of Water - Cost of Energy = SVA.
print(f"{selling_price} - {cost_water} - {cost_energy} = {sva}")