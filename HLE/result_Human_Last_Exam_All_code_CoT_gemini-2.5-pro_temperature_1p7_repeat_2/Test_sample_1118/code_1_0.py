def find_symbolic_object():
    """
    This function stores and prints the information about Curt Querner's 1933 self-portrait.
    """
    # The year the self-portrait was painted
    year = 1933
    
    # The object Querner held as a symbol of resistance
    symbol_of_resistance = "a red poppy"
    
    # The name of the painting is "Selbstbildnis mit Mohnblume" (Self-Portrait with Poppy)
    
    print(f"The year of the self-portrait is: {year}")
    print(f"The object held as a symbol of resistance is: {symbol_of_resistance}")

# Execute the function to display the answer
find_symbolic_object()