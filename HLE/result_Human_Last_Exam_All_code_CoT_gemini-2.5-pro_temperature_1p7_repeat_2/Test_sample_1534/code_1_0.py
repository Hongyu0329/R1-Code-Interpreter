def find_adjacent_brain_regions():
    """
    This function identifies and prints the brain regions that are located
    posteriorly adjacent to the palliovisceral lobe in a dwarf cuttlefish.

    The palliovisceral lobe is part of the posterior subesophageal mass of the
    cephalopod brain. The most posterior parts of this brain mass, which are
    functionally and anatomically connected to the palliovisceral lobe,
    are the fin lobes and the posterior chromatophore lobes.
    """
    
    # Define the brain regions
    posterior_region_1 = "Posterior chromatophore lobes"
    posterior_region_2 = "Fin lobes"

    # Print the answer
    print("The brain regions that lie adjacent posteriorly to the palliovisceral lobe in the dwarf cuttlefish are:")
    print(f"- {posterior_region_1}")
    print(f"- {posterior_region_2}")

# Execute the function to display the answer
find_adjacent_brain_regions()