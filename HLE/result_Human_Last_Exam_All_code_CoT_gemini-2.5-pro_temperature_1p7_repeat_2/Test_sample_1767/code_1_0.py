# The Chern number is an additive quantity for combined topological systems.
# We are given two Chern insulators, each with a Chern number of 1.

# Chern number of the first insulator
c1 = 1

# Chern number of the second insulator
c2 = 1

# The Chern number of the junction is the sum of the individual Chern numbers.
c_total = c1 + c2

# Print the calculation and the result
print(f"The Chern number of the junction is calculated by summing the individual Chern numbers:")
print(f"{c1} + {c2} = {c_total}")