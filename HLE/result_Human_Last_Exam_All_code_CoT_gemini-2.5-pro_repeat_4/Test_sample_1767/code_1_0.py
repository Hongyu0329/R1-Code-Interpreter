# Step 2: The Chern number of a junction of two topological systems is the sum of their individual Chern numbers.
total_chern_number = chern_insulator_1 + chern_insulator_2

# Step 3: Print the final equation and the result.
# The final equation will show the sum of the individual Chern numbers.
print(f"The Chern number of the junction is calculated by summing the Chern numbers of the individual insulators.")
print(f"Final Equation: {chern_insulator_1} + {chern_insulator_2} = {total_chern_number}")