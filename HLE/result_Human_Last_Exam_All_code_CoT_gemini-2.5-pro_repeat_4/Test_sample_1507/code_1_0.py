# A regular icosahedron is a polyhedron with 20 faces.
# The faces of a regular icosahedron are equilateral triangles.
num_faces_icosahedron = 20

# The outer-hull (or convex hull) of a great icosahedron is a regular icosahedron.
# Therefore, the number of visible triangles on the surface of the great icosahedron's
# outer-hull is equal to the number of faces of a regular icosahedron.

print("The final equation is:")
print(f"Number of triangles = {num_faces_icosahedron}")