def find_borges_names():
    """
    This function identifies and prints the names of the three individuals
    cited by Jorge Luis Borges in his essay "Our Poor Individualism"
    to illustrate patriotism.
    """
    # The three individuals mentioned by Borges are the Greek biographer Plutarch,
    # the English historian Edward Gibbon, and the German philosopher Arthur Schopenhauer.
    the_greek = "Plutarch"
    the_englishman = "Gibbon"
    the_german = "Schopenhauer"

    # Formatting the names as a comma-separated string
    result = f"{the_greek}, {the_englishman}, {the_german}"

    print(result)

find_borges_names()