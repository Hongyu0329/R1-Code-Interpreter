def find_character():
    """
    This function identifies and prints the name of the character from
    William Morris's "The House of the Wolfings" who describes the
    Roman founding myth of Romulus and Remus.
    """
    # The character in question is a former thrall who was captured by the Romans
    # and lived in one of their cities before returning to the Goths.
    # In Chapter V, "Otter tells of the Romans," he describes what he saw.
    character_name = "Otter"

    # He describes the Roman token or symbol as follows:
    # "...a She-wolf giving suck to two men-children, and this they have
    # in all their temples, and on their banners..."
    # This is a direct reference to the legend of Romulus and Remus.
    
    print(f"The character in William Morris's The House of the Wolfings who describes the Roman myth of Romulus and Remus is: {character_name}")

find_character()