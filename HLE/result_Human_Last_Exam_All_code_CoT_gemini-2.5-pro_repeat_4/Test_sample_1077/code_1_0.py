def find_philosopher():
    """
    This function identifies and prints the name of the philosopher
    who wrote the specified letter to Heidegger.
    """
    author_name = "José Ortega y Gasset"
    context = (
        "The letter was written by the Spanish philosopher José Ortega y Gasset in 1934, "
        "following a visit to Martin Heidegger in Freiburg that left Ortega feeling disappointed."
    )
    
    print(f"The philosopher who wrote the letter to Heidegger was: {author_name}")
    print(f"Context: {context}")

find_philosopher()