# 'quadrant' is interpreted as 90 (from 90 degrees in a quadrant of a circle).
num1 = 90
# '𒌋𒌋𒌋' is 10 + 10 + 10.
num2 = 30
# '𒌋𒌋𒁹𒁹𒁹' is 10 + 10 + 1 + 1 + 1.
