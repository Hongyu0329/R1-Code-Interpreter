# This script determines and prints the shape of the water surface in a
# half-filled icosahedron tank standing on one of its faces.

# The reasoning is based on the symmetry of the icosahedron.
# 1. An icosahedron has a center of symmetry.
# 2. When standing on a face, the plane that divides the volume in half
#    is the central plane parallel to the base face.
# 3. This central cross-section of an icosahedron is a regular hexagon.

# The name of the resulting shape.
surface_shape = "A regular hexagon"

# Print the final answer.
print("The shape of the water surface in a half-filled icosahedron tank standing on a face is:")
print(surface_shape)