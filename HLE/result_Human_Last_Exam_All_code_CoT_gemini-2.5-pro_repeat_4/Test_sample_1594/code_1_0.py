def print_building_shape():
    """
    This function prints an ASCII art representation of the letter
    formed by the buildings of Istituto Galilei-Pacinotti and
    Liceo Ulisse Dini when viewed from above.
    """
    # The top horizontal bar represents the Liceo Dini building.
    print("TTTTTTTTT")
    
    # The vertical stem represents the Galilei-Pacinotti building.
    print("    T    ")
    print("    T    ")
    print("    T    ")
    print("    T    ")
    print("    T    ")

# Execute the function to display the letter.
print_building_shape()