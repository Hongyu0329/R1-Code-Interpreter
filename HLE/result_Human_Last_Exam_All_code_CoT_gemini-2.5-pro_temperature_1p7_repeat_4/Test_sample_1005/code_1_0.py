import math

# The value of the integral simplifies to the expression: 2^(15/16)
base = 2
numerator = 15
denominator = 16

# We now calculate this value
value = base**(numerator/denominator)

# Print the components of the final expression
print(f"The final expression is: {base}^({numerator}/{denominator})")

# Print the final numerical result
print(f"The numerical value of the integral is: {value}")