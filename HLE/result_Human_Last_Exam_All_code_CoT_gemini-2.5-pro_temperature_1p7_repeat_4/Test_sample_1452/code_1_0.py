import sys

# This script will print the name and famous equation of Max Born.
# Max Born's equation is the Born rule, which is a fundamental postulate of quantum mechanics.
# It gives the probability density of finding a particle at a given location.
# The equation is P = |Ψ|², where Ψ is the wave function.
# The number in this equation is 2.

scientist = "\"Max Born\""
equation = r"\(P = |\Psi|^2\)"

print(f"Name: {scientist}")
print(f"Equation: {equation}")