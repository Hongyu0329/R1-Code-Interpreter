# must be able to reproduce the same anomaly values.
#
# This requirement severely restricts the possible spectrum of low-energy particles and how
# symmetries are realized (i.e., whether they remain intact or are spontaneously broken).
# Therefore, the most accurate and encompassing description of its physical implication is that
# it acts as a strong constraint on low-energy effective theories.

