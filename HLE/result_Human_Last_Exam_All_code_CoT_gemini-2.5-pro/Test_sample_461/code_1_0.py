# This script prints the name of the polycyclic hydrocarbon
# named after an extinct flying reptile.

# The molecule's name is a portmanteau of "pterodactyl" and "diene"
# (a hydrocarbon with two double bonds).
molecule_name = "Pterodactyladiene"

print(f"The polycyclic hydrocarbon named after a creature extinct for over 65 million years is: {molecule_name}")