def find_radical_name():
    """
    This function provides the name of the radical for the Japanese character 謄.
    """
    character = '謄'
    # The radical for 謄 is 言.
    # When this radical appears on the left side of a character,
    # its name is ごんべん (gonben).
    radical_name_hepburn = "Gonben"

    print(f"The bushu (radical) in the character '{character}' is called '{radical_name_hepburn}'.")

find_radical_name()