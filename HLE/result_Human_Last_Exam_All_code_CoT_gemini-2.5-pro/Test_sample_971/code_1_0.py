def solve_ballet_riddle():
    """
    This function calculates and prints the number of turns in Kitri's
    famous Act I variation sequence from Don Quixote.
    """

    # The celebrated number of turns in the turning sequence of the Act I variation.
    number_of_turns = 16

    # We can create a simple equation to represent this single sequence of turns.
    number_of_sequences = 1

    # The total number of turns is the result of the equation.
    total_turns = number_of_sequences * number_of_turns

    print("In the famous turning sequence of Kitri's Act I variation, the number of turns is represented by this equation:")
    print(f"{number_of_sequences} * {number_of_turns} = {total_turns}")


solve_ballet_riddle()