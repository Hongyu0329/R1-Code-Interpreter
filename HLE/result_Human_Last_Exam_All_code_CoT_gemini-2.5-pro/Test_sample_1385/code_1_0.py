def solve():
    """
    This function prints the correct order of the given Old Russian enclitics.
    The order is determined by established linguistic rules.
    """
    # The enclitics in their correct grammatical order
    ordered_enclitics = ["же", "бо", "бы", "еси", "мя"]

    # Print the final ordered sequence
    print("The correct order of the enclitics is:")
    print(" -> ".join(ordered_enclitics))

solve()