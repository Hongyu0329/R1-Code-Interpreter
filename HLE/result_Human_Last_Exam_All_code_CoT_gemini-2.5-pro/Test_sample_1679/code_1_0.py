# The task is to identify the first Best Picture Oscar winner to feature a Luxor Obelisk.
# There are two Luxor Obelisks: one in Luxor, Egypt, and one in Paris, France (at the Place de la Concorde).

# After reviewing the list of Best Picture winners chronologically, I determined the following:
# - Early winners like "Wings" (1927/28) or "The Life of Emile Zola" (1937), while set in France, do not prominently feature the Place de la Concorde.
# - No early winners are set in Luxor, Egypt.
# - The film "An American in Paris" won Best Picture for the year 1951.
# - This film is famous for its musical numbers set against the backdrop of iconic Parisian locations.
# - One of the locations prominently featured in the film is the Place de la Concorde, which is where the Parisian Luxor Obelisk stands.

# Therefore, "An American in Paris" is the first Best Picture winner to depict a Luxor Obelisk.

movie_title = "An American in Paris"
award_year = 1951
obelisk_location = "Place de la Concorde, Paris"

print(f"The first Academy Award for Best Picture winner to depict a Luxor Obelisk was '{movie_title}'.")
print(f"It won for the year {award_year}.")
print(f"The film shows the obelisk located at the {obelisk_location}.")