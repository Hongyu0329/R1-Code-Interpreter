# This script will provide the answer to the philosophical question about Kant's Critique of Judgment.
# The question is whether Kant's account of aesthetic judgment is purely descriptive or also normative.

# The reasoning is that Kant's account is both.
# 1. Descriptive: It describes the a priori cognitive conditions (e.g., the free play of imagination and understanding)
#    that make aesthetic judgments possible.
# 2. Normative: It also sets the standards or rules for what constitutes a *valid* or *pure* judgment of taste
#    (e.g., disinterestedness), distinguishing it from mere personal preference.
#    This creates a norm for how one *ought* to judge to make a claim to universal validity.

final_answer = "The account is both descriptive and normative."

print(final_answer)