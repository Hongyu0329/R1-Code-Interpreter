def find_original_pen_owner():
  """
  This function provides the family name of the revealed original owner of the
  lucky ballpoint pen from the Odd Taxi audio drama 13.3.
  """
  # In the audio drama, it's revealed that the pen was a gift from a father
  # to his daughter, high school student Rui Nikaido. She is the first owner
  # in the chain of events that leads to the main story.
  family_name = "Nikaido"
  
  print(f"The family name of the revealed original owner of the lucky ballpoint pen is: {family_name}")

find_original_pen_owner()