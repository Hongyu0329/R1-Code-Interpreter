def get_born_equation():
    """
    This function provides the famous equation associated with Max Born.
    """
    scientist = "Max Born"
    # The Born rule gives the probability density of finding a particle at a given point.
    # It is the square of the magnitude of the wave function.
    # The equation includes the number 2.
    equation = "P(x) = |Ψ(x)|²"
    
    print(f"Name: {scientist}")
    print(f"Equation: {equation}")

get_born_equation()