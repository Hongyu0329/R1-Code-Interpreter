    # by the major American poet Allen Ginsberg.
    poet_last_name = "Ginsberg"

    # Based on an analysis of Allen Ginsberg's famous recordings
    # of "Kaddish," his performance gives powerful and dramatic
    # emphasis to the word "holler," reflecting the poem's
    # intense emotional content regarding his mother's suffering.
