import math

def solve_dimension_problem():
    """
    This function calculates the largest possible dimension for the quotient ring R/I.

    The problem asks for the largest possible dimension of R/I, where R is a polynomial
    ring in 10 variables and I is the ideal generated by positive-degree homogeneous
    invariants of a group G of order 10000 acting on C^10.

    According to a fundamental theorem in invariant theory, the dimension of the
    coinvariant algebra R/I is equal to the order of the group effectively acting on the
    vector space. Let the action be described by a representation rho: G -> GL_10(C).
    The acting group is H = rho(G).
    So, dim(R/I) = |H| = |rho(G)|.

    By the first isomorphism theorem, |rho(G)| = |G| / |ker(rho)|.
    We are given |G| = 10000.
    To maximize dim(R/I), we must minimize the size of the kernel, |ker(rho)|.

    The minimum possible order for any subgroup is 1 (the trivial subgroup). This
    occurs when the representation rho is faithful (injective).
    If we can find a group G of order 10000 with a faithful 10-dimensional
    representation, the maximum dimension will be |G| / 1.

    An example is the cyclic group G = C_10000. It has a faithful representation
    in GL_10(C). For instance, mapping a generator of G to a diagonal matrix
    diag(exp(2*pi*i/10000), 1, ..., 1) results in a faithful representation.

    Therefore, the maximum possible dimension is indeed achievable.
    """

    # The order of the group G.
    order_G = 10000

    # The minimum possible size of the kernel for the representation rho.
    min_kernel_size = 1

    # The maximum possible dimension is the order of G divided by the minimum kernel size.
    max_dimension = order_G / min_kernel_size

    print("The largest possible dimension is calculated as follows:")
    print(f"Order of the group G: |G| = {order_G}")
    print(f"Minimum possible size of the kernel of the representation: |ker(rho)| = {min_kernel_size}")
    print(f"The final equation for the maximum dimension is: {order_G} / {min_kernel_size} = {int(max_dimension)}")

solve_dimension_problem()