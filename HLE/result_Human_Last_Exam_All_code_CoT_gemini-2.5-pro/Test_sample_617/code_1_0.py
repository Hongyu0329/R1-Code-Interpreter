def explain_pseudomonas_color():
    """
    This function explains why a concentrated sample of Pseudomonas aeruginosa is blue-green.
    """
    organism = "Pseudomonas aeruginosa"
    pigment_blue = "pyocyanin"
    pigment_green = "pyoverdin"
    final_color = "blue-green"

    print(f"Step 1: The organism in question is {organism}.")
    print(f"Step 2: This bacterium is known for producing pigments, most notably {pigment_blue} (which is blue) and {pigment_green} (which is yellow-green).")
    print(f"Step 3: The mixture of the blue pigment and the yellow-green pigment gives the bacterial culture its characteristic {final_color} appearance.")
    print("Step 4: The procedure involves washing and concentrating the cells. This creates a dense pellet of bacteria.")
    print("Step 5: Even after washing away the liquid medium, a significant amount of pigment remains associated with the cells themselves.")
    print(f"Conclusion: Therefore, the dense, concentrated sample of {organism} will be {final_color}.")
    print("\nFinal Answer Choice: C")

explain_pseudomonas_color()