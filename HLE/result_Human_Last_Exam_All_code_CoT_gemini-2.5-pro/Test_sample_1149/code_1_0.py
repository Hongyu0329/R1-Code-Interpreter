# Based on the description by Sues et al. (2002), the lacrimal recess
# of Irritator challengeri is subdivided into two distinct depressions.

# Number of smaller, anterodorsal depressions
anterodorsal_depressions = 1

# Number of larger, posteroventral depressions
posteroventral_depressions = 1

# Calculate the total number of depressions
total_depressions = anterodorsal_depressions + posteroventral_depressions

# Print the result with the full equation
print(f"The lacrimal recess in Irritator challengeri is subdivided into an anterodorsal depression and a posteroventral depression.")
print(f"Therefore, it contains a total of {total_depressions} depressions.")
print(f"Equation: {anterodorsal_depressions} + {posteroventral_depressions} = {total_depressions}")