def get_perdita_common_name_info():
    """
    This function provides the common name and year it was coined for the bee genus Perdita.
    
    Research indicates the common name "Fairy bees" was proposed for the genus Perdita
    by the entomologist Jerome G. Rozen, Jr. in an issue of the entomological newsletter 'Sphecos'.
    """
    
    # The common name for the genus Perdita
    common_name = "Fairy bees"
    
    # The year the name was proposed in the newsletter "Sphecos" (No. 13, April 1987)
    year_coined = 1987
    
    # Format and print the output string as requested
    print(f"{common_name}, {year_coined}")

get_perdita_common_name_info()