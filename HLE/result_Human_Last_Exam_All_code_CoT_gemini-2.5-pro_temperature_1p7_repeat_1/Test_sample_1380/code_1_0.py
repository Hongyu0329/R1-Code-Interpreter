def get_bachelier_discovery():
    """
    This function identifies the physical process that Louis Bachelier discovered
    was mathematically analogous to financial market behavior.
    """
    # In his 1900 thesis "Théorie de la spéculation," Louis Bachelier modeled
    # the stochastic nature of stock prices.
    # This mathematical model was identical to that of the random motion of
    # particles suspended in a fluid, a phenomenon known as Brownian motion.
    # His work on this predates Einstein's famous paper on the subject by five years.
    process_name = "Brownian motion"
    
    print(f"The physical process Louis Bachelier described mathematically is known as: {process_name}")

if __name__ == "__main__":
    get_bachelier_discovery()