def solve_equation():
    """
    This function prints the name and famous equation for Max Born.
    """
    name = "Max Born"
    # The Born rule gives the probability density as the square of the wave function's magnitude.
    # The symbol Ψ (Psi) is used for the wave function, consistent with the Schrödinger example.
    # The equation includes the number 2 as the exponent.
    equation = "|Ψ|²"

    print(f"Name: {name}")
    print(f"Equation: \\( {equation} \\)")

solve_equation()