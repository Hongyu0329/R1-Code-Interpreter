def solve():
    """
    Determines and prints the correct grammatical order of Old Russian enclitics.
    """
    # The enclitics in their correct grammatical order
    # 1. бы (conditional particle)
    # 2. же (intensifying particle)
    # 3. бо (conjunctional particle)
    # 4. мя (pronominal form, accusative)
    # 5. еси (verbal form, 2nd person singular 'are')
    ordered_enclitics = ["бы", "же", "бо", "мя", "еси"]

    # Print the final ordered sequence
    print("The correct order of the enclitics is:")
    print(", ".join(ordered_enclitics))

solve()
