def get_cited_lecturer():
  """
  This function provides the name of the scientist whose lectures and work
  on statistical mechanics were foundational to Albert Einstein's 1905 paper
  on Brownian motion.
  """
  # The key figure is Ludwig Boltzmann, a pioneer of statistical mechanics and the kinetic theory of gases.
  lecturer_name = "Ludwig Boltzmann"

  print("In his 1905 paper on Brownian motion, Albert Einstein's work was deeply rooted in the foundational principles of statistical mechanics established by:")
  print(lecturer_name)

get_cited_lecturer()