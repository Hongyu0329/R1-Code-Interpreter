def find_french_equivalent():
    """
    This function provides the French equivalent for the name Odobeccus.
    """
    gaulish_name = "Odobeccus"
    # The name Odobeccus is a Latinized form of a Germanic (Frankish) name.
    # The root 'Odo-' is equivalent to 'Eudes' in French.
    french_equivalent = "Eudes"

    print(f"The French equivalent of the name '{gaulish_name}' is '{french_equivalent}'.")

find_french_equivalent()