# Define the Chern numbers of the two individual insulators
C1 = 1
C2 = 1

# The Chern number of the junction is the sum of the individual Chern numbers
C_junction = C1 + C2

# Print the final equation showing how the result is obtained
print(f"The Chern number of the junction is the sum of the individual Chern numbers.")
print(f"C_junction = C1 + C2")
print(f"C_junction = {C1} + {C2} = {C_junction}")