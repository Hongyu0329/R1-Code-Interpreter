# The question is whether Immanuel Kant's account of aesthetic judgment in the
# Critique of Judgment is purely descriptive or also normative.
# The analysis concludes that the account has both aspects.
# It is descriptive in that it details the cognitive mechanics of making a judgment of taste.
# It is normative in that it sets conditions (e.g., disinterestedness) for a *valid* judgment
# and posits that a valid judgment makes a universal claim, implying that others *ought* to agree.
# Therefore, the account is not purely descriptive.

final_answer = "The account is both descriptive and normative."

print(final_answer)