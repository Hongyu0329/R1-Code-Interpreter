def find_bacterium_genus():
    """
    This function provides the name of a bacterium genus known to produce
    a compound that exhibits triboluminescent properties.
    """
    # The bacterium Photorhabdus luminescens produces the enzyme luciferase.
    # When purified and crystallized, this luciferase compound exhibits triboluminescence
    # (emits light when crushed).
    genus = "Photorhabdus"
    
    # Print the explanation and the final answer.
    print("The genus of bacterium that produces a chemical compound (luciferase) that exhibits triboluminescent properties is:")
    print(genus)

# Execute the function to display the answer.
find_bacterium_genus()