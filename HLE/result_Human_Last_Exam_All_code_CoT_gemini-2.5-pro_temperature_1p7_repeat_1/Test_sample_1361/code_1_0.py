# This script calculates the total number of gargouillades performed by Darcey Bussell
# in her Act III solo from the 2005 ROH production of 'Sylvia'.

# Based on viewing the performance, she performs a sequence of gargouillades on one side, then repeats it on the other.
# Number of gargouillades in the first sequence.
gargouillades_first_set = 4

# Number of gargouillades in the second sequence.
gargouillades_second_set = 4

# Calculate the total.
total_gargouillades = gargouillades_first_set + gargouillades_second_set

print(f"Darcey Bussell performed a total of {gargouillades_first_set} + {gargouillades_second_set} = {total_gargouillades} gargouillades.")