def find_scunthorpe_anthem():
  """
  This function identifies and prints the song played at Scunthorpe United's home games.
  """
  # The club is nicknamed "The Iron" because of the town's steel-making history.
  # This makes the song "Iron Man" by Black Sabbath the logical and correct choice.
  correct_song = "Iron Man"
  correct_artist = "Black Sabbath"
  
  print(f"The song played just before kick-off at every Scunthorpe United Football Club home game is: {correct_song} - {correct_artist}")

find_scunthorpe_anthem()