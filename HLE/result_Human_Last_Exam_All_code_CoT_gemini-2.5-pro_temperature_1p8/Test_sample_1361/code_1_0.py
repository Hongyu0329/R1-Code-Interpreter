# The famous Pizzicato solo from Act III of "Sylvia" features two
# distinct sequences of gargouillade jumps.

# Number of gargouillades in the first sequence
first_sequence_gargouillades = 4

# Number of gargouillades in the second sequence
second_sequence_gargouillades = 4

# Calculate the total number of gargouillades
total_gargouillades = first_sequence_gargouillades + second_sequence_gargouillades

# Print the final equation showing the breakdown and the total
print(f"The total number of gargouillades is the sum of the two main sequences.")
