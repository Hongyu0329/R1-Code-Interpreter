# This script calculates the sum of the number of letters 
# in the words "doppelgänger" and "syndrome".

# Define the two words from the condition's name.
word1 = "doppelgänger"
word2 = "syndrome"

# Get the length of each word.
len1 = len(word1)
len2 = len(word2)

# Calculate the sum of the lengths.
total_len = len1 + len2

# As requested, print the final equation including each number.
print(f"{len1} + {len2} = {total_len}")