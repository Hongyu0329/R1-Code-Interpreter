def find_hydrocarbon_name():
    """
    This function identifies and prints the name of a polycyclic hydrocarbon
    named after a creature that went extinct over 65 million years ago.
    """
    # The creature group that has been extinct for over 65 million years
    creature_group = "Dinosaur"
    
    # The relevant time period mentioned in the question
    time_period = "65 million years"
    
    # The name of the polycyclic hydrocarbon
    hydrocarbon_name = "Dino-sterane"
    
    # Print the information in a structured way
    print(f"The creature that has been extinct for over {time_period} is the {creature_group}.")
    print(f"The polycyclic hydrocarbon named after it is called:")
    print(f"--> {hydrocarbon_name}")
    print("\nExplanation: Dino-steranes are complex molecular fossils (biomarkers) found in rocks. They are derived from the steroids of organisms that lived during the age of dinosaurs.")

# Execute the function to display the answer
find_hydrocarbon_name()