# This script calculates the total number of cabrioles devants performed by
# Maria Khoreva in her Act I variation from the 2017 Paquita production.

# By observing the choreography, we can identify two sequences containing the step.

# 1. Count the cabrioles in the first diagonal sequence of jumps.
first_sequence_count = 2

# 2. Count the cabrioles in the second diagonal sequence of jumps.
second_sequence_count = 2

# 3. Calculate the total by summing the counts from both sequences.
total_count = first_sequence_count + second_sequence_count

# 4. Print the final equation and the result.
# The result shows the breakdown of the count from each part of the performance.
print(f"The calculation for the total number of cabrioles devants is:")
print(f"{first_sequence_count} + {second_sequence_count} = {total_count}")