# Step 1: Define the numbers from the equation.
# 'сеп' is interpreted as 7.
# '𒌋𒌋𒌋' is cuneiform for 30 (10+10+10).
# '𒌋𒌋𒁹𒁹𒁹' is cuneiform for 23 (10+10+1+1+1).
num1 = 7
num2 = 30
num3 = 23

# Step 2: Perform the calculation.
result = (num1 / num2) - num3

# Step 3: Print the equation with modern numbers and the final answer.
print(f"The equation in modern numbers is:")
print(f"{num1} / {num2} - {num3} = {result}")
