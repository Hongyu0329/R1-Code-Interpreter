def find_imprint_numbers():
    """
    This function identifies and prints the different numerical imprints on
    Teva brand fluoxetine 10mg and 20mg capsules.
    """
    imprint_10mg = "3105"
    imprint_20mg = "3106"

    print(f"The Teva brand 10mg fluoxetine capsule is imprinted with the number {imprint_10mg}.")
    print(f"The Teva brand 20mg fluoxetine capsule is imprinted with the number {imprint_20mg}.")

find_imprint_numbers()