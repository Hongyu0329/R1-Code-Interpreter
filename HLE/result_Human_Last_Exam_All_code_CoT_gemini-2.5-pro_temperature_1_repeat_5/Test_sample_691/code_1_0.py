def solve_topology_problem():
    """
    This function explains the step-by-step solution to the topology problem.
    """
    print("Step 1: Determine the topology of the sewn pants.")
    g_pants = 0
    n_pants = 3
    chi_pants = 2 - 2 * g_pants - n_pants
    print(f"A single pair of pants is a surface of genus g={g_pants} with n={n_pants} boundaries.")
    print(f"Its Euler characteristic is χ = 2 - 2*g - n = {chi_pants}.")

    num_pants = 2
    chi_sewn = chi_pants * num_pants
    print(f"\nSewing {num_pants} pants together gives a surface S with Euler characteristic χ(S) = {chi_pants} + {chi_pants} = {chi_sewn}.")

    n_sewn = 2
    chi_closed = chi_sewn + n_sewn
    # For a closed surface, chi = 2 - 2g. So, g = 1 - chi/2.
    g_sewn = 1 - chi_closed // 2
    print(f"This surface S has {n_sewn} boundaries (the waistbands).")
    print(f"Capping these boundaries gives a closed surface S' with χ(S') = {chi_sewn} + {n_sewn} = {chi_closed}.")
    print(f"A closed surface with χ = {chi_closed} is a torus, which has genus g = {g_sewn}.")
    print(f"Therefore, the space S is a torus with {n_sewn} holes.")

    print("\nStep 2: Determine the fundamental group of S.")
    # The fundamental group of a surface of genus g with n boundaries is a free group
    # on 2g + n - 1 generators.
    num_generators = 2 * g_sewn + n_sewn - 1
    print(f"The number of generators for the fundamental group of S is 2*g + n - 1 = 2*{g_sewn} + {n_sewn} - 1 = {num_generators}.")
    print("The group π₁(S) has the presentation <a, b, c₁, c₂ | [a,b]c₁c₂ = 1>.")
    print("Here, 'a' and 'b' are the torus generators, and 'c₁', 'c₂' are loops around the two boundaries (waistbands).")

    print("\nStep 3: Apply the final operation.")
    print("Identifying the waistbands to a point collapses the boundary loops c₁ and c₂.")
    print("This adds the relations c₁ = 1 and c₂ = 1 to the group.")

    print("\nStep 4: Calculate the final fundamental group.")
    print("Original relation: [a,b]c₁c₂ = 1")
    rel_c1 = 1
    rel_c2 = 1
    print(f"Substituting c₁ = {rel_c1} and c₂ = {rel_c2} gives: [a,b]({rel_c1})({rel_c2}) = 1")
    print("This simplifies to [a,b] = 1, which is the commutation relation ab = ba.")
    print("The resulting group is generated by 'a' and 'b' with the relation ab = ba.")
    print("This is the group ℤ × ℤ.")

    # The final equation is Z x Z. We output the symbols.
    final_equation_part_1 = "ℤ"
    final_equation_part_2 = "ℤ"
    print(f"\nFinal Equation: π₁(X) = {final_equation_part_1} × {final_equation_part_2}")


solve_topology_problem()
