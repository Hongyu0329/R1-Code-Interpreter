def find_milyukovs_criticism():
    """
    This function identifies and prints the term Pavel Milyukov used to
    criticize a cultural movement he described as a 'stagnant swamp'.
    """
    # Pavel Milyukov, a prominent Russian historian and liberal politician,
    # was a staunch critic of what he saw as the decline of Western culture.
    # He specifically targeted the modernist art movements of his time.
    x = "Decadents"
    y = "Symbolists"

    # He grouped these two related movements together in his condemnations.
    print(f"The term X-Y that Pavel Milyukov denounced is: {x}-{y}")

find_milyukovs_criticism()