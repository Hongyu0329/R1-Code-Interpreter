# Define the Chern number for each of the two insulators.
C1 = 1
C2 = 1

# The Chern number of a junction of two topological insulators is the sum of their individual Chern numbers.
C_total = C1 + C2

# Display the calculation and the result.
# The final line shows the full equation with the numbers substituted.
print("The Chern number of the first insulator is C1 = 1")
print("The Chern number of the second insulator is C2 = 1")
print("The total Chern number of the junction is C_total = C1 + C2")
print(f"Therefore, the calculation is: {C1} + {C2} = {C_total}")
print(f"The Chern number of the junction is: {C_total}")
