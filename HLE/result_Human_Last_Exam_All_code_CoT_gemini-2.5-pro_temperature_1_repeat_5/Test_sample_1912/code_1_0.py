# Define the numbers for the final equation in the sequence.
a = 6
b = 12

# The problem follows a conditional pattern based on the relationship between a and b.
# For this specific problem, b is twice a (12 = 2 * 6).
# The discovered pattern for this case is: a * b + a - 2.
result = a * b + (a - 2)

# Print the final equation, showing each number and the result.
print(f"{a} + {b} = {result}")