def solve():
    """
    This function prints the name and famous equation of Max Born.
    """
    name = "Max Born"
    # The Born rule states that the probability density of finding a particle at a given point
    # is proportional to the square of the magnitude of the particle's wave function at that point.
    equation_text = r"P = |\Psi|^2"

    print(f"Name: {name}")
    print(f"Equation: {equation_text}")

solve()