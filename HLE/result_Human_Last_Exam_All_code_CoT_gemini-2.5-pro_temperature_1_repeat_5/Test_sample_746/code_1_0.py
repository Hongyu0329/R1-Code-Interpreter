# Let X be a compact connected metric space and D be its set of dispersion points.
# The goal is to find the maximum possible cardinality of D.

# A proof based on the Boundary Bumping Theorem shows that |D| cannot be greater than 1.
# The argument proceeds by assuming two distinct dispersion points, p and q,
# and shows this leads to the contradiction that p = q.
# Therefore, the maximum cardinality is at most 1.

# Furthermore, examples of such spaces exist. The Knaster-Kuratowski fan
# is a classic example of a compact connected metric space with exactly one
# dispersion point.
# This demonstrates that a cardinality of 1 is achievable.

# Combining these two facts, the maximum cardinality must be 1.
# We can express this conclusion as an equation.
maximum_cardinality = 1

# The final equation is: Maximum Cardinality = 1.
# The following code prints the number in this equation.
print(maximum_cardinality)