def find_cited_lecturer():
    """
    This function provides the answer to the historical question about
    Einstein's 1905 paper on Brownian motion.
    """
    # The scientist whose published lectures were a major foundation for Einstein's paper.
    scientist_name = "Ludwig Boltzmann"

    # The title of the lectures.
    lecture_title = "Lectures on Gas Theory"

    # In his 1905 paper on Brownian motion, "On the movement of small particles
    # suspended in a stationary liquid, as required by the molecular-kinetic
    # theory of heat," Albert Einstein's work was deeply rooted in the principles
    # of statistical mechanics. He had closely studied the published lectures of
    # the Austrian physicist Ludwig Boltzmann, which provided the theoretical
    # foundation for his own analysis.
    print("In his 1905 paper on Brownian motion, Albert Einstein's analysis was fundamentally based on the work presented in the published lectures of:")
    print(f"Scientist: {scientist_name}")
    print(f"Title of published lectures: '{lecture_title}'")

# Execute the function to display the answer.
find_cited_lecturer()