# The space of digitary functions is the set of functions f(x) = ax + b.
# This space is spanned by two basis vectors:
# 1. The constant function f_1(x) = 1 (when a=0, b=1)
# 2. The identity function f_2(x) = x (when a=1, b=0)
# Any linear combination of these two functions is another function of the form ax+b.
# The basis functions {1, x} are linearly independent.
# Therefore, the dimension of the vector space is 2.

dimension = 2
print(f"The dimension of the vector space of digitary functions is:")
print(dimension)