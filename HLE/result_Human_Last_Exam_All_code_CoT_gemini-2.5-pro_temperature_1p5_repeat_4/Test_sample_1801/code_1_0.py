def find_enron_laureates():
    """
    This function identifies and prints the names of the two Nobel laureates
    featured in the 2000 Enron 'Ask Why' commercial.
    """
    # Paul Krugman won the Nobel Memorial Prize in Economic Sciences in 2008.
    laureate_1 = "Paul Krugman"

    # Myron Scholes won the Nobel Memorial Prize in Economic Sciences in 1997.
    laureate_2 = "Myron Scholes"

    print(f"The two Nobel laureates featured in the infamous 2000 Enron 'Ask Why' commercial were {laureate_1} and {laureate_2}.")

# Execute the function to display the answer
find_enron_laureates()