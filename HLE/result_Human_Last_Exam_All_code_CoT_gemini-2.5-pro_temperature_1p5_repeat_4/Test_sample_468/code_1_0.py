def find_bee_common_name_info():
    """
    This function provides the common name for the bee genus Perdita
    and the year the name was coined.

    The information is based on historical entomological publications.
    """
    # The common name for the genus "Perdita", as coined by T.D.A. Cockerell.
    common_name = "Fairy Bees"

    # The year the name was coined in "The American Naturalist" journal.
    # The four numbers of the year are 1, 8, 9, and 6.
    year_coined = 1896

    # Print the result in the specified format: "<name>, <year>"
    print(f"{common_name}, {year_coined}")

find_bee_common_name_info()