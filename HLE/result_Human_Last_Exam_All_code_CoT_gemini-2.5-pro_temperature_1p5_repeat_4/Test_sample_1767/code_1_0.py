# Define the Chern numbers of the two individual insulators.
C1 = 1
C2 = 1

# The Chern number of a junction of two topological systems is the
# sum of their individual Chern numbers.
C_junction = C1 + C2

# Print the calculation and the final result.
# The final equation shows each number involved in the sum.
print(f"The Chern number of the junction is C_junction = C1 + C2 = {C1} + {C2} = {C_junction}")