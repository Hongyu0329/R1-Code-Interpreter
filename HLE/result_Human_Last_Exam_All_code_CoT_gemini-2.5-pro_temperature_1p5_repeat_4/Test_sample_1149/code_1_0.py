# The scientific paper describing the skull of Irritator challengeri
# (Sues et al., 2002) notes that the lacrimal recess is subdivided.

# Number of larger, dorsal depressions in the lacrimal recess.
dorsal_depression = 1

# Number of smaller, ventral depressions in the lacrimal recess.
ventral_depression = 1

# Calculate the total number of depressions.
total_depressions = dorsal_depression + ventral_depression

print("Based on the description of the Irritator challengeri skull, the lacrimal recess contains a specific number of depressions.")
print(f"The total is the sum of a dorsal depression and a ventral depression: {dorsal_depression} + {ventral_depression} = {total_depressions}")
print(f"Therefore, the lacrimal recess in Irritator challengeri contains {total_depressions} depressions.")
