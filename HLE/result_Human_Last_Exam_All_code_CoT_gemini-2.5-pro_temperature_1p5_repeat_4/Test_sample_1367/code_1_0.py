def solve_vertex_algebra_questions():
    """
    This function provides answers to theoretical questions about the vertex algebra V(p).
    """

    # Part (a): Is V(p) simple for all p in Z_>=1?
    # The vertex algebra V(p), for integer p > 1, is known to contain singular vectors
    # that generate proper non-trivial ideals. Therefore, it is not simple for all p.
    answer_a = "No"

    # Part (b): If a non-trivial ideal exists, what condition must be met?
    # By definition, an ideal I in V(p) must be a module over any subalgebra, including L_k(sl_2).
    # Furthermore, non-trivial ideals in such vertex algebras are generated by singular vectors.
    # Thus, any non-trivial ideal must contain a singular vector. Both conditions are necessary.
    answer_b = "both"

    # Part (c): Does the simplicity of V(p) imply that it is also irreducible as an L_k(sl_2)-module?
    # Simplicity of V(p) means it is irreducible as a module over itself. However, it can still
    # be reducible as a module over a subalgebra like L_k(sl_2). It's possible for V(p) to decompose
    # into a sum of multiple irreducible L_k(sl_2)-modules.
    answer_c = "No"

    # Construct the final answer string in the required format.
    final_answer = f"(a) {answer_a}; (b) {answer_b}; (c) {answer_c}"

    print(final_answer)

if __name__ == "__main__":
    solve_vertex_algebra_questions()