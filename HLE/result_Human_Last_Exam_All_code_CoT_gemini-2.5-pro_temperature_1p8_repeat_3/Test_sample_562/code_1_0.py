# The task is to find the name of the bushu (radical) for the Japanese character '謄'.
# The character is '謄'.
# This character is composed of a left part and a right part.
# The radical is the left part: 言
# The name for the 言 radical when it's on the left side of a character is ごんべん (gonben).
# The Hepburn romanization for ごんべん is "Gonben".
# This script will print the final answer.

answer = "Gonben"
print(answer)