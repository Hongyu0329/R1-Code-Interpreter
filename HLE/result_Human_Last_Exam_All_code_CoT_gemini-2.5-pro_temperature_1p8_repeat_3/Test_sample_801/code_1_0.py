# A simple script to sum the number of letters in the Kalabari titles
# from the multiple-choice options.

# Number of letters in 'Amanyanabo'
num1 = 10

# Number of letters in 'Alabo'
num2 = 5

# Number of letters in 'Asawo'
num3 = 5

# Number of letters in 'Opu asawo'
num4 = 8

# Calculate the sum
total = num1 + num2 + num3 + num4

# Print the final equation with the result
print(f"The total number of letters is: {num1} + {num2} + {num3} + {num4} = {total}")