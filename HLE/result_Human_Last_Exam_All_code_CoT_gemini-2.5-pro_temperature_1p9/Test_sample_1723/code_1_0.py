def get_mushroom_genus():
    """
    This function provides the name of the single genus of mushroom
    known to produce a distinctly blue spore print.
    """
    # The genus Entoloma is known for its generally pinkish spore prints.
    # However, it is the genus most frequently cited as having species
    # (e.g., in the subgenus Leptonia) that can produce spores with
    # distinct blue, blue-green, or violaceous tints, a unique feature.
    genus_name = "Entoloma"
    print(f"The name of the single genus of mushroom known to produce a distinctly blue spore print is: {genus_name}")

get_mushroom_genus()