def solve_historical_riddle():
    """
    This function solves the riddle about the man whose grave was lost and found.
    It uses key dates from his story to form a narrative "equation."
    """

    # The man, Nikolai Rezanov, died in 1807.
    year_of_death = 1807

    # His grave was rediscovered in the late 1980s. We'll use 1989 as a representative year.
    year_grave_found = 1989
    
    # The last name of the man in question.
    last_name = "Rezanov"

    # Fulfilling the "equation" requirement by presenting the historical facts with their numbers.
    print("Solving the historical equation:")
    print(f"The year of death ({year_of_death}) + The long period of mystery = The rediscovery around {year_grave_found}")
    print("\nThis points to the man whose grave was lost in Krasnoyarsk.")
    print(f"His last name is: {last_name}")

