# The Chern numbers for the two individual insulators.
C1 = 1
C2 = 1

# When two Chern insulators are joined, the Chern number of the resulting
# junction is the sum of their individual Chern numbers.
C_total = C1 + C2

# Display the calculation and the final answer.
print(f"The total Chern number of the junction is the sum of the individual Chern numbers.")
print(f"C_total = {C1} + {C2}")
print(f"C_total = {C_total}")