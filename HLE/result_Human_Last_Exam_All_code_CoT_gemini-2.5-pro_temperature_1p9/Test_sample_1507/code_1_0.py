# The outer hull of a great icosahedron is a regular icosahedron.
# A regular icosahedron is a Platonic solid with 20 faces, all of which are triangles.
# Therefore, the minimal number of triangles to triangulate its surface is simply its number of faces.

num_faces_of_icosahedron = 20

# The equation is straightforward as no calculation is needed.
# We present the final number clearly.
print(f"The minimal number of triangles needed is: {num_faces_of_icosahedron}")