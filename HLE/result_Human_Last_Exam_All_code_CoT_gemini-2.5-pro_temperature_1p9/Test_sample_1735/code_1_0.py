def solve():
    """
    This function prints the letters of the true statements in sorted order.
    The analysis of each statement is provided above.
    """
    true_statements = ['A', 'C', 'H', 'J']
    # Sorting the letters for the final output format.
    true_statements.sort()
    print(", ".join(true_statements))

solve()
