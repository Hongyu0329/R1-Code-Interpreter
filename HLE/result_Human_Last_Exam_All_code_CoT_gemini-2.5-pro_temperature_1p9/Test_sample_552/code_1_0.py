def find_influential_lecturer():
    """
    Identifies the physicist whose published lectures were a key influence
    on Einstein's 1905 Brownian motion paper.
    """
    # While Einstein did not formally cite lectures in the 1905 paper, his work
    # was heavily based on the principles of statistical mechanics which he studied
    # from the published "Lectures on Gas Theory" by this scientist.
    influential_physicist = "Ludwig Boltzmann"

    print("Albert Einstein's work on Brownian motion was deeply influenced by the published lectures of:")
    print(influential_physicist)

if __name__ == "__main__":
    find_influential_lecturer()