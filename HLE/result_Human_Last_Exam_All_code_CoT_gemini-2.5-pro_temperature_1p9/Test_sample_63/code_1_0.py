def solve_riddle():
  """
  This function solves the riddle by printing the name that fits the criteria.
  
  The criteria are:
  1. The character can be described as looking like a porcupine.
  2. The name consists of two words that start with the same letter.
  
  The solution is Donald Duck, as his feathers stand on end like quills when he's angry,
  and his name fits the "D. D." pattern.
  """
  first_name = "Donald"
  last_name = "Duck"
  
  print(f"The name of the character is: {first_name} {last_name}")

solve_riddle()