# The number of regular rigid indecomposable modules in the exceptional tubes
# is the sum of the ranks of these tubes.
# The ranks are given by the lengths of the paths in the quiver description.
path_length_1 = 2
path_length_2 = 3

# Calculate the total number
total_modules = path_length_1 + path_length_2

# Print the final equation and the result
print(f"{path_length_1} + {path_length_2} = {total_modules}")