    """
    Identifies and prints the genus of a bacterium that produces a triboluminescent compound.
    """
    # The genus of bacterium known for producing the enzyme luciferase,
    # which can exhibit triboluminescence when crystallized and crushed.
    genus = "Photorhabdus"
    
    # The chemical compound is an enzyme.
    compound = "luciferase"

