def explain_optical_rotation_phenomenon():
    """
    This function explains the visual phenomenon that occurs when white light
    is passed through a D-glucose solution in a long tube.
    """

    # --- Problem Setup ---
    print("Step 1: Understanding the setup.")
    print(" - We have a beam of white light, which contains all colors of the rainbow.")
    print(" - This light passes through a 1-meter long tube filled with a D-glucose solution.")
    print(" - D-glucose is an 'optically active' or 'chiral' molecule.")
    print(" - We are observing the tube from the side.")
    print("-" * 20)

    # --- Relevant Physics ---
    print("Step 2: Applying key physics principles.")
    print(" - Principle of Optical Activity: Chiral molecules like glucose rotate the plane of polarized light.")
    print(" - Principle of Optical Rotatory Dispersion (ORD): The angle of rotation depends on the color (wavelength) of the light. Shorter wavelengths (like blue) are rotated more than longer wavelengths (like red).")
    print(" - The total rotation angle increases as the light travels further down the tube.")
    print("-" * 20)

    # --- Conclusion ---
    print("Step 3: Deducing the visual appearance.")
    print(" - As the white light beam travels down the tube, the plane of polarization for each color is rotated at a different rate.")
    print(" - The plane for blue light rotates fastest, green is in the middle, and red rotates slowest.")
    print(" - An observer at the side sees light that is scattered from the main beam.")
    print(" - The specific color observed from any point along the tube depends on the rotation angle of that color's polarization plane at that point.")
    print(" - Because this rotation happens continuously around the central axis of the tube, the bands of color will appear to form a helix or spiral.")
    print("\nFinal Result: The tube will have a rainbow gradient progressing in a spiral.")

explain_optical_rotation_phenomenon()