def find_hydrocarbon():
    """
    This function identifies and prints the name of a polycyclic hydrocarbon
    named after a creature that went extinct approximately 65 million years ago.
    """
    # The creature is the Pterodactyl, and the molecule's shape resembles it.
    hydrocarbon_name = "Pterodactyladiene"
    
    print(f"The polycyclic hydrocarbon named after a creature that has been extinct for over 65 million years is: {hydrocarbon_name}")

if __name__ == "__main__":
    find_hydrocarbon()