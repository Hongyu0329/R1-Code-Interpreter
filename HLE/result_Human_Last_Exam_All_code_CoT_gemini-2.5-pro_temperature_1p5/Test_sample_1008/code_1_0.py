# This script defines and prints the hierarchy of sound importance for a dance scene in film.
# The ranking is based on which element is most fundamental to the scene's purpose.

# 1. Music is the most important as it provides the foundation for the choreography and mood.
# 2. Sound Effects add realism and texture to the physical performance.
# 3. Speech is typically the least important as the story is told through movement.
sound_hierarchy = ["Music", "Sound Effects", "Speech"]

# We will join the elements of the list with a comma and a space for clear output.
final_ranking = ", ".join(sound_hierarchy)

# Print the final ranked order.
print(final_ranking)