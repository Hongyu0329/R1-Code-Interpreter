# This script presents the solution to the literary riddle about Father Brown.

def find_place_names():
    """
    This function stores and prints the two place names from the Father Brown story
    "The Secret of Flambeau" that match the user's criteria.
    """

    # The two place names mentioned by Father Brown that begin with paired consonants.
    place_name_1 = "Troy"  # Begins with "Tr"
    place_name_2 = "Clapham Junction"  # "Clapham" begins with "Cl"

    print("The two place names mentioned by Father Brown are:")
    print(f"1. {place_name_1}")
    print(f"2. {place_name_2}")

# Execute the function to print the answer.
find_place_names()