import math

# The value alpha is the golden ratio phi.
# The equation for phi is alpha = (1 + sqrt(5)) / 2
one = 1
five = 5
two = 2
alpha = (one + math.sqrt(five)) / two

# Print the equation with the numbers and the final result.
print(f"The value of alpha is determined by the equation: alpha = ({one} + sqrt({five})) / {two}")
print(f"alpha = {alpha}")
