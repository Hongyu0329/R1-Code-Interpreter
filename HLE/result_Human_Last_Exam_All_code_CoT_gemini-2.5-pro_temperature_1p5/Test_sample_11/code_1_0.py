def find_first_statute():
  """
  This function identifies and prints the first statute in the modern State of Israel
  to explicitly introduce the concept of "good faith".
  """
  # The Contracts (General Part) Law, 1973 is widely recognized by legal scholars
  # as the first major Israeli statute to explicitly and generally introduce the
  # principle of "good faith" (tom lev) into Israeli law, particularly in its
  # famous sections 12 (good faith in negotiation) and 39 (good faith in performance).
  statute_name = "Contracts (General Part) Law"
  print(statute_name)

find_first_statute()