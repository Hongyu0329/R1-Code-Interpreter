def solve_chess_puzzle():
    """
    This function prints the sequence of moves for the chess puzzle.
    The puzzle is a variation of Philidor's Legacy.
    """
    # The correct sequence of moves leading to a smothered checkmate.
    # E. 1... Nf2+ 2. Kg1 Nh3+ 3. Kh1 Qg1+ 4. Rxg1 Nf2#
    mate_sequence = "1... Nf2+ 2. Kg1 Nh3+ 3. Kh1 Qg1+ 4. Rxg1 Nf2#"
    print(mate_sequence)

solve_chess_puzzle()