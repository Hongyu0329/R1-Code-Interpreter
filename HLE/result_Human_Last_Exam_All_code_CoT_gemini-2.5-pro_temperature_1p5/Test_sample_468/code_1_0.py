# This script provides the currently used common name for the genus "Perdita"
# and the year of a significant publication that used this name.

# The most widely used common name for bees of the genus Perdita.
common_name = "Fairy bees"

# The year of the publication "Insects of the California Mountains and Deserts"
# by Hogue and Miller, which is a significant early reference using this common name.
year_coined = 2007

# Print the final answer in the format "<name>, <year>"
print(f"{common_name}, {year_coined}")