# The user wants to identify a Chinese character based on a riddle.
# Let's break down the riddle's clues:

# 1. "One horizontal stroke, another horizontal stroke, after another;"
# This suggests multiple horizontal strokes.

# 2. "one vertical stroke, another vertical stroke, after another;" (mentioned twice)
# This suggests multiple vertical strokes.

# 3. "one vertical on the left, one vertical on the right;"
# This clearly describes two vertical strokes forming a frame.

# By combining these clues, we can deduce the character.
# The character that fits this description is 冊 (cè).
# Visually, it represents ancient bamboo slips (the vertical strokes)
# tied together with strings (the horizontal strokes), which were used as books.
# The outer two strokes match the "vertical on the left, one vertical on the right" clue.

# Therefore, the code will print this character.
final_character = "冊"

print("The Chinese character described by the riddle is:")
print(final_character)