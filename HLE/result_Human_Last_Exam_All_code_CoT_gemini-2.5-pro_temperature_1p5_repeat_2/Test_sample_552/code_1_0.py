def find_cited_lecturer():
  """
  This function provides the name of the scientist whose work and lectures 
  were a major influence on Einstein's 1905 Brownian motion paper.
  """
  # The question asks whose lectures were cited. While direct citation of "lectures"
  # is less common in papers than citing published works, the paper builds heavily
  # on the foundations of statistical mechanics.
  # The key figure in this field whose work, disseminated through publications and lectures,
  # was fundamental to Einstein's paper is Ludwig Boltzmann.
  scientist_name = "Ludwig Boltzmann"
  
  print(f"In his 1905 paper on Brownian motion, Albert Einstein's work was heavily based on the theoretical framework of statistical mechanics developed by: {scientist_name}.")

find_cited_lecturer()