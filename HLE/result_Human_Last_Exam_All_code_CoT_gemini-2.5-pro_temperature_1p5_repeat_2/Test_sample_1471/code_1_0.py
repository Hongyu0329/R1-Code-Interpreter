# The equation is X / C - CMXCIX = ?
# First, let's define the values of the Roman numerals in modern numbers.
x = 10
c = 100
cmxcix = 999

# Now, let's perform the calculation following the order of operations (division then subtraction).
result = (x / c) - cmxcix

# Finally, let's print the full equation with the solution.
print(f"The equation {x} / {c} - {cmxcix} results in:")
print(result)