# This script answers the user's question about the common name for the bee genus "Perdita".
# The information is based on research into entomological literature.
# The name "Fairy Bees" was coined or at least widely popularized by entomologist Jerome G. Rozen, Jr.
# One of its earliest prominent appearances in print, cementing its use,
# is in Thomas Eisner's 2003 book "For the Love of Insects," where he attributes the name to Rozen.

# The common name for the genus "Perdita".
common_name = "Fairy Bees"

# The year the name was notably published and popularized.
year = 2003

# Phrasing the answer as "<name>, <year>"
print(f"{common_name}, {year}")