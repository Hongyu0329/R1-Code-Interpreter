def find_french_equivalent():
    """
    This function provides the French equivalent for the historical name 'Odobeccus'.
    """
    gaulish_name = "Odobeccus"
    # The name is often rendered as Edobichus in Latin, and its direct
    # Francization used by historians is "Édobic".
    french_equivalent = "Édobic"

    print(f"The historical French equivalent of the name '{gaulish_name}' is '{french_equivalent}'.")

find_french_equivalent()