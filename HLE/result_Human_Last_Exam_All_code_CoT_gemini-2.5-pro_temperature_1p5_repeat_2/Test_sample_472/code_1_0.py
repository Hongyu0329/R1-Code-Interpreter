# This script prints the scansion of the provided Latin line.
# The analysis and breakdown into feet are explained above.
# L = Long syllable, S = Short syllable

foot_1 = "L S S"
foot_2 = "S L"
foot_3 = "L L"
foot_4 = "L L"
foot_5 = "S S L"
foot_6 = "S S"

