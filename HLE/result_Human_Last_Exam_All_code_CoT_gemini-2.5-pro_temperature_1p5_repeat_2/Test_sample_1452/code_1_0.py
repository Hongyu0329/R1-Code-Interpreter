#!/usr/bin/env python3

def find_born_equation():
    """
    This function prints the famous equation associated with Max Born.
    """
    name = "Max Born"
    
    # The Born Rule: P is the probability density, Ψ is the wave function.
    # The number '2' is the exponent.
    equation = "P = |Ψ|^2"
    
    print(f"Name: {name}")
    print(f"Equation: {equation}")

if __name__ == "__main__":
    find_born_equation()
