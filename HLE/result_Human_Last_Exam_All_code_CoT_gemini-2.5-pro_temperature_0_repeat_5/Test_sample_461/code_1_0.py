def find_dinosaur_chemical():
    """
    This function identifies and prints the name of a polycyclic hydrocarbon
    named after a creature that went extinct over 65 million years ago.
    """
    # The name of the polycyclic hydrocarbon
    chemical_name = "Pterodactyladiene"
    
    # The creature it is named after
    creature_name = "Pterodactyl"
    
    # The extinction period mentioned in the question
    extinction_years_ago = 65
    
    # Print the final answer
    print(f"The polycyclic hydrocarbon named after a creature ({creature_name}) that has been extinct for over {extinction_years_ago} million years is called: {chemical_name}")

find_dinosaur_chemical()