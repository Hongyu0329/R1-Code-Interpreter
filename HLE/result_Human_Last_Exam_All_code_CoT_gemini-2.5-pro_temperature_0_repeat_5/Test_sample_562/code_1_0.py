def find_radical_name():
    """
    This function identifies the radical for the kanji 謄 and prints its name in Hepburn romanization.
    """
    kanji_character = "謄"
    # The radical for 謄 is 言, which is called "gonben" when it's on the left side.
    radical_name_hepburn = "Gonben"
    print(f"The radical for the character '{kanji_character}' is called: {radical_name_hepburn}")

find_radical_name()