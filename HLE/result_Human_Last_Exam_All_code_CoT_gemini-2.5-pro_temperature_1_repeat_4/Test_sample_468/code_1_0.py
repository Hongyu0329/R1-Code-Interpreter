def get_bee_common_name_info():
  """
  Provides the common name and year of popularization for the bee genus Perdita.
  """
  # The common name "fairy bees" is widely used for the genus Perdita due to their small size.
  common_name = "fairy bees"

  # The name was significantly popularized and established in the influential 2014 book
  # "California Bees and Blooms: A Guide for Gardeners and Naturalists".
