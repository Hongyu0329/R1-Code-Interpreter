def find_french_equivalent():
    """
    This function provides the French equivalent for the root of the name 'Odobeccus'.
    """
    # The name 'Odobeccus' is a Latinized Frankish name.
    original_name = "Odobeccus"
    
    # The primary root of the name is 'Odo'.
    name_root = "Odo"
    
    # The French equivalent of the root 'Odo' is 'Eudes'.
    french_equivalent = "Eudes"
    
    print(f"The name '{original_name}' is of Frankish origin.")
    print(f"Its main root, '{name_root}', evolved into the French name '{french_equivalent}'.")
    print(f"Therefore, the closest French equivalent to 'Odobeccus' is '{french_equivalent}'.")

find_french_equivalent()