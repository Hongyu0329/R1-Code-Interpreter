def get_cited_lecturer():
    """
    Identifies the lecturer whose work was foundational to Einstein's 1905 Brownian motion paper.
    """
    # While Einstein's 1905 paper on Brownian motion famously had no formal citations,
    # his work was heavily based on the statistical mechanics developed by Ludwig Boltzmann,
    # particularly the ideas presented in his "Lectures on Gas Theory."
    lecturer_name = "Ludwig Boltzmann"
    print(lecturer_name)

get_cited_lecturer()