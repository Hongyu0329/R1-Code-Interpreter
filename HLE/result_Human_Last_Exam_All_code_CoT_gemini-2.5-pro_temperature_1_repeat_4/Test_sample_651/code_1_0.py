import math

# Based on the step-by-step analysis, the limit of M(theta) as theta approaches 0 is pi / 2.
# We can represent this as a final equation M = pi / 2.
# This script calculates and prints the value of this limit.

# The numerator in the final expression
numerator = math.pi
# The denominator in the final expression
denominator = 2

# Calculate the final result
limit_value = numerator / denominator

print("The final result is given by the equation: M = pi / 2")
print(f"The value of pi is approximately: {numerator}")
print(f"The value of the denominator is: {denominator}")
print(f"The numerical value of the limit is M = {limit_value}")
