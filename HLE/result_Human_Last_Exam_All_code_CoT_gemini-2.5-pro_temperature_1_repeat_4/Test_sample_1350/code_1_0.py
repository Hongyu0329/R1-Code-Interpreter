def find_character():
    """
    This function identifies and prints the name of the character from
    "The House of the Wolfings" who describes the Roman origin myth.
    """
    character_name = "Thiodolf's foster-father"
    context = "a Roman by birth who had joined the Goths"
    myth_reference = "Romulus and Remus"

    print(f"The character in William Morris's 'The House of the Wolfings' who alludes to the story of {myth_reference} is {character_name}.")
    print(f"This character was {context}, and he describes the Roman belief that they were 'the sons of a wolf'.")

find_character()