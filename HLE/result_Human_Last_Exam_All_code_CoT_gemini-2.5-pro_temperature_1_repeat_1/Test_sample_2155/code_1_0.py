def solve_chesterton_puzzle():
    """
    This function identifies and prints the two place names mentioned by
    Father Brown that begin with paired consonants, reflecting on the
    transformation of marital love.
    """
    # The first place name, famous for elopements, begins with 'Gr'.
    place_name_1 = "Gretna Green"

    # The second, a major London railway interchange, begins with 'Cl'.
    # Father Brown uses it metaphorically.
    place_name_2 = "Clapham Junction"

    print("The two place names mentioned by Father Brown are:")
    print(f"1. {place_name_1}")
    print(f"2. {place_name_2}")

solve_chesterton_puzzle()