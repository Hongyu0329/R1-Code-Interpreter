def find_removed_feature():
    """
    This function identifies and prints the architectural feature
    removed from Piazza della Rotonda in 1950.
    """
    location = "Piazza della Rotonda"
    year_removed = 1950
    removed_feature = "the tramway tracks"

    print(f"The unique feature in the '{location}' that was removed by the city administration around the year {year_removed} was {removed_feature}.")

find_removed_feature()