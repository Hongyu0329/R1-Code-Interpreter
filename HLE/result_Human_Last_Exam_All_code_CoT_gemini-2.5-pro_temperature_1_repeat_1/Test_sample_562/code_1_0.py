def find_radical_name():
    """
    This function identifies the radical for the kanji 謄 and prints its name in Hepburn romanization.
    """
    # The kanji in question is 謄.
    # It is composed of the radical 言 (gen, meaning 'word') on the left
    # and the component 朕 on the right.
    # The radical 言 on the left side of a character is called "gonben" (ごんべん).
    radical_name_hepburn = "Gonben"
    print(radical_name_hepburn)

find_radical_name()