def find_imprint_differences():
    """
    This function identifies and prints the different numerical imprints on
    Teva brand fluoxetine capsules for 10mg and 20mg dosages.
    """
    # The numerical imprint for Teva brand 10mg fluoxetine capsules is "3105"
    imprint_10mg = "3105"
    
    # The numerical imprint for Teva brand 20mg fluoxetine capsules is "3109"
    imprint_20mg = "3109"
    
    print(f"The number imprinted on the 10mg capsule is {imprint_10mg}.")
    print(f"The number imprinted on the 20mg capsule is {imprint_20mg}.")

find_imprint_differences()