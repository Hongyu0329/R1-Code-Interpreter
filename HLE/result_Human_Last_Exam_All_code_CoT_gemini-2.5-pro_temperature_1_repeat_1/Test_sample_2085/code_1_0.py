def find_french_equivalent():
    """
    This function provides the French equivalent of the Gaulish name "Odobeccus".
    """
    gaulish_name = "Odobeccus"
    french_equivalent = "Odobec"
    
    print(f"The equivalent of the Gaulish name '{gaulish_name}' in French is '{french_equivalent}'.")

find_french_equivalent()