def find_province_of_defeat():
    """
    This function identifies and prints the current Vietnamese province
    where Ming General Mu Sheng suffered his first major defeat.
    """
    # The historical event is the Battle of Khả Lưu Pass in 1427.
    # Mu Sheng (Mộc Thạnh in Vietnamese) was defeated by Vietnamese forces
    # led by Phạm Văn Xảo and Trịnh Khả.
    # This battle site is located in the territory of modern-day Nghệ An Province.
    province = "Nghệ An"

    print(f"Ming General Mu Sheng's first major defeat occurred at the Battle of Khả Lưu Pass.")
    print(f"This location is in the modern-day Vietnamese province of: {province}")

find_province_of_defeat()