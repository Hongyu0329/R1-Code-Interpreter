import math

# The value of the integral is -pi / 2.
# The numbers in this final equation are pi and 2.

pi_val = math.pi
denominator = 2

# Calculate the final result
result = -pi_val / denominator

print(f"The integral is given by the expression: -pi / 2")
print(f"Value of pi: {pi_val}")
print(f"Value of the denominator: {denominator}")
print(f"The final value of the integral is: {result}")