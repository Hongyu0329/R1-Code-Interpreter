def find_equation():
    """
    This function stores and prints the famous equation associated with Max Born.
    """
    # The physicist in question.
    name = "Max Born"
    
    # Max Born's famous equation is the Born rule, which gives the probability density.
    # It is represented as the square of the magnitude of the wave function, Ψ.
    # The number '2' is explicitly part of this equation.
    equation = "|Ψ|²"
    
    print(f"Name: {name}")
    print(f"Equation: {equation}")

find_equation()