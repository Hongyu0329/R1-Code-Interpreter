# The scientist's name.
name = "Max Born"

# The equation associated with the scientist, known as the Born rule.
# It states that the probability density of finding a particle is
# proportional to the square of the wave function's magnitude.
# Ψ is the capital Greek letter Psi, representing the wave function.
# ² is the superscript two character.
equation = "|Ψ|²"

# Print the name and the equation in the desired format.
print(f"Name: {name}")
print(f"Equation: {equation}")