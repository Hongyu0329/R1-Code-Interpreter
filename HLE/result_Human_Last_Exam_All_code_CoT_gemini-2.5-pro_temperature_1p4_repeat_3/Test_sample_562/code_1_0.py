# The Japanese character is 謄.
# The radical (bushu) is the component on the left, which is 言.
# When the radical 言 is on the left side of a character, it is called a "hen" (偏).
# The name for this specific radical form is ごんべん.
# In Hepburn romanization, ごんべん is transcribed as "Gonben".
radical_name = "Gonben"
print(radical_name)