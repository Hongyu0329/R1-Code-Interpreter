# In "The Secret of Flambeau," Father Brown reflects on how a noble
# idea of romantic love can become twisted and desperate.
# He illustrates this transformation with a metaphor of a road
# between two real-world places in Great Britain.

# The starting point, representing a mystical, holy, and romantic ideal of love.
place_name_1 = "Glastonbury"

# The ending point, representing a destination for hasty, desperate elopements.
place_name_2 = "Gretna Green"

print("The two place names mentioned by Father Brown are:")
print(place_name_1)
