def find_common_elements():
  """
  This function identifies and prints the common literary elements
  between "Key West" and "The Yellow Wallpaper" based on the provided list.
  """
  # Based on literary analysis, the common elements are II and IV.
  common_elements = ["II", "IV"]

  # The output format is a series of Roman numerals, in numerical order,
  # separated by commas.
  result = ", ".join(common_elements)

  print(result)

find_common_elements()