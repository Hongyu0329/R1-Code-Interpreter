# Define the Chern numbers of the two insulators
c1 = 1
c2 = 1

# The Chern number of the junction is the sum of the individual Chern numbers
c_total = c1 + c2

# Print the equation and the final result
print(f"The Chern number of the junction is the sum of the individual Chern numbers:")
print(f"{c1} + {c2} = {c_total}")