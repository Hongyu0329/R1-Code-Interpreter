# This script prints the currently used common name for the bee genus "Perdita"
# and the year it was verifiably published and attributed.
#
# Common Name: Fairy bees
# Year Coined/Published: 1999 (by Jerome G. Rozen, Jr., as published in "In a Desert Garden")

# The final answer is a combination of the name and the year.
common_name = "Fairy bees"
year = 1999

# Print the final result in the format "<name>, <year>"
# The numbers 1, 9, 9, 9 are all present in the final output string.
print(f"{common_name}, {year}")