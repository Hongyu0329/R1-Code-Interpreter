def find_imprinted_numbers():
    """
    This function stores and prints the numerical imprints on Teva brand
    fluoxetine 10mg and 20mg capsules.
    """
    # The number imprinted on the 10mg capsule
    imprint_10mg = "3105"

    # The number imprinted on the 20mg capsule
    imprint_20mg = "3106"

    print(f"The Teva brand fluoxetine 10mg capsule is imprinted with the number: {imprint_10mg}")
    print(f"The Teva brand fluoxetine 20mg capsule is imprinted with the number: {imprint_20mg}")

find_imprinted_numbers()