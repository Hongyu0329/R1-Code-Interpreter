def solve_covalency_question():
    """
    This function analyzes the relationship between orbital overlap and
    covalency for CeF6(2-) and CeCl6(2-) and prints the conclusion.
    """

    # Step 1: Establish the fundamental principle.
    # Covalency is a measure of electron sharing. In molecular orbital theory,
    # stronger covalent character arises from greater overlap between atomic orbitals.
    relationship_overlap_covalency = "directly proportional"

    # Step 2: Use the information given in the problem.
    # It is stated that CeF6(2-) has greater orbital overlap than CeCl6(2-).
    relative_overlap = "CeF6(2-) has greater orbital overlap than CeCl6(2-)"

    # Step 3: Draw a conclusion based on the principle and the given info.
    # Since covalency is directly proportional to orbital overlap, the compound
    # with greater overlap will exhibit stronger covalency.
    conclusion = "Therefore, CeF6(2-) displays stronger covalency."

    # Step 4: Print the reasoning and the final answer.
    print(f"The strength of covalency is {relationship_overlap_covalency} to the degree of orbital overlap between the metal and ligand orbitals.")
    print(f"Given that {relative_overlap}, it means there is more significant electron sharing in the Ce-F bonds than in the Ce-Cl bonds.")
    print(conclusion)

# Execute the function to get the answer.
solve_covalency_question()