def find_literary_parallel():
    """
    This script identifies and prints a specific parallel between the Moscow and Jerusalem
    narratives in Bulgakov's 'The Master and Margarita' based on the theme of a character
    being tormented by a bird.
    """
    # 1. The character in Moscow is Rimsky. In Chapter 14, his fear is amplified
    # by a sparrow chirping "restlessly and mournfully" outside his window.
    moscow_character = "Rimsky"

    # 2. The parallel bird in the Jerusalem narrative is a barn swallow.
    # It torments Pontius Pilate in Chapter 25.
    jerusalem_bird = "barn swallow"

    # 3. The character in Jerusalem tormented by the bird is Pontius Pilate,
    # whose anguish is mirrored by the bird's persistent flight.
    jerusalem_character = "Pontius Pilate"

    # The final answer is constructed by joining the three pieces of information.
    final_answer = f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}"
    
    print(final_answer)

find_literary_parallel()