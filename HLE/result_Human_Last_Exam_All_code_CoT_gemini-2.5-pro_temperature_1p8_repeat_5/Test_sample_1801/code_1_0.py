def find_enron_laureates():
    """
    This function identifies and prints the names of the two Nobel laureates
    who were featured in the infamous 2000 Enron commercial with the
    tagline "Ask Why".
    """
    laureate_1 = "Paul Krugman"  # Nobel Memorial Prize in Economic Sciences, 2008
    laureate_2 = "Merton Miller" # Nobel Memorial Prize in Economic Sciences, 1990

    print("The two Nobel laureates featured in the 2000 Enron 'Ask Why' commercial were:")
    print(f"1. {laureate_1}")
    print(f"2. {laureate_2}")

find_enron_laureates()