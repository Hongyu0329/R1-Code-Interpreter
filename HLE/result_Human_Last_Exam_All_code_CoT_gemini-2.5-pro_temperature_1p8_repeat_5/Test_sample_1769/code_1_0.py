def find_hypothetical_word():
    """
    Calculates the modern English word for 'sister'
    without Norse influence.
    """
    # 1. The original Old English word for sister.
    old_english_word = "sweostor"
    print(f"The original Old English word was: {old_english_word}")

    # 2. In Middle English, this word evolved. The 'w' was often dropped after an
    # initial 's', and the 'eo' vowel sound simplified to 'u'.
    middle_english_word = "suster"
    print(f"It would have evolved into the Middle English form: {middle_english_word}")

    # 3. This Middle English form, "suster", would likely have been retained
    # into Modern English, as the short 'u' sound is stable.
    # The Norse word "systir" (sister) replaced it in our timeline.
    hypothetical_modern_word = middle_english_word
    print(f"Without Norse influence, the likely Modern English word would be: {hypothetical_modern_word}")

find_hypothetical_word()