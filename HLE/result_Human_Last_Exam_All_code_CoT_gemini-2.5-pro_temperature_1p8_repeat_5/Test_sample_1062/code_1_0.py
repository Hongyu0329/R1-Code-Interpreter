def find_kickoff_song():
    """
    This function identifies and prints the song traditionally played
    just before kick-off at Scunthorpe United Football Club home games.
    """
    # Scunthorpe United's nickname is "The Iron", a reference to the town's
    # steel industry history.
    correct_song = "Iron Man"
    artist = "Black Sabbath"

    print("The song played just before kick-off at every Scunthorpe United Football Club home game is:")
    print(f"'{correct_song}' by {artist}")

if __name__ == "__main__":
    find_kickoff_song()