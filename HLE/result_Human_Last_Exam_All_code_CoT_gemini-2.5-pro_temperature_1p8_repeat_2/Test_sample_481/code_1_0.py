# The imprints on Teva brand fluoxetine capsules vary by dosage.
# This script will display the different numbers found on the 10mg and 20mg capsules.

# The number imprinted on the 10mg capsule.
imprint_10mg = "3105"
# The number imprinted on the 20mg capsule.
imprint_20mg = "3106"

# Print the information for the 10mg capsule
print(f"The number imprinted on the Teva 10mg fluoxetine capsule is {imprint_10mg}.")
