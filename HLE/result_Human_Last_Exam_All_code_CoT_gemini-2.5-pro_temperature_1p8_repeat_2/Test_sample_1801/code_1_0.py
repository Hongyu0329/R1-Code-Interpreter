def solve_enron_commercial_question():
    """
    Identifies and prints the names of the two Nobel laureates
    featured in a 2000 Enron commercial.
    """
    # Paul Krugman won the Nobel Memorial Prize in Economic Sciences in 2008.
    # Myron Scholes won the Nobel Memorial Prize in Economic Sciences in 1997.
    laureate1 = "Paul Krugman"
    laureate2 = "Myron Scholes"

    print(f"The two Nobel laureates featured in the 2000 Enron 'Ask Why' commercial were {laureate1} and {laureate2}.")

solve_enron_commercial_question()