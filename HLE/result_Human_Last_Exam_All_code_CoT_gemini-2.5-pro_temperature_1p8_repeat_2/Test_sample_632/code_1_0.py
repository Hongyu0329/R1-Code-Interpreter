# This script demonstrates how to solve a simple equation and print the result
# in the requested format.

# 1. Define the numbers for our equation.
num1 = 118
num2 = 235

# 2. Calculate the result of the addition.
result = num1 + num2

# 3. Print the final equation, showing each number.
