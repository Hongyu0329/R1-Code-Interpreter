def get_evaluation():
    """
    This function stores and prints Liu Ban's evaluation of Zhang Ji's poetry.
    """
    # Liu Ban's evaluation is recorded in his work 《中山诗话》 (Zhongshan Shihua).
    # The original Chinese text provides the most precise answer.
    evaluation = "根据刘攽在其著作《中山诗话》中的记载，他对张籍七言诗的评价原文是：“张籍七言诗，拙涩可笑，独‘边将皆承主恩泽，无人解道取凉州’之句，为人传诵。”"
    
    # This translates to: "Zhang Ji's seven-character poems are clumsy, unsmooth, and laughable, with the sole exception of the lines 'All the border generals have received the emperor's favor, yet no one knows how to retake Liangzhou,' which are widely recited."
    
    print(evaluation)

