def find_french_equivalent():
    """
    This function explains the linguistic evolution of the name 'Odobeccus'
    to its French equivalents.
    """
    original_name = "Odobeccus"
    linguistic_origin = "Germanic (Frankish)"
    root_element = "Odo-"
    root_meaning = "wealth, prosperity"
    
    # The Germanic root "Odo-" evolved into specific names in French.
    primary_french_equivalent = "Eudes"
    secondary_french_equivalent = "Odon"

    print(f"Original Name: {original_name}")
    print(f"Linguistic Origin: {linguistic_origin}")
    print("-" * 30)
    print(f"The name is based on the root element '{root_element}'.")
    print(f"Meaning of the root: '{root_meaning}'")
    print("-" * 30)
    print("This root evolved into modern French names.")
    print(f"The most common French equivalent is: {primary_french_equivalent}")
    print(f"A secondary, more Latinized equivalent is: {secondary_french_equivalent}")

if __name__ == "__main__":
    find_french_equivalent()