def find_speaker():
    """
    This function identifies and prints the name of the person who spoke the
    specified quote to G.K. Chesterton.
    
    The anecdote is recounted by Chesterton in his book, "The Everlasting Man" (Part I, Chapter 5),
    where he attributes the remark to his wife during their visit to Jerusalem.
    """
    speaker = "his wife, Frances Chesterton"
    location = "on the Mount of Olives with a view of Gethsemane"
    quote = "Well, anyhow, it must be obvious to anybody that the doctrine of the Fall is the only cheerful view of human life"

    print(f"According to G.K. Chesterton, the person who told him '{quote}' while they stood together {location} was {speaker}.")

find_speaker()