def find_prophet_verses():
    """
    Identifies and prints the range of verses (abyat) in 'Aqeedat al-'Awaam'
    that list the names of the prophets.
    """
    # The list of the 25 prophets begins at bayt (verse) 35.
    start_bayt = 35

    # The list concludes at bayt (verse) 38.
    end_bayt = 38

    print(f"In the poem 'Aqeedat al-'Awaam', the names of the Prophets are mentioned from bayt {start_bayt} to bayt {end_bayt}.")

find_prophet_verses()