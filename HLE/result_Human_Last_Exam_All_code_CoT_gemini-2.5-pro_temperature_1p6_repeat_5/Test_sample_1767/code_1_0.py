# The Chern number of the first insulator
c1 = 1

# The Chern number of the second insulator
c2 = 1

# The Chern number of the junction is the sum of the individual Chern numbers
c_junction = c1 + c2

# Print the final equation
print(f"The Chern number of the junction is calculated by summing the individual Chern numbers.")
print(f"C_junction = C1 + C2 = {c1} + {c2} = {c_junction}")