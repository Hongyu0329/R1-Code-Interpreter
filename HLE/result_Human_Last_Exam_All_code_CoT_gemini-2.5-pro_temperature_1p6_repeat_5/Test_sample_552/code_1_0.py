def find_cited_lecturer():
    """
    This function provides the name of the scientist whose lectures were
    cited by Albert Einstein in his 1905 paper on Brownian motion.
    """
    
    # Historical research shows that Einstein's work on statistical mechanics,
    # which he applied to Brownian motion, was built on the foundations
    # laid by a particular physicist. The term "lectures" most directly
    # refers to this physicist's influential published work on the subject.
    
    scientist_name = "Ludwig Boltzmann"
    paper_year = 1905
    paper_topic = "Brownian motion"
    
    # Albert Einstein's 1905 paper, "On the Movement of Small Particles
    # Suspended in a Stationary Liquid Required by the Molecular-Kinetic
    # Theory of Heat," is fundamentally based on the principles of statistical
    # mechanics developed by Ludwig Boltzmann, whose work was famously
    # published as "Lectures on Gas Theory."
    
    print(f"In his paper on {paper_topic} from the year {paper_year}, Albert Einstein cited the lectures of {scientist_name}.")

find_cited_lecturer()