# The task is to identify the name of the value computed between heartbeats to measure HRV.
# This value is the duration of the time interval between two consecutive heartbeats.
# The most common and technically accurate term for this is the "Inter-Beat Interval" (IBI).

# Define the term as a string.
term = "Inter-Beat Interval (IBI)"

# Print the answer in a clear statement.
print(f"The value computed as the time between consecutive heartbeats for HRV calculation is called the: {term}")
