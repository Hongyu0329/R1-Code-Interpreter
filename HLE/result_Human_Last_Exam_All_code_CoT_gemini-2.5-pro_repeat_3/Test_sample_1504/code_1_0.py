def solve_extremal_graph_problem():
    """
    This function formats and prints the solution to the user's graph theory problem.
    """
    
    # Part (a) Answer
    part_a = "True"
    
    # Part (b) Answer
    part_b = "False"
    
    # Part (c) Answer
    # The derivation provides the expression for the upper bound.
    # We will represent it as a string.
    s = "s"
    t = "t"
    # The expression derived is (s-1)(2s+2t-5).
    # The instruction "output each number in the final equation" is interpreted as
    # simply writing down the formula with its numerical constants.
    part_c_expression = f"({s}-1)(2*{s}+2*{t}-5)"

    # Format the final output string
    final_answer_string = f"(a) {part_a}; (b) {part_b}; (c) {part_c_expression}"
    
    print(final_answer_string)

solve_extremal_graph_problem()