def find_parallel_torment():
    """
    This function identifies and prints the characters and bird involved
    in a parallel theme of torment by small birds in Bulgakov's
    "The Master and Margarita".
    """

    # In Chapter 18, the house sparrow torments the barman Sokov in Professor Kuzmin's office.
    moscow_character = "Sokov"

    # The corresponding bird in the Jerusalem narrative is the swallow that annoys Pontius Pilate.
    # From the options provided, this is the "barn swallow".
    jerusalem_bird = "barn swallow"

    # The barn swallow flies around Pontius Pilate on his balcony in Chapter 2.
    jerusalem_character = "Pontius Pilate"

    # Construct the final answer string separated by semicolons.
    answer = f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}"

    print(answer)

find_parallel_torment()
