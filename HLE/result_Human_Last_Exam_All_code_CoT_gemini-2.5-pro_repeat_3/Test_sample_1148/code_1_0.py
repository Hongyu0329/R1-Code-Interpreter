def find_borges_names():
    """
    This function identifies and prints the names of the three people
    Jorge Luis Borges cites in his essay "The Argentine Writer and Tradition"
    to illustrate his point about patriotism and literature.
    """
    # The three individuals cited by Borges are the Greek philosopher,
    # the English playwright, and the German writer.
    greek = "Plato"
    englishman = "Shakespeare"
    german = "Goethe"

    # The final output is a comma-separated string of these names.
    print(f"{greek},{englishman},{german}")

find_borges_names()