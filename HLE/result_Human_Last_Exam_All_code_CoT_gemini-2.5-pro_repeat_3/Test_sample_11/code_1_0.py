def find_first_good_faith_statute():
    """
    This function identifies and prints the name of the first statute in modern Israeli law
    to explicitly introduce the concept of "good faith" as a general principle.
    """
    # Research indicates that while the concept existed in case law,
    # the Contracts (General Part) Law, 5733-1973 was the first major statute
    # to explicitly codify "good faith" as a broad, overarching duty
    # in both pre-contractual negotiations (Section 12) and in the fulfillment
    # of contractual obligations (Section 39).
    statute_name = "Contracts (General Part) Law"
    print(statute_name)

find_first_good_faith_statute()