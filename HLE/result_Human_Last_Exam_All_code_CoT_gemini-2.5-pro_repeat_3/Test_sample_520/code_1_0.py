def find_profession():
    """
    This function stores and prints the true profession of Andre Vernault.
    """
    # Andre Vernault, a Belgian refugee, arrived in the UK during World War II.
    # He concealed his profession from his MI5 interrogators, leading them to
    # suspect he was a German spy.
    true_profession = "an undertaker"

    # He was reportedly ashamed of his job and feared that if the British authorities
    # knew, they would send him back to occupied Belgium where his services
    # would be in high demand due to the war.

    print(f"The true profession Andre Vernault concealed from British interrogators was that of {true_profession}.")

find_profession()