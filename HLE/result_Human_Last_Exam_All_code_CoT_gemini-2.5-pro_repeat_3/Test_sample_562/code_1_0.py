def find_radical_name():
    """
    This function identifies the radical for the kanji 謄 and prints its name.
    """
    kanji = "謄"
    radical_component = "月"
    radical_name_japanese = "にくづき"
    radical_name_hepburn = "Nikuzuki"

    print(f"The Japanese character is: {kanji}")
    print(f"The radical (bushu) is the left component: {radical_component}")
    print(f"The name of this radical is '{radical_name_japanese}', which is transcribed as '{radical_name_hepburn}' in Hepburn romanization.")

find_radical_name()