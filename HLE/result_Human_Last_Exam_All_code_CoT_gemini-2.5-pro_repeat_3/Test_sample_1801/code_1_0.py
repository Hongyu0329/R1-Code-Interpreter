def solve_enron_commercial_question():
    """
    This function identifies and prints the names of the two Nobel laureates
    featured in a 2000 Enron commercial.
    """
    # Based on historical information, the two individuals were economists.
    # One had already won the Nobel prize, and the other would win it later.
    laureate1 = "Paul Krugman"  # Nobel Memorial Prize in Economic Sciences in 2008
    laureate2 = "Myron Scholes" # Nobel Memorial Prize in Economic Sciences in 1997

    print("The two Nobel laureates featured in an infamous 2000 commercial for Enron were:")
    print(f"1. {laureate1}")
    print(f"2. {laureate2}")

solve_enron_commercial_question()