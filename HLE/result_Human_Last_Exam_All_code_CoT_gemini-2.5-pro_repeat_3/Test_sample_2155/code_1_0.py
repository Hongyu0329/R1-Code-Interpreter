# This script identifies the two place names from G.K. Chesterton's Father Brown story, "The Secret Garden".
# In the story, Father Brown reflects on how the nature of love can change over time.
# He uses a metaphor: "He knew that the love which begins in Gretna Green may end in Smithfield."

# The first place, symbolizing a romantic beginning.
# It starts with the paired consonant 'Gr'.
place1 = "Gretna Green"

# The second place, symbolizing a sordid end.
# It starts with the paired consonant 'Sm'.
place2 = "Smithfield"

print("The two place names that begin with paired consonants are:")
print(place1)
print("and")
print(place2)