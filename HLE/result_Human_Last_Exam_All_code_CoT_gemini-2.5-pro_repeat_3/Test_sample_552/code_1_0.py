def find_cited_lecturer():
    """
    This function provides the name of the scientist whose lectures
    influenced Albert Einstein's 1905 paper on Brownian motion.
    """
    scientist_name = "Ludwig Boltzmann"
    paper_year = 1905
    subject_of_lectures = "statistical mechanics and the kinetic theory of heat"

    print(f"In his {paper_year} paper on Brownian motion, Albert Einstein's work was conceptually based on principles from the lectures and writings of {scientist_name}, particularly concerning {subject_of_lectures}.")

find_cited_lecturer()