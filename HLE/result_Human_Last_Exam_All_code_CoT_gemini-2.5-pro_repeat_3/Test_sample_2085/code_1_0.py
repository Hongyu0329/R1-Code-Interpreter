def find_french_equivalent():
    """
    This function provides the French equivalent for the Gaulish/Frankish name "Odobeccus".
    """
    gaulish_name = "Odobeccus"
    french_equivalent = "Eudoxe"
    
    print(f"The equivalent of the Gaulish/Frankish name '{gaulish_name}' in French is '{french_equivalent}'.")

find_french_equivalent()