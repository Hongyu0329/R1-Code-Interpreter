def get_bee_common_name_info():
    """
    This function stores and prints the common name and the year it was coined
    for the bee genus "Perdita".
    """
    # The common name for the genus Perdita is "fairy bees".
    common_name = "fairy bees"

    # This name was officially proposed in a 1996 publication in the American Entomologist journal.
    year_coined = 1996

    # Print the result in the format "<name>, <year>"
    print(f"{common_name}, {year_coined}")

get_bee_common_name_info()