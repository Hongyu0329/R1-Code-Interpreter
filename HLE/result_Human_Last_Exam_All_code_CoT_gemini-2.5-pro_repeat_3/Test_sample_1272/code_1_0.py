def find_defeat_location():
    """
    This function provides information about Ming General Mu Sheng's first major defeat.
    """
    general = "Mu Sheng"
    battle = "Battle of Tốt Động-Chúc Động"
    year = 1426
    modern_location = "Hanoi"
    
    # The Battle of Tốt Động-Chúc Động in 1426 is considered the first major defeat
    # for the Ming forces under the command umbrella of Mu Sheng during the Lam Sơn uprising.
    # The battle site is located in the modern-day Chương Mỹ District, which is part of Hanoi.
    # Hanoi is a municipality with the same administrative status as a province in Vietnam.
    
    print(f"The first major defeat for the Ming forces led by General {general} occurred during the {battle} in the year {year}.")
    print(f"This battle took place in an area that is now part of modern-day {modern_location}.")

