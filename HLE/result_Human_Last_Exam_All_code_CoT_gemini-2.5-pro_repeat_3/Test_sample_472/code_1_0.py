def scan_latin_line():
    """
    This function prints the scansion of the Latin line:
    "et tibi bene esse soli quom sibi sit male"
    L = Long Syllable
    S = Short Syllable
    Feet are separated by spaces.
    """
    # The meter is an Iambic Septenarius.
    # Feet: et ti | bi be(nesse) | nes-se | so-li | quom si | -bi sit | ma-le
    scansion = "L L S S L S L S L S S L S S"
    print(scansion)

scan_latin_line()