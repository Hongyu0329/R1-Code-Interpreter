# The particular solution for the initial value problem is x(t) = 8 / (-4*t^2 + 8*t - 1).
# We need to find the value of x(1).

# Value of t to substitute
t = 1

# Numerator of the expression
numerator = 8

# Denominator of the expression
denominator = -4 * (t**2) + 8 * t - 1

# Calculate the result
x_1 = numerator / denominator

# Print the equation with substituted values
print(f"The final equation to solve is x(1) = {numerator} / (-4 * ({t})^2 + 8 * ({t}) - 1)")

# Print the final result
print(f"The value of x(1) is: {x_1}")