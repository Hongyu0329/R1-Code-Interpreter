def solve_chesterton_riddle():
    """
    This script solves the riddle about Father Brown and the two place names.
    The solution is found in G.K. Chesterton's story "The Arrow of Heaven".
    """

    # Explanation of the context
    story = "The Arrow of Heaven"
    character_action = "a wife murdering a blackmailer for her husband's sake"
    father_brown_reflection = "the transformation of marital love into a destructive, servile devotion"

    print(f"The story is '{story}', in which a character commits misdeeds ({character_action}).")
    print(f"Reflecting on this, Father Brown discusses {father_brown_reflection}.")
    print("In his final speech, he mentions two symbolic places whose names begin with paired consonants.")
    print("-" * 40)

    # The two place names from the story's text
    place_1 = "Gretna Green"
    place_2 = "Bluebeard's Chamber"

    print("The two place names are:")
    print(f"1. {place_1}")
    print(f"2. {place_2}")

solve_chesterton_riddle()