def find_milyukovs_target():
    """
    This function identifies and prints the group Pavel Milyukov
    famously denounced as a 'stagnant swamp'.
    """
    # Pavel Milyukov, a leader of the Constitutional Democratic Party (Kadets),
    # was a rationalist and historian who viewed the rising tide of
    # modernism and mysticism in Russian culture with disdain.
    
    # The term he used, represented as X-Y in the question, was "Symbolist poets".
    x = "Symbolist"
    y = "poets"
    
    # The script will now print the answer.
    print(f"Pavel Milyukov denounced {x}-{y} as a 'stagnant swamp'.")

find_milyukovs_target()