def print_borges_reference():
    """
    This function identifies and prints the novel and author
    that Jorge Luis Borges praised with the given quote.
    """
    novel_title = "El astillero (The Shipyard)"
    author_name = "Juan Carlos Onetti"

    print("The novel Jorge Luis Borges refers to is:")
    print(f"Title: {novel_title}")
    print(f"Author: {author_name}")
    print("\nBorges made these remarks in his prologue to the novel.")

print_borges_reference()