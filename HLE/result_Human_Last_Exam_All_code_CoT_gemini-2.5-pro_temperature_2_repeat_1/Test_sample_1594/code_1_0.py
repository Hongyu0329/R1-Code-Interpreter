def find_building_shape():
    """
    This function stores and prints the letter formed by the combined shape
    of two high schools in Pisa when viewed from above.
    """
    # The two high schools located next to each other in Pisa
    school_1 = "Istituto Galilei-Pacinotti"
    school_2 = "Istituto Ulisse Dini"

    # The letter formed by the layout of their buildings
    letter_shape = "H"

    # Print the result
    print(f"When viewed from above, the buildings of the high schools '{school_1}' and '{school_2}' stand next to each other, forming the letter:")
    print(letter_shape)

find_building_shape()