def find_cited_lectures():
    """
    This function provides the name of the scientist whose lectures and writings
    were a major influence on Albert Einstein's 1905 paper on Brownian motion.
    """
    # The theoretical work in Einstein's paper is based on the principles of
    # statistical mechanics, a field largely developed by Ludwig Boltzmann.
    # Einstein was known to have studied Boltzmann's "Lectures on Gas Theory."
    scientist = "Ludwig Boltzmann"

    print(f"Albert Einstein's 1905 paper on Brownian motion was conceptually built upon the theoretical work and lectures of: {scientist}")

find_cited_lectures()