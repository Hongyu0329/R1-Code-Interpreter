def find_imprint_numbers():
    """
    This function stores and prints the numerical imprints for Teva brand
    fluoxetine 10mg and 20mg capsules.
    """
    imprint_10mg = 3105
    imprint_20mg = 3106

    print(f"The number imprinted on the Teva fluoxetine 10mg capsule is {imprint_10mg}.")
