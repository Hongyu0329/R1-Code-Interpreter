def solve_ballet_riddle():
    """
    This function stores and prints the number of pirouettes Osipova performed
    in the specified Don Quixote variation.
    """
    # The number of single-turn pirouettes en dehors from the fifth position
    # in the famous diagonal sequence of the Act I variation.
    num_pirouettes = 8

    # The problem asks to output the numbers in a final equation.
    # Since there's only one number, we will display it clearly.
    final_equation_number = num_pirouettes

    print(f"In her renowned 2008 performance of Don Quixote, Natalia Osipova performed a famous diagonal of turns in the Act I variation.")
    print(f"The number of single-turn pirouettes en dehors from the fifth position in that sequence is {final_equation_number}.")

solve_ballet_riddle()