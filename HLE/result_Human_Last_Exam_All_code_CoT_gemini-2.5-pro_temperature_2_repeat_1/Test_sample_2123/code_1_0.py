def solve_riddle():
    """
    This function explains the solution to the literary riddle by breaking down the pun.
    """

    # The key to the riddle is a metaphorical description in a Russian essay.
    source_essay = "Zoo, or Letters Not about Love"
    author = "Viktor Shklovsky"
    location = "Vienna"

    # In the essay, the boulevards of Vienna are compared to an Amazon's backside.
    # The Russian word for "backside" is the core of the pun.
    key_russian_word = "попа"
    phonetic_pronunciation = "popa"

    # This sound directly corresponds to the surname of a famous English poet.
    english_poet_surname = "Pope"

    # Print the explanation step-by-step.
    print("The riddle's solution is based on a literary reference and a linguistic pun:")
    print(f"1. The essay is '{source_essay}' by {author}.")
    print(f"2. When describing the wide boulevards of {location}, the author uses a specific metaphor.")
    print(f"3. The Russian word at the heart of the metaphor is '{key_russian_word}', pronounced roughly as '{phonetic_pronunciation}'.")
    print(f"4. This sound is phonetically similar to the surname of the English poet, Alexander {english_poet_surname}.")
    print("\nTherefore, the surname is:")
    print(english_poet_surname)

# Execute the function to print the solution.
solve_riddle()