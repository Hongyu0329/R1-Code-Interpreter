    # Balanchine Method Explanation
    print("Balanchine Method:")
    print("  - Encourages a deeper, more dynamic backbend.")
    print("  - To achieve this, the dancer is allowed to release the hip of the standing leg forward.")
    print("  - This shifting of the hip facilitates a greater range of motion and a more extreme line.")
