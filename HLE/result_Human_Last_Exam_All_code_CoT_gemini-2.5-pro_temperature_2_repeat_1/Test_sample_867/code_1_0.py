# In the 2014 Bolshoi performance of the Swan Lake Act II Odette variation,
# Svetlana Zakharova performs two series of sissone fermés.

# Count the number of sissone fermés in the first series.
first_series_sissones = 6

# Count the number of sissone fermés in the second series.
second_series_sissones = 6

# Calculate the total number of sissone fermés.
total_sissones = first_series_sissones + second_series_sissones

# Print the final equation.
print(f"{first_series_sissones} + {second_series_sissones} = {total_sissones}")