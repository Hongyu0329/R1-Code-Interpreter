def solve_physics_riddle():
    """
    This function identifies and prints the name of the physicist
    who discovered phase contrast microscopy.
    """
    
    # The clues point to the Dutch physicist Frits Zernike.
    # - He worked in microscopy.
    # - He discovered phase contrast, an optical effect that makes transparent objects visible.
    # - The discovery was famously inspired by observing a janitor cleaning a diffraction grating (a type of concave mirror),
    #   which made the normally invisible grooves on the grating visible.
    
    physicist_name = "Frits Zernike"
    
    print(physicist_name)

solve_physics_riddle()