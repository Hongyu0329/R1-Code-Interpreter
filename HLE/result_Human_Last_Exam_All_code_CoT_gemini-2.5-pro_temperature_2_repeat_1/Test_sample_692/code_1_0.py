# As requested, here are the numbers that make up the recorded date:
print(f"B'ak'tun (period of 144,000 days): {baktun}")
print(f"K'atun (period of 7,200 days): {katun}")
print(f"Tun (period of 360 days): {tun}")
print(f"Winal (period of 20 days): {winal}")
print(f"K'in (period of 1 day): {kin}")

print(f"\nThe complete Long Count date notation is: {baktun}.{katun}.{tun}.{winal}.{kin}")
