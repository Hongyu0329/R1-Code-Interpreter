def solve_chess_riddle():
    """
    This function identifies the player of the black pieces in the given chess game.
    The game is a well-known historical match.
    """
    # The provided game is Game 16 of the 1985 World Championship.
    white_player = "Kasparov, Garry"
    black_player = "Karpov, Anatoly"

    # The question asks for the player of the black pieces.
    print(f"The player of the White pieces was: {white_player}")
    print(f"The player of the Black pieces was: {black_player}")

    # Matching the player with the answer choices.
    # B. Karpov, Anatoly
    answer_choice = "B"
    print(f"\nThis corresponds to answer choice: {answer_choice}")

solve_chess_riddle()