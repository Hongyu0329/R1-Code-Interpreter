# First, let's define the numbers based on our interpretation.
# 'പ്പെ' is interpreted as the Cuneiform value 600.
num1 = 600
# '𒌋𒌋𒌋' is 10 + 10 + 10.
num2 = 30
# '𒌋𒌋𒁹𒁹𒁹' is 10 + 10 + 1 + 1 + 1.
num3 = 23

# Now, we solve the equation.
# First, perform the division.
division_result = num1 / num2
# Then, perform the subtraction.
final_result = division_result - num3

# Finally, we print the full equation with the solution.
print(f"The equation in modern numbers is:")
print(f"{int(num1)} / {int(num2)} - {int(num3)} = {int(final_result)}")
