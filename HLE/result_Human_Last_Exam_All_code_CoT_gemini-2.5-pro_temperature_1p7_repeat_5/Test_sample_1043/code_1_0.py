def solve_poet_riddle():
  """
  This function identifies the poet and the most emphasized word
  from a spoken performance of the poem and prints the result.
  """
  poet_last_name = "Brooks"
  emphasized_word = "holler"
  
  # The poem is "To the Young Who Want to Die" by Gwendolyn Brooks.
  # In recordings of her reading the poem, she places a strong, shouted
  # emphasis on the word "holler" in the line "to hie to a house that does not holler".
  
  print(f"{poet_last_name}, {emphasized_word}")

solve_poet_riddle()