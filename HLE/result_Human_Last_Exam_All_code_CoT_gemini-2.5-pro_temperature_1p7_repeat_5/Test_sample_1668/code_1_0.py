def calculate_total_value():
    """
    Calculates the total value of four specified 1992 series Belarusian banknotes.
    """
    # Denominations of the 1992 series banknotes
    bison_value = 100
    elk_value = 25
    lynx_value = 5
    hare_value = 1

    # Calculate the sum
    total_value = bison_value + elk_value + lynx_value + hare_value

    # Print the equation as requested
    print(f"{bison_value} + {elk_value} + {lynx_value} + {hare_value} = {total_value}")

if __name__ == "__main__":
    calculate_total_value()