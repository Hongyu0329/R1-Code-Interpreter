def find_hypothetical_word():
    """
    This script explains and reveals the Modern English word for 'sister'
    if the Norse had never influenced the language.
    """
    print("Analyzing the hypothetical evolution of the word 'sister' without Norse influence.")
    print("----------------------------------------------------------------------")
    
    # Define the historical words
    old_english_word = "sweostor"
    old_norse_word = "systir"
    
    # Explain the actual historical development
    print(f"The Old English (Anglo-Saxon) word for sister was '{old_english_word}'.")
    print(f"Our modern word 'sister' was heavily influenced by the Old Norse word '{old_norse_word}'.")
    print("The 'i' vowel and the hard 's' sound reflect this Norse influence.")
    print("----------------------------------------------------------------------")
    
    # Explain the hypothetical development
    print("If the word had evolved purely from Old English, it would have followed a different path:")
    
    # Step 1: Starting point
    print(f"\nStep 1: Start with the Old English word '{old_english_word}'.")
    
    # Step 2: Evolution to Middle English
    middle_english_word = "suster"
    print(f"\nStep 2: Through natural sound changes, '{old_english_word}' would likely become '{middle_english_word}' in Middle English.")
    print(f"   - This form, '{middle_english_word}', was common in southern England, which had less Norse influence.")
    print("   - The initial 'sw-' simplified to 's-' and the 'eo' vowel shifted to 'u'.")
    
    # Step 3: Evolution to Modern English
    modern_hypothetical_word = "suster"
    print(f"\nStep 3: This Middle English form '{middle_english_word}' would have been retained into Modern English.")
    print("   - Similar to how 'sunne' became 'sun', 'suster' would remain 'suster'.")
    
    print("\n----------------------------------------------------------------------")
    print("Therefore, without Norse influence, the Modern English word would be:")
    print(modern_hypothetical_word)

