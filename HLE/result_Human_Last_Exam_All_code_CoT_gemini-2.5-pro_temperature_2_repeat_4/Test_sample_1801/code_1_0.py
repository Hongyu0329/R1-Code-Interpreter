def find_enron_commercial_laureates():
    """
    This script identifies and prints the names of the two economists,
    both Nobel laureates, who appeared in a 2000 Enron commercial.
    """
    laureate1 = "Paul Krugman"
    laureate2 = "Myron Scholes"

    print(f"The two Nobel laureates featured in the infamous 2000 Enron 'Ask Why' commercial were {laureate1} and {laureate2}.")
    print("\nNote: At the time the commercial aired, only Myron Scholes was a Nobel laureate (having won in 1997). Paul Krugman received his Nobel Memorial Prize in Economic Sciences in 2008.")

find_enron_commercial_laureates()