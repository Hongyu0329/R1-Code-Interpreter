def find_building_shape():
  """
  This function reveals the letter formed by the buildings of
  Istituto Galilei-Pacinotti and Istituto Ulisse Dini in Pisa
  when seen from above.
  """
  # By observing the satellite view of the two adjacent schools,
  # the main building of Istituto Ulisse Dini forms the vertical stem,
  # and the main building of Istituto Galilei-Pacinotti forms the top horizontal bar.
  letter = "T"
  print(f"The letter formed by the two school buildings is: {letter}")

find_building_shape()