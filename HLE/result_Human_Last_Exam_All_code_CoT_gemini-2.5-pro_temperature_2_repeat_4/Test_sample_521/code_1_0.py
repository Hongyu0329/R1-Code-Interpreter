    # 4. Calculate the Result and show the equation
    equation_parts = []
    for i, bit in enumerate(binary_string):
        if bit == '1':
            power = 7 - i
            term = 2**power
            equation_parts.append(str(term))

    print(f"The Wolfram code is determined by converting the binary rule '{binary_string}' to decimal:")
    equation = " + ".join(equation_parts)
    print(f"{equation} = {wolfram_code}")

