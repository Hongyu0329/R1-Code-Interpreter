def find_parallel_characters():
    """
    This function identifies the parallel characters and birds from Bulgakov's
    'The Master and Margarita' based on the theme of torment by small birds.
    """

    # In Chapter 18, Andrei Fokich Sokov, the buffet master, is distracted
    # and annoyed by a restless house sparrow outside the window of Professor Kuzmin's office.
    moscow_character = "Sokov"

    # The parallel scene occurs in Chapter 2. During the trial of Yeshua, as
    # Pontius Pilate suffers from a terrible migraine, a swallow's fleeting,
    # darting flight through the colonnade acts as a minor disturbance
    # mirroring the stabbing pain in his head.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"

    # Print the answer in the specified format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

find_parallel_characters()