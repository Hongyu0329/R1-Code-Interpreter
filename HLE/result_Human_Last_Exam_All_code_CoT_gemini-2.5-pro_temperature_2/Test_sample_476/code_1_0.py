def find_author():
    """
    Identifies and prints the name of the classical author associated with the quote.
    """
    # The quote "prope soli iam in scholis sunt relicti" is from the "Satyricon".
    # The author of the "Satyricon" is Petronius.
    author = "Petronius"
    print(f"The classical author is: {author}")

if __name__ == "__main__":
    find_author()