def solve_riddle():
    """
    This function analyzes the clues provided in the riddle to identify the historical figure.
    """
    # Clue 1: The description and its author.
    author = "Andrei Voznesensky"
    description = "a joy-discovering sailor"
    
    # Clue 2: The long-standing mystery.
    mystery_quote = "Where is your grave, even a mound?"
    
    # Clue 3: The time of resolution.
    resolution_period = "late 1980s"

    print("Analyzing the clues to identify the man:")
    print(f"1. He was described by the Russian poet {author} as '{description}'.")
    print("   - This suggests an adventurous figure, likely a fellow poet, whose work celebrated exploration and life.")
    print(f"\n2. His final resting place was a mystery for decades, encapsulated by the question: '{mystery_quote}'")
    print("   - This points to someone who was a victim of a secret execution or disappearance.")
    print(f"\n3. The mystery was solved in the {resolution_period}.")
    print("   - This timeline points directly to the period of Glasnost in the USSR, when information about victims of Soviet political repression was revealed.")
    
    print("\nSynthesizing the facts:")
    print("- The man is a famous Russian poet known for his travels (fitting 'sailor').")
    print("- He was executed by the Soviet state in its early years.")
    print("- His mass grave was finally located during the late 1980s.")

    # The person who fits all these criteria is Nikolai Gumilyov.
    last_name = "Gumilyov"
    
    print("\nThe person who fits this description is Nikolai Gumilyov.")
    print("Therefore, the requested last name is:")
    print(last_name)

solve_riddle()