# Step 1: Define the numbers from the final calculation step.
# The integral is I = constant_factor * integral_value
constant_factor = 81
# The value of the definite integral is a rational number.
integral_numerator = 4
integral_denominator = 27

# Step 2: Calculate the final result of the integral I.
result = constant_factor * (integral_numerator / integral_denominator)

# Step 3: Print the final equation with each number.
# The final equation is I = 81 * (4/27).
print("The final calculation is based on the equation:")
print(f"Integral = {constant_factor} * ({integral_numerator} / {integral_denominator})")
print("The value of the integral is:")
print(int(result))