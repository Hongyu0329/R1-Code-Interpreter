def solve_trilogy():
    """
    This function solves a three-part riddle to find a trilogy of cities
    and prints the result in the specified format.
    """

    # Clue 1: The city from Irving Wallace's "The Miracle".
    city_1 = "Lourdes"

    # Clue 2: The city associated with Yuri Krasnokutsky's poem about "Kholodny Yar".
    city_2 = "Chyhyryn"

    # Clue 3: The city of the main analogue to Zagreb's "Dolac" market.
    city_3 = "Paris"

    # Printing the final trilogy in the required format.
    print(f"{{{city_1}, {city_2}, {city_3}}}")

solve_trilogy()