def find_tacitus_quote():
    """
    This script constructs and prints the five-word Latin quote from Tacitus
    that fits the user's specific criteria.
    """
    
    # The five words of the Latin quote
    word1 = "integros"
    word2 = "servare"
    word3 = "et"
    word4 = "fortibus"
    word5 = "consulere"
    
    # Construct the full quote by joining the words with spaces
    # The instruction to "output each number in the final equation" is interpreted
    # here by explicitly defining each of the five words before printing the result.
    final_quote = f"{word1} {word2} {word3} {word4} {word5}"
    
    print(final_quote)

find_tacitus_quote()