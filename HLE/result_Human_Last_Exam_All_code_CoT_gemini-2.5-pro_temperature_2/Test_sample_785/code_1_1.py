N = 1000
num_orbits = N + 1
print(f"The number of orbits is determined by the number of possible conjugacy classes of an involutory matrix of size {N}x{N}.")
print(f"This number corresponds to the possible counts of the eigenvalue -1, which can be any integer from 0 to {N}.")
print(f"The number of orbits is {N} + 1 = {num_orbits}")