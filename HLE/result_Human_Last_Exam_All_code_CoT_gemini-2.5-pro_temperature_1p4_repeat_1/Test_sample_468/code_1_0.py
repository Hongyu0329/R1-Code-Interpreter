def get_perdita_common_name_info():
    """
    This function provides the common name for the genus Perdita and the year it was coined.
    """
    # The common name for the genus "Perdita" is "Fairy bees".
    common_name = "Fairy bees"

    # This name is widely attributed to P.H. Timberlake, the foremost 20th-century expert on the genus.
    # While he may have used it informally earlier, his landmark, multi-volume revision of the genus
    # began publication in 1954. This year represents the start of the period where his work
    # cemented the identity of these tiny bees, for which the descriptive name is so fitting.
    year_coined = 1954

    # Print the final answer in the format "<name>, <year>"
    print(f"{common_name}, {year_coined}")

get_perdita_common_name_info()