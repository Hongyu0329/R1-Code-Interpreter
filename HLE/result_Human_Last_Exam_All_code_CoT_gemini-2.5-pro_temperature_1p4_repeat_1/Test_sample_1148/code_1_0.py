# The essay is "Our Poor Individualism" by Jorge Luis Borges.
# In the essay, Borges cites three individuals to illustrate the "illusions of patriotism".

# The Greek who mocked chauvinism.
greek_figure = "Plutarch"

# The Englishman who claimed English superiority in thinking.
english_figure = "John Dryden"

# The German who claimed only Germans could be profound.
german_figure = "Heinrich von Treitschke"

# Combine the names into a single string, separated by commas.
final_answer = ", ".join([greek_figure, english_figure, german_figure])

# Print the final result.
print(final_answer)