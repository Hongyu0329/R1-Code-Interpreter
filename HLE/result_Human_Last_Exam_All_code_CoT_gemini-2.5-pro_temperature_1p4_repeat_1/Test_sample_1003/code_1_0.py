import math

# The problem simplifies to finding the value of the expression 2 - sqrt(2).
# We will calculate this value and print the equation.

# Define the numbers in the final equation
val_2 = 2
val_sqrt_2 = math.sqrt(2)

# Calculate the final result
result = val_2 - val_sqrt_2

# Output the equation with each number and the final result
print(f"The final equation is: {val_2} - {val_sqrt_2}")
print(f"The numerical result is: {result}")