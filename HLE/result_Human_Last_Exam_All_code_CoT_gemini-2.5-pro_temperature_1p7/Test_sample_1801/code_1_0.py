def solve_enron_commercial_question():
    """
    This function identifies and prints the names of the two Nobel laureates
    featured in the 2000 Enron "Ask Why" commercial.
    """
    # The two Nobel laureates in question are Amartya Sen and Paul Krugman.
    # Amartya Sen won the Nobel Prize in Economic Sciences in 1998.
    # Paul Krugman won the Nobel Prize in Economic Sciences in 2008, after the commercial aired.
    laureate1 = "Paul Krugman"
    laureate2 = "Amartya Sen"

    print(f"The two Nobel laureates featured in the 2000 Enron 'Ask Why' commercial were {laureate1} and {laureate2}.")

solve_enron_commercial_question()