def find_common_elements():
    """
    This function identifies and prints the common literary elements between
    Koji Suzuki's "Key West" and Charlotte Perkins Gilman's "The Yellow Wallpaper".
    """
    
    # Analysis reveals that elements I, II, III, IV, and V are common to both stories.
    # I. Physical confinement
    # II. Progressive detachment from reality
    # III. Indifference to others' emotions at the end
    # IV. Family member attempts to reunite at the end
    # V. External locus of control
    
    common_elements = ["I", "II", "III", "IV", "V"]
    
    # Format the output as a comma-separated string
    result = ", ".join(common_elements)
    
    print(result)

find_common_elements()