def find_borges_names():
    """
    This function stores and prints the names of the three figures
    mentioned in Jorge Luis Borges's essay "Our Poor Individualism".
    """
    # The essay is "Our Poor Individualism" ("Nuestro pobre individualismo").
    # The three figures Borges cites are:
    greek_figure = "Plutarch"
    english_figure = "Gibbon"
    german_figure = "Hindenburg"

    # Formatting the output as a comma-separated string.
    result = f"{greek_figure},{english_figure},{german_figure}"
    print(result)

find_borges_names()