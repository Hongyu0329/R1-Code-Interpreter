# The task is to provide the famous equation associated with Max Born.
# Max Born's key contribution to quantum mechanics is the Born rule.
# The rule states that the probability density of finding a particle at a point
# is the square of the magnitude of the wave function at that point.

# Print the name of the scientist
print("Name: Max Born")

# Print the equation associated with him.
# The equation is the Born rule: probability density = |Ψ|^2
print("Equation: |Ψ|^2")