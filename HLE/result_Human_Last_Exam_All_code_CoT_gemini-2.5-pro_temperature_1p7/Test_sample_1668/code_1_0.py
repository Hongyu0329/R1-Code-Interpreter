# Denominations of the 1992 series Belarusian ruble banknotes
bison_value = 100
elk_value = 5
lynx_value = 10
# The hare banknote is 50 kapeykas, which is 0.5 rubles.
hare_value = 0.5

# Calculate the total value by summing the individual denominations
total_value = bison_value + elk_value + lynx_value + hare_value

# Print the equation showing each number and the final sum
print(f"{bison_value} + {elk_value} + {lynx_value} + {hare_value} = {total_value}")