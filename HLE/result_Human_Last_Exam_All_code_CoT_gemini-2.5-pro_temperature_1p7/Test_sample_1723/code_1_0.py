# The user is asking for the name of a mushroom genus known for the color blue.
# While no genus actually produces a truly blue spore print, the genus Lactarius
# is famous for the species Lactarius indigo, a mushroom that is blue
# through-and-through and exudes a blue latex "milk" when cut.
# For this reason, it is the common answer to this classic question.

genus_name = "Lactarius"

print(genus_name)