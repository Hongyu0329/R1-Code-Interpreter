def solve_mushroom_riddle():
    """
    This function provides the name of the mushroom genus known for a
    distinctly blue species.

    While Lactarius indigo is a vibrant blue mushroom, it's a common
    misconception that its spore print is blue. The blue color is in the
    flesh and the latex it exudes. The actual spore print is yellowish-white.
    However, Lactarius is the genus most famously associated with this trait
    and is the expected answer to this question.
    """
    genus_name = "Lactarius"
    print(f"The genus of mushroom known to produce a distinctly blue color is: {genus_name}")

solve_mushroom_riddle()