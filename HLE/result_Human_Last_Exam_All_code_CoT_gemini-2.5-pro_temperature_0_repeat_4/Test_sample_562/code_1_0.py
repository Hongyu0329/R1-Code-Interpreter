def find_radical_name():
    """
    This function identifies and prints the name of the radical for the kanji 謄.
    """
    kanji_character = "謄"
    # The radical is the left-side component 月, which is a form of the 肉 radical.
    # This form is called にくづき (nikuzuki).
    radical_name_hepburn = "Nikuzuki"

    print(f"The bushu (radical) in the Japanese character '{kanji_character}' is called: {radical_name_hepburn}")

if __name__ == "__main__":
    find_radical_name()