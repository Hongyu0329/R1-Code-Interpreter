def find_common_elements():
    """
    This function identifies and prints the Roman numerals for the common elements
    between "Key West" and "The Yellow Wallpaper".
    """
    # List of Roman numerals for the identified common elements
    common_elements = ["II", "III", "IV"]

    # Join the elements with a comma and a space for clear output
    result = ", ".join(common_elements)

    print(result)

find_common_elements()