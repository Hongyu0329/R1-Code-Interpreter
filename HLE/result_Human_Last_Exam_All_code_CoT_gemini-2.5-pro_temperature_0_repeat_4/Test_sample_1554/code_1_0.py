def find_philosopher():
    """
    This function identifies and prints the name of the Lviv-Warsaw School
    philosopher who distinguished between symptoms and signals.
    """
    # The philosopher is Kazimierz Ajdukiewicz. He made this distinction
    # as part of his analysis of signs (or hallmarks).
    philosopher_name = "Kazimierz Ajdukiewicz"

    # The concepts in question
    broader_notion = "hallmark"
    component_1 = "symptoms"
    component_2 = "signals"

    print(f"The Lviv-Warsaw School philosopher who decomposes the notion of '{broader_notion}' into '{component_1}' and '{component_2}' is:")
    print(philosopher_name)

# Execute the function to provide the answer.
find_philosopher()