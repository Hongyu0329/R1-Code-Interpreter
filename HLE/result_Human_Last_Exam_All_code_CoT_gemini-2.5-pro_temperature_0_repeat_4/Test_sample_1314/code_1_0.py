def find_condition():
    """
    This function identifies and prints the primary disease associated with
    the doppelgänger syndrome.
    """
    # The doppelgänger syndrome, or heautoscopy, is a type of delusional
    # misidentification syndrome. While it can have various neurological causes,
    # it is recognized as a symptom within a primary psychiatric disorder.
    primary_disease = "Schizophrenia"

    print("A specific condition identified as a primary disease underlying the doppelgänger syndrome is:")
    print(primary_disease)

if __name__ == "__main__":
    find_condition()