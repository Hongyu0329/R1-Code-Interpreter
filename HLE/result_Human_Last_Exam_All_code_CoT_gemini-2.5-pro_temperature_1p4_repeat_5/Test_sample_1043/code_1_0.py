def solve_poetry_puzzle():
    """
    Identifies the poet and the most emphasized word from a poem snippet
    based on recordings of the poet's performance.
    """
    poet_last_name = "Ammons"
    emphasized_word = "holler"
    
    # The poem is "For Harold Bloom" by A. R. Ammons.
    # In recordings of his readings, Ammons gives a distinct, harsh emphasis
    # to the word "holler" to stress the contrast with the quiet house the speaker seeks.
    
    print(f"{poet_last_name}, {emphasized_word}")

solve_poetry_puzzle()