def find_cited_scientist():
    """
    This function identifies and prints the name of the scientist whose
    lectures and work were foundational to Albert Einstein's 1905 paper
    on Brownian motion.
    """
    # The scientist in question is Ludwig Boltzmann, whose work on statistical
    # mechanics and the kinetic theory of gases was a direct precursor to
    # Einstein's analysis.
    scientist_name = "Ludwig Boltzmann"

    # Print the answer in a full sentence.
    print(f"In his 1905 paper on Brownian motion, Albert Einstein cited the foundational work and concepts from the lectures of: {scientist_name}")

find_cited_scientist()