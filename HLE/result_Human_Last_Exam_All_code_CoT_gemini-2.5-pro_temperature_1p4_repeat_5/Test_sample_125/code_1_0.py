# This script identifies the first card shown by J. Daniel Atlas at the end of his opening scene.

# The rank of the card, represented as a number.
card_rank_number = 7

# The rank of the card, represented as a word.
card_rank_name = "Seven"

# The suit of the card.
card_suit = "Diamonds"

# We will now construct the full name of the card.
# The 'equation' here is combining the rank and the suit.
# The number from the 'equation' is 7.
print(f"Card Number: {card_rank_number}")
print(f"Full Card Name: {card_rank_name} of {card_suit}")