# This script solves the riddle by printing the correct name of the coffee drink.

# The riddle provides several clues:
# 1. The name starts with "Cafi," which is phonetically similar to "Caffè."
# 2. It's one of "THEM," a group of items that can be "correct" or have a "peculiar shape." This points to Italian coffee drinks.
# 3. "Correct" alludes to "Caffè Corretto."
# 4. "Peculiar shape" alludes to "macchiato," which means "stained" or "marked" in Italian.

# Combining these clues leads to the correct name.
correct_name = "Caffè Macchiato"

print(correct_name)