def find_radical_name():
    """
    This function identifies the radical for the kanji '謄' and prints its name.
    """
    kanji = "謄"
    # The radical for 謄 is 言.
    # When used as the left part of a character (hen), its name is ごんべん.
    # The Hepburn transcription of ごんべん is "Gonben".
    radical_name_hepburn = "Gonben"

    print(f"The bushu/radical for the character '{kanji}' is {radical_name_hepburn}.")

find_radical_name()