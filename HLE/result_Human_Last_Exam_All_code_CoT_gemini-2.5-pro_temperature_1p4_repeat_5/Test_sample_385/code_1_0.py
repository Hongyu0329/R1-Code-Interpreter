def find_tacitus_quote():
    """
    This function provides the five-word Latin quote attributed to Agricola by Tacitus.
    """
    # The quote is "honestam mortem turpi vitae potiorem".
    # It translates to "[consider] an honorable death preferable to a disgraceful life."
    # The output is formatted in all lowercase and without punctuation as requested.
    
    quote_words = ["honestam", "mortem", "turpi", "vitae", "potiorem"]
    
    # Print each word in the final quote
    print(" ".join(quote_words))

find_tacitus_quote()