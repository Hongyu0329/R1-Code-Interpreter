def find_common_elements():
    """
    This function identifies and prints the common literary elements between
    "The Yellow Wallpaper" and "Key West" based on a pre-determined analysis.
    """

    # Based on the analysis, the following elements are common to both stories:
    # I. Physical confinement
    # II. Progressive detachment from reality
    # III. Protagonist's indifference to others' emotions at the end
    # V. External locus of control
    # VI. Medical nature of the conflict
    common_elements = ["I", "II", "III", "V", "VI"]

    # Format the output as a comma-separated string
    answer = ", ".join(common_elements)

    print(answer)

