def solve_enron_commercial_question():
    """
    This function identifies and prints the names of the two Nobel laureates
    featured in a 2000 Enron commercial.
    """
    # At the time of the commercial in 2000, Myron Scholes had already won the Nobel in 1997.
    # Paul Krugman would later win the Nobel in 2008. Both are now Nobel laureates.
    laureate1 = "Paul Krugman"
    laureate2 = "Myron Scholes"

    print(f"The two Nobel laureates featured in the infamous 2000 Enron commercial with the tagline 'Ask Why' were {laureate1} and {laureate2}.")

solve_enron_commercial_question()