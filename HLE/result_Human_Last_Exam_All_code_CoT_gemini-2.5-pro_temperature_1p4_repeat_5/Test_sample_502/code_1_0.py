import sys

def solve_dimension_problem():
    """
    Solves the problem of finding the largest possible dimension for the quotient ring R/I.

    The problem asks for the largest possible dimension of R/I, where R is the
    polynomial ring C[x_1, ..., x_10] and I is the ideal generated by positive-degree
    homogeneous polynomials invariant under a group G. G is a finite group of
    order 10000 acting on C^10.
    """

    # Step 1: Define the given parameters from the problem.
    # The order of the group G.
    group_order = 10000
    # The dimension of the vector space the group acts on.
    space_dimension = 10

    # Step 2: State the relevant mathematical theorem.
    # In the invariant theory of finite groups, a fundamental result (often attributed to Chevalley)
    # states that the dimension of the coinvariant algebra is equal to the order of the group.
    # The coinvariant algebra is the quotient ring R/I defined in the problem.
    # This theorem holds for any finite group G acting on a vector space over a field
    # of characteristic zero, like the complex numbers C.
    #
    # The dimension is calculated as dim(R/I) = |G|.

    # Step 3: Apply the theorem to the specific values of the problem.
    # The group G acts on C^10, and its order is |G| = 10000.
    # The action is defined by a representation rho: G -> GL_10(C).
    # The dimension of the coinvariant ring is the order of the image of this representation, |rho(G)|.
    # If we interpret the problem as G being a subgroup of GL_10(C) (treating the GL_100(C) mention as a typo),
    # the action is faithful, meaning the dimension of R/I is exactly |G|.
    dimension_of_quotient_ring = group_order

    # Step 4: Address the "largest possible dimension" part of the question.
    # The problem asks for the largest possible dimension as G varies over all possible
    # groups of the specified type.
    # Since the dimension of R/I is always equal to the order of the group, which is fixed
    # at 10000, the dimension is constant.
    # Therefore, the largest possible dimension is this constant value.
    # We just need to ensure that such a group exists. A cyclic group of order 10000 generated
    # by a diagonal matrix with a primitive 10000-th root of unity is a valid example.
    largest_possible_dimension = dimension_of_quotient_ring

    # Step 5: Print the final answer and the reasoning.
    print("Let R = C[x_1, ..., x_10] be the polynomial ring, and G be a finite group of order 10000 acting on C^10.")
    print("Let I be the ideal of R generated by the positive-degree homogeneous G-invariant polynomials.")
    print("According to a fundamental theorem of invariant theory, the dimension of the quotient ring R/I is equal to the order of the group G.")
    print("")
    print("The final equation is:")
    print(f"Dimension(R/I) = |G|")
    print(f"Given that the order of the group |G| is {group_order}, the dimension is:")
    print(f"Dimension(R/I) = {dimension_of_quotient_ring}")
    print("")
    print(f"Since this dimension is constant for any such group G, the largest possible dimension is {largest_possible_dimension}.")


if __name__ == "__main__":
    solve_dimension_problem()
