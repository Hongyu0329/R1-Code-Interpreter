def find_borges_reference():
    """
    This function identifies and prints the novel and author
    Borges praised with the given descriptions.
    """
    author = "Juan Carlos Onetti"
    novel_title = "La vida breve (A Brief Life)"
    quote_description = "the intensity of a tiger and the variety that a chess duel can achieve"
    author_description = "a continuator (and simplifier) of Faulkner"

    print(f"The novel Jorge Luis Borges refers to is '{novel_title}'.")
    print(f"The author is {author}, whom Borges described as '{author_description}'.")
    print(f"Borges used the phrase \"{quote_description}\" to describe the novel's quality.")

find_borges_reference()