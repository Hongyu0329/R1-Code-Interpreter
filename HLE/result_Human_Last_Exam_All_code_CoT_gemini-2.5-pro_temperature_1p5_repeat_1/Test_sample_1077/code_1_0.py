def find_philosopher():
    """
    This function identifies and prints the name of the philosopher
    who wrote the specified letter to Martin Heidegger.
    """
    # The letter was written by the Spanish philosopher María Zambrano in 1928
    # after a disappointing meeting with Heidegger.
    philosopher_name = "María Zambrano"

    print(f"The philosopher who wrote the letter to Heidegger is: {philosopher_name}")

if __name__ == "__main__":
    find_philosopher()