def find_blue_mushroom_genus():
    """
    Identifies the mushroom genus famously associated with the color blue.
    
    While no mushroom genus is known to produce a truly blue spore print,
    the genus Lactarius is renowned for its blue latex and blue-staining flesh,
    particularly in the species Lactarius indigo. This is the common answer
    to this mycological trivia question.
    """
    
    # The genus known for its blue latex and bruising.
    genus_name = "Lactarius"
    
    # Print the name of the genus.
    print(f"The genus of mushroom known for its distinct blue latex is: {genus_name}")

find_blue_mushroom_genus()