def solve_bulgakov_riddle():
    """
    This function identifies and prints the solution to the riddle
    about parallel characters and birds in "The Master and Margarita".
    """
    # In chapter 18, Sokov, the buffet-manager, visits Professor Kuzmin.
    # A house sparrow flies into the consulting-room, tormenting the professor and unnerving Sokov.
    moscow_character = "Sokov"

    # The parallel scene is in chapter 2, during Pilate's interrogation of Yeshua.
    # A barn swallow repeatedly flies in and out of the colonnade, annoying the Procurator.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"

    # Print the final answer in the specified format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

solve_bulgakov_riddle()