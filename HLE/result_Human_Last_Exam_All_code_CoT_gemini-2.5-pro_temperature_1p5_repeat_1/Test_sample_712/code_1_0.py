# Triboluminescence is light produced when a material is mechanically stressed.
# The bacterium Photorhabdus luminescens produces a protein called luciferase.
# When crystals of this luciferase are crushed or broken, they emit light, which is a form of triboluminescence.
# Therefore, the genus is Photorhabdus.

# Storing the answer in a variable
