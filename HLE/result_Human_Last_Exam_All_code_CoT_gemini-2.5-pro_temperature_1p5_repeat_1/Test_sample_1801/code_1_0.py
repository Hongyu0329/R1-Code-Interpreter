def find_enron_laureates():
  """
  This function identifies and prints the names of the two Nobel laureates
  featured in a 2000 Enron commercial.
  """
  # The two individuals were economist Paul Krugman (who won the Nobel in 2008)
  # and economist Amartya Sen (who won the Nobel in 1998).
  laureate1 = "Paul Krugman"
