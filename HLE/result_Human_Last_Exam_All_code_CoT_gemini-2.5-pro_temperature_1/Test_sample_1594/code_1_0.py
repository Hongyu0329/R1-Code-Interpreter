def find_building_shape():
  """
  This function provides the answer to the geographical puzzle about the
  shape of the high school buildings in Pisa.
  """
  # The two parallel school buildings are connected in the middle,
  # forming a specific letter shape when viewed from above.
  letter_shape = "H"

  print(f"The buildings of Istituto Galilei-Pacinotti and Istituto Ulisse Dini in Pisa, when seen from above, form the letter: {letter_shape}")

find_building_shape()