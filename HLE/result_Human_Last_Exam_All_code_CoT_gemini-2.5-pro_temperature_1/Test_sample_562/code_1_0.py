def find_radical_name():
    """
    This function identifies and prints the name of the radical for the kanji 謄.
    """
    kanji = "謄"
    # The radical for 謄 is 肉 (niku), which appears on the left side (hen)
    # in a form that looks like the moon radical (月).
    # This specific form is called "nikuzuki" (肉月).
    radical_name_hepburn = "Nikuzuki"
    
    print(f"The radical for the character '{kanji}' is called '{radical_name_hepburn}'.")

find_radical_name()