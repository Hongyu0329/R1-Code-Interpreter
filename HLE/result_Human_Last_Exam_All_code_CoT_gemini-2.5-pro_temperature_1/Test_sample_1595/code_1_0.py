def solve_chesterton_riddle():
    """
    This function identifies and prints the name of the person
    who made a specific statement to G.K. Chesterton.
    """
    # The person who made the statement, according to Chesterton scholars.
    person_name = "his father, Edward Chesterton"

    # The quote in question.
    quote = "the doctrine of the Fall is the only cheerful view of human life"

    # Print the final answer in a clear, descriptive sentence.
    print(f"The person who told G.K. Chesterton that '{quote}' was {person_name}.")

# Execute the function to provide the answer.
solve_chesterton_riddle()