# Define the Chern numbers of the two individual insulators
C1 = 1
C2 = 1

# The Chern number of the junction is the sum of the individual Chern numbers
C_total = C1 + C2

# Print the result, showing the full equation
print(f"The first Chern insulator has a Chern number C1 = {C1}.")
print(f"The second Chern insulator has a Chern number C2 = {C2}.")
print(f"The Chern number of the junction is the sum of the individual numbers: C_total = {C1} + {C2} = {C_total}.")