# The equation is 🏮🏮🏮🏮 / 🍥🍥🍥 - 🍥🍥🍥∩∩∩||| = ?

# Step 1: Convert the Egyptian numerals to modern numbers.
# 🏮 = 1000, so 🏮🏮🏮🏮 = 4000
num1 = 4000
# 🍥 = 100, so 🍥🍥🍥 = 300
num2 = 300
# 🍥 = 100, ∩ = 10, | = 1, so 🍥🍥🍥∩∩∩||| = 300 + 30 + 3 = 333
num3 = 333

# Step 2: Perform the calculation.
result = num1 / num2 - num3

# Step 3: Print the equation in modern numbers along with the result.
print(f"{num1} / {num2} - {num3} = {result}")