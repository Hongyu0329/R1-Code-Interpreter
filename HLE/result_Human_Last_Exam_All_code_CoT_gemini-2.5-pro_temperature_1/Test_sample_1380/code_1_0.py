def find_bachelier_phenomenon():
    """
    This function identifies and prints the physical process mathematically
    modeled by Louis Bachelier in his work on financial markets.
    """
    # In his 1900 thesis, "The Theory of Speculation," Louis Bachelier developed
    # mathematical models for stock price fluctuations.
    # He discovered that the probability distribution of stock prices over time
    # followed a random walk, which is mathematically identical to the
    # physical process describing the random movement of particles suspended in a fluid.
    # This physical process is named after the botanist Robert Brown, who first
    # observed it in 1827. Bachelier's work was the first rigorous mathematical
    # modeling of this phenomenon, predating Einstein's more famous 1905 paper.
    
    phenomenon_name = "Brownian motion"
    
    print(f"The physical process that Louis Bachelier found to be mathematically identical to the spread of stock option probabilities is called: {phenomenon_name}")

find_bachelier_phenomenon()