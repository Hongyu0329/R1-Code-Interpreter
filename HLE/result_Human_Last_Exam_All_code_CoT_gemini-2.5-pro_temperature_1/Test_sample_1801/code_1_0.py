def find_enron_laureates():
    """
    This function identifies and prints the names of the two Nobel laureates
    featured in a specific 2000 Enron commercial.
    """
    # The two Nobel laureates in the commercial.
    laureate_one = "Paul Krugman"
    laureate_two = "Merton Miller"
    
    # The year the commercial was released.
    year = 2000

    # Print the final answer, including the number from the original question.
    print(f"In the year {year}, the two Nobel laureates featured in the Enron 'Ask Why' commercial were:")
    print(f"1. {laureate_one}")
    print(f"2. {laureate_two}")

find_enron_laureates()