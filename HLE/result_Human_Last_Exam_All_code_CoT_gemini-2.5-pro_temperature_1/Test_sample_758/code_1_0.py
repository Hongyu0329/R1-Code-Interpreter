def find_common_elements():
    """
    This function identifies and prints the common literary elements between
    Koji Suzuki's "Key West" and Charlotte Perkins Gilman's "The Yellow Wallpaper".
    """
    
    # Analysis revealed the following common elements:
    # II. The protagonist experiences a progressive detachment from reality.
    # III. The protagonist is indifferent to the emotions of others as the conflict is resolved.
    # V. The protagonist experiences an external locus of control throughout the story.
    
    common_elements = ["II", "III", "V"]
    
    # Format the output as a comma-separated string
    output = ", ".join(common_elements)
    
    print(output)

find_common_elements()