def find_french_equivalent():
    """
    This function provides the French equivalent of the Gaulish name "Odobeccus".
    
    The name "Odobeccus" is the Latinized form of the name of a 4th-century Frankish general.
    In French historical contexts, his name is most commonly rendered as "Édobich".
    This script will print this equivalent.
    """
    gaulish_name = "Odobeccus"
    french_equivalent = "Édobich"
    
    print(f"The French equivalent of the name '{gaulish_name}' is '{french_equivalent}'.")

find_french_equivalent()