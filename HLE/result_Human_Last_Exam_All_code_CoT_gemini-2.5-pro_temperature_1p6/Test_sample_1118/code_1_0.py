def find_symbolic_object():
    """
    This function identifies and prints the symbolic object in
    Curt Querner's 1933 self-portrait.
    """
    artist = "Curt Querner"
    year = 1933
    event = "escaped from Gestapo"
    symbolic_object = "a thistle"
    
    # The painting is titled "Self-Portrait with Thistle".
    # The prickly, resilient thistle is seen as a symbol of his resistance.
    
    print(f"In {year}, after he had {event}, the artist {artist} painted a self-portrait.")
