# This script prints the answer to the question about Kant's Critique of Judgment.
# Kant's account is descriptive in that it details the cognitive faculties (e.g., the free play of imagination and understanding)
# involved in making an aesthetic judgment.
# However, it is also normative because it sets the conditions for a *valid* judgment of taste.
# Kant argues that when we judge something to be beautiful, we do so with a claim to universal validity,
# meaning we believe everyone *ought* to agree. This "ought" introduces a prescriptive, or normative, standard.
# Therefore, the account is not purely descriptive.
print("The account is both descriptive and normative.")