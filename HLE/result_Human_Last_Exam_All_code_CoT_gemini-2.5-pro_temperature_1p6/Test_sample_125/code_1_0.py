# This script will identify and print the name of the first card shown by
# Jesse Eisenberg's character at the end of his opening scene in "Now You See Me".

# The card's value and suit are determined from the movie scene.
card_value = "Seven"
card_suit = "Hearts"

# Combine the value and suit to form the full card name.
full_card_name = f"{card_value} of {card_suit}"

# Print the final answer.
print(full_card_name)