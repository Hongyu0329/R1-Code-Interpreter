def solve_watch_riddle():
    """
    This function stores and prints the text found on a specific watch face.
    The watch is the Heuer Monaco, worn by Steve McQueen.
    The text "AUTOMATIC CHRONOGRAPH" is located directly above the date window.
    The answer is then converted to lowercase as requested.
    """
    
    # The text written on the watch face
    text_on_watch = "AUTOMATIC CHRONOGRAPH"
    
    # Convert the text to all lowercase
    answer = text_on_watch.lower()
    
