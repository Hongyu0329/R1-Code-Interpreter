def find_radical_name():
    """
    This function identifies and prints the Hepburn-romanized name of the radical for the kanji '謄'.
    """
    # The character in question is 謄.
    # The radical is the '言' component on the left, known as 訁.
    # Its Japanese name is ごんべん.
    # The Hepburn romanization is "Gonben".
    radical_name = "Gonben"
    print(radical_name)

find_radical_name()