def get_bee_common_name_info():
    """
    This function provides the common name and the year it was popularized
    for the bee genus Perdita.
    """
    # The common name "Fairy Bees" was widely popularized and arguably
    # coined for the genus Perdita in the influential 2016 field guide
    # "The Bees in Your Backyard" by Wilson & Carril.
    common_name = "Fairy Bees"
    year = 2016

    # Print the result in the specified format
    print(f"{common_name}, {year}")

get_bee_common_name_info()