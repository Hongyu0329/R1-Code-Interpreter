def find_cited_lecturer():
    """
    This function identifies and prints the name of the scientist whose lectures and
    writings on statistical mechanics were a key foundation for Albert Einstein's
    1905 paper on Brownian motion.
    """
    # The primary scientist whose work in statistical mechanics, developed
    # through his lectures and writings, was cited by Einstein.
    scientist = "Ludwig Boltzmann"

    # Print the full answer
    print(f"In his 1905 paper on Brownian motion, Albert Einstein cited the work based on the foundational lectures and writings on statistical mechanics of {scientist}.")

# Run the function to display the answer
find_cited_lecturer()