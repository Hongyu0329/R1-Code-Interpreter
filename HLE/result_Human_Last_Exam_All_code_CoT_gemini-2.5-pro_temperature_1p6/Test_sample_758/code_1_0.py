def solve_riddle():
    """
    This function determines and prints the common elements between "Key West" and "The Yellow Wallpaper".
    """
    # The common elements identified are II and V.
    # II: The protagonist experiences a progressive detachment from reality.
    # V: The protagonist experiences an external locus of control throughout the story.
    
    common_elements = ["II", "V"]
    
    # The final output should be the Roman numerals, separated by a comma.
    print(", ".join(common_elements))

solve_riddle()