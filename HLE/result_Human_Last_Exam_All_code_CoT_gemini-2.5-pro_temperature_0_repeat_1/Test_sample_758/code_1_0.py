def find_common_elements():
    """
    This function identifies and prints the common literary elements
    between "Key West" and "The Yellow Wallpaper" based on a predefined analysis.
    """
    # Based on the analysis, the following elements are common to both stories:
    # I. Physical confinement plays a key role in the central conflict.
    # II. The protagonist experiences a progressive detachment from reality.
    # III. The protagonist is indifferent to the emotions of others as the conflict is resolved.
    # V. The protagonist experiences an external locus of control throughout the story.
    
    common_elements = ["I", "II", "III", "V"]
    
    # The output should be a series of Roman numerals, in numerical order, separated by commas.
    result = ", ".join(common_elements)
    
    print(result)

