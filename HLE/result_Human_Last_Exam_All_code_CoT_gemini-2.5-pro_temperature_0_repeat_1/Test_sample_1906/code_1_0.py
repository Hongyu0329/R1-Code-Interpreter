# The proof that a cartesian closed abelian category must be trivial
# hinges on the fact that for any object X, its ring of endomorphisms
# must be the zero ring. The defining equation of the zero ring is 1 = 0.
# The following code prints the numbers in this final, decisive equation.

equation_numbers = [1, 0]
for number in equation_numbers:
    print(number)
