def find_israeli_statute():
    """
    This function identifies and prints the name of the first major Israeli statute
    to explicitly introduce the concept of "good faith" as a general principle.
    """
    # The Contracts (General Part) Law, 1973, is widely recognized as the
    # foundational statute that introduced the duty of good faith into Israeli law,
    # particularly in sections 12 and 39.
    statute_name = "Contracts (General Part) Law"
    print(statute_name)

find_israeli_statute()