# This script calculates the total rehearsal hours for a dance scene
# based on the number of dancers, weeks of rehearsal, and hours per week.

# Define the variables
num_dancers = 12
rehearsal_weeks = 4
hours_per_week_per_dancer = 10

# Calculate the total rehearsal hours
total_hours = num_dancers * rehearsal_weeks * hours_per_week_per_dancer

# Print the final equation with all the numbers
# This fulfills the requirement to "output each number in the final equation"
print(f"The calculation for total rehearsal hours is:")
print(f"{num_dancers} dancers * {rehearsal_weeks} weeks * {hours_per_week_per_dancer} hours/week = {total_hours} total hours")