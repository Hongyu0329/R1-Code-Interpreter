# The physicist is Max Born.
# His most famous contribution is the Born rule in quantum mechanics.
# The rule gives the probability density of finding a particle as the square of the magnitude of its wave function.
# The equation is |Ψ|², which contains the number 2 as an exponent.
# This script will print the name and the equation in the desired format.

name = "Max Born"
equation = r"\(|\Psi|^2\)"

print(f"Name: {name}")
print(f"Equation: {equation}")