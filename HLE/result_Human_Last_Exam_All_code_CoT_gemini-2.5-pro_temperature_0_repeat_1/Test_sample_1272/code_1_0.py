def find_province_of_defeat():
    """
    This function identifies and prints the current Vietnamese province
    where Ming General Mu Sheng experienced his first major defeat.
    """
    # The historical event is the Battle of Lãnh Câu-Đan Xá in 1426,
    # during the Lam Sơn Uprising led by Lê Lợi.
    # Historical sources place the location of this battle in the region
    # that corresponds to a specific modern Vietnamese province.
    province = "Thanh Hóa Province"
    
    print(f"Ming General Mu Sheng's first major defeat occurred in the area of modern-day: {province}")

find_province_of_defeat()