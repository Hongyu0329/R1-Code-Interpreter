def print_born_equation():
  """
  Prints the name Max Born and his famous equation.
  The Born rule states that the probability density (P) of finding a particle at a 
  given point is equal to the squared modulus of its wave function (Ψ).
  """
  name = "Max Born"
  
  # The equation P = |Ψ|²
  # P represents Probability density
  # |Ψ| represents the modulus of the wave function
  # ² represents the square
  equation_text = "P = |Ψ|²"
  
  print(f"Name: {name}")
  print(f"Equation: {equation_text}")

print_born_equation()