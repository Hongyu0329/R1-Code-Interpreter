# Define the numbers X and Y based on historical facts about Minsk.
x = 27  # Building number of the former exhibition hall at Yanki Kupaly st.
y = 1   # Number of the iconic tram route that passed through Kastryčnickaja st.

# Calculate X to the power of Y.
result = x ** y

# Print the explanation and the final equation with the result.
print(f"The exhibition hall was at building number X = {x}.")
print(f"The iconic tram route passing through Kastryčnickaja st. was number Y = {y}.")
print("The result of X raised to the power of Y is:")
print(f"{x} ^ {y} = {result}")