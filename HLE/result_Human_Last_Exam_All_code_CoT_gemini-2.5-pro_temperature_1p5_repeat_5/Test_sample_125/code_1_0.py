# The task is to identify the first card shown by Jesse Eisenberg's character
# in the opening scene of "Now You See Me" (2013).

# Storing the rank and suit of the card as variables.
rank = "Seven"
suit = "Diamonds"

# Constructing the full name of the card.
# This represents the "equation" of combining the rank and the suit.
final_card = f"{rank} of {suit}"

# Printing the final answer.
print(final_card)