# A regular icosahedron is a polyhedron with 20 faces.
# The faces are equilateral triangles.
# The convex hull (outer-hull) of a great icosahedron is a regular icosahedron.
# The task asks for the minimal number of triangles to triangulate this surface.
# Since the surface is already made of triangles, the number is simply the number of faces.

number_of_faces_on_icosahedron = 20

print(f"The outer-hull of the great icosahedron is a regular icosahedron.")
print(f"A regular icosahedron's surface is already composed of triangles.")
print(f"The number of triangular faces on a regular icosahedron is: {number_of_faces_on_icosahedron}")