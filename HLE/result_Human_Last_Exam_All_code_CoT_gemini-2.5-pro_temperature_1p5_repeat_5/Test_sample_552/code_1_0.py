def find_cited_lectures():
    """
    This function identifies and prints the name of the scientist whose lectures
    Albert Einstein cited in his 1905 paper on Brownian motion.
    """
    # Based on historical analysis of Einstein's 1905 Annalen der Physik paper
    # on Brownian motion, the work he cited was foundational to statistical mechanics.
    scientist_name = "Ludwig Boltzmann"
    
    # The specific work was Boltzmann's "Lectures on Gas Theory".
    # The code will print this information in a full sentence.
    
    print(f"In his 1905 paper on Brownian motion, Albert Einstein cited the lectures of {scientist_name}.")

# Execute the function to provide the answer.
find_cited_lectures()