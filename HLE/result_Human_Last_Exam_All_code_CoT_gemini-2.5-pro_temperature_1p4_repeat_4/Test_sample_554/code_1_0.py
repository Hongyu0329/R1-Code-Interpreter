# Step 1: Define the number of hydrogen atoms in a standard nanocar chassis (C30H20).
num_hydrogen_atoms = 20

# Step 2: In a "perfluoro-" compound, all hydrogen atoms are replaced by fluorine atoms.
# Therefore, the number of fluorine atoms equals the number of hydrogen atoms.
num_fluorine_atoms = num_hydrogen_atoms

# Step 3: Print the logic and the final equation.
print("A standard nanocar chassis has the chemical formula C30H20.")
print("The prefix 'perfluoro-' indicates that all hydrogen atoms are replaced by fluorine atoms.")
print("The equation is based on this replacement:")
print(f"Number of Hydrogen Atoms = {num_hydrogen_atoms}")
print(f"Number of Fluorine Atoms = {num_fluorine_atoms}")
