# The Gaulish name "Odobeccus" is a Latinized form.
# To find its French equivalent, we perform two main steps:
# 1. Remove the Latin masculine ending "-us".
#    "Odobeccus" -> "Odobecc"
# 2. Simplify the final double consonant "cc" to "c", a common process in the evolution of French.
#    "Odobecc" -> "Odebec"

french_equivalent = "Odebec"
print(f"The French equivalent of 'Odobeccus' is: {french_equivalent}")