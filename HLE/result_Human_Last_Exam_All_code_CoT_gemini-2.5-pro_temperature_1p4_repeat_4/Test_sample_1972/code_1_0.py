def find_borges_reference():
    """
    This function stores and prints the information about the novel and author
    Borges referenced with the given descriptions.
    """
    novel_title = "El astillero (The Shipyard)"
    author_name = "Juan Carlos Onetti"
    source_of_quote = "Borges's prologue to the novel"

    print(f"The novel Jorge Luis Borges praised is '{novel_title}'.")
