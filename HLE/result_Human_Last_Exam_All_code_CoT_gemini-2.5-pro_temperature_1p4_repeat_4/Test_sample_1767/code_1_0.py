# The Chern number of the first insulator
c1 = 1
# The Chern number of the second insulator
c2 = 1

# The Chern number of the combined system (the junction) is the sum of the individual Chern numbers.
c_total = c1 + c2

print(f"The Chern number of the first insulator is: {c1}")
print(f"The Chern number of the second insulator is: {c2}")
print("The Chern number of the junction is the sum of the individual Chern numbers.")
print(f"Equation: {c1} + {c2} = {c_total}")