def find_character():
    """
    This function identifies and prints the name of the character from
    William Morris's "The House of the Wolfings" who describes the
    Roman founding myth.
    """
    character_name = "Otter"
    explanation = (
        "In Chapter III, 'They Talk of the Romans', the character Otter, "
        "a Goth who was captured and raised among the Romans before returning to his people, "
        "describes Roman customs. He tells his kinsmen the story of the founding of the great "
        "Roman city by twin brethren who were suckled by a she-wolf, which the reader "
        "recognizes as the myth of Romulus and Remus."
    )

    print(f"The character is: {character_name}")
    print("\nDetails:")
    print(explanation)

find_character()