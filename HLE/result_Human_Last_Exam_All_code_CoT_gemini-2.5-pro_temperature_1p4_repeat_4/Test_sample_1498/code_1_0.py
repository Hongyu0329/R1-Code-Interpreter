def find_prophets_verses():
    """
    This function identifies and prints the range of verses (bayts)
    in 'Aqeedat al-'Awaam' where the prophets' names are mentioned.
    """
    
    # The list of the 25 prophets starts at verse 32 and ends at verse 35.
    start_bayt = 32
    end_bayt = 35

    print("In the poem 'Aqeedat al-'Awaam' by Sayyid Ahmad al-Marzuqi, the names of the Prophets are mentioned in the following verses:")
    print(f"From bayt number: {start_bayt}")
    print(f"To bayt number: {end_bayt}")

# Execute the function to display the result
find_prophets_verses()