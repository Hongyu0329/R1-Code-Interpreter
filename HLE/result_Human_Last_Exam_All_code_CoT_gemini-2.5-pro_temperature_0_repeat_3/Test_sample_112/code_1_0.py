def find_accompanying_book():
    """
    This function identifies and prints the name of the book of manners
    preserved alongside the medieval romance "Sir Launfal".
    """
    # The poem "Sir Launfal" by Thomas Chestre is preserved in the manuscript Cotton Caligula A.ii.
    poem = "Sir Launfal"
    
    # Research shows this manuscript also contains a well-known Middle English book of manners.
    book_of_manners = "The Boke of Curtasye"
    
