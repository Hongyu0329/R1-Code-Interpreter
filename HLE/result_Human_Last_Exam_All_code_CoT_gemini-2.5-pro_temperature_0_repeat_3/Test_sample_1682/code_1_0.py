    print("-" * 50)

    # Statement B Analysis
    print("Statement B: In the Zhou Dynasty, brides covered their heads with a thin silk veil before leaving.")
    print("Analysis: This is correct. The practice of veiling the bride is documented in ancient texts like the 'Book of Rites' from the Zhou period.")
