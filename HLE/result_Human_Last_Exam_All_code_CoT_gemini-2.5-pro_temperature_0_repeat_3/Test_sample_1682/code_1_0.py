    print("-" * 50)

    # Statement D Analysis
    print("Statement D: After the Song Dynasty, the xiapei, a shawl-like garment, became the standard attire for married women. The floral crowns...were particularly elaborate for royal consorts...referred to as the phoenix crown.")
    print("Analysis: This is correct. The xiapei was a formal shawl indicating rank, and the phoenix crown was an elaborate headdress for empresses and noblewomen, prominent in the Song and Ming dynasties.")
    print("-" * 50)

    # Statement E Analysis
    print("Statement E: During the Qing Dynasty, Han Chinese brides typically dressed in a red silk blouse beneath a red embroidered robe...and a wide red skirt...")
