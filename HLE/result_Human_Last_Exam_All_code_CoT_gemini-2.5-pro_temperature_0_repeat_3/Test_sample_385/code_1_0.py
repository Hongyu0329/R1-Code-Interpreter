# This script identifies the five-word Latin quote from Tacitus' "Agricola".
# The quote is "Honesta mors turpi vita potior".

# Define the words of the quote in a list.
quote_words = ["honesta", "mors", "turpi", "vita", "potior"]

# Combine the words into a single string, separated by spaces.
# The output will be in all lowercase and without punctuation as requested.
final_quote = " ".join(quote_words)

# Print the final formatted quote.
print(final_quote)