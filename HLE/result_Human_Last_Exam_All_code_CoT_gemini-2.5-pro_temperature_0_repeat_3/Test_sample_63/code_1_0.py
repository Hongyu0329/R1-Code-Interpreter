# The user wants to identify a man based on a clue from Kurt Vonnegut.
# The clue is that Vonnegut described a man as looking "like a porcupine".
# This description was aimed at the composer Johannes Brahms.
# However, the answer must be two words that start with the same letter.
# "Johannes Brahms" (J.B.) does not fit this rule.
# The riddle, therefore, points to a man with a similar "porcupine-like" appearance (bushy beard and hair)
# whose name does fit the rule.
# The poet Walt Whitman is famous for his bushy beard and his name fits the "W.W." pattern.

first_name = "Walt"
last_name = "Whitman"

print(f"{first_name} {last_name}")