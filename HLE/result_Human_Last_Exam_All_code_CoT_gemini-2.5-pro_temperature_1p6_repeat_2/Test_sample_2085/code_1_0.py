def find_french_equivalent():
    """
    This function explains and prints the French equivalent of the name Odobeccus.
    """
    gaulish_name = "Odobeccus"
    french_equivalent_1 = "Eudes"
    french_equivalent_2 = "Odon"

    print(f"The name '{gaulish_name}' is of Germanic origin, not strictly Gaulish.")
    print(f"The root 'Odo-' corresponds to the French names '{french_equivalent_1}' and '{french_equivalent_2}'.")
    print(f"Therefore, the French equivalent of {gaulish_name} is {french_equivalent_1} or {french_equivalent_2}.")

find_french_equivalent()