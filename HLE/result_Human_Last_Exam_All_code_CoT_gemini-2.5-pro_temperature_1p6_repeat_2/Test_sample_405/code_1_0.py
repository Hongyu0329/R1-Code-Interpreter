# The primary allotropes of pure phosphorus have distinct colors.
# We will list these colors to find the total number.

# List of colors for the main allotropes of phosphorus
colors_of_phosphorus = ["white", "red", "violet", "black"]

# Calculate the number of distinct colors
count = len(colors_of_phosphorus)

# Print the result
print(f"The distinct colors observed in pure allotropes of phosphorus are: {', '.join(colors_of_phosphorus)}.")
print(f"Based on this, the number of observable colors is {count}.")