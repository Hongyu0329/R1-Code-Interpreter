def solve_paleoclimate_isotope_question():
    """
    This script explains the reasoning to determine the climate conditions
    that lead to low levels of the 18O isotope in lake sulfate deposits.
    """

    print("Step 1: Understanding Oxygen Isotope Fractionation")
    print("--------------------------------------------------")
    print("Oxygen has two primary stable isotopes: a common, lighter isotope (16O) and a rare, heavier isotope (18O).")
    print("During evaporation, water molecules containing the lighter 16O isotope evaporate more easily.")
    print("This leaves the remaining body of water, like a lake, enriched with the heavier 18O isotope.")
    # As requested, outputting the numbers involved.
    print("The mass numbers of the isotopes in question are 16 and 18.")
    print("\n")

    print("Step 2: Identifying the Goal")
    print("-----------------------------")
    print("The question asks for conditions that cause LOW levels of 18O in sulfate deposits.")
    print("Sulfate forms using oxygen from the lake water, so we are looking for conditions that result in low 18O levels in the lake itself.")
    print("This means we need a climate that minimizes evaporation and/or maximizes the input of fresh water.\n")

    print("Step 3: Evaluating the Climate Factors")
    print("---------------------------------------")
    print(" - Temperature (Warm vs. Cold): A 'Cold' climate reduces the rate of evaporation. Less evaporation means the lake becomes less enriched in 18O.")
    print(" - Precipitation (Wet vs. Dry): A 'Wet' climate means more rainfall and runoff feeding the lake. This inflow of isotopically-light fresh water dilutes the lake's 18O concentration, leading to lower levels.")
    print(" - Lake Level (High vs. Shallow): 'High' lake levels are a direct result of a positive water balance where inflow exceeds evaporation. This state is consistent with conditions that keep 18O levels low.")
    print("\n")

    print("Step 4: Conclusion")
    print("--------------------")
    print("To achieve low 18O levels, we need a combination of minimal evaporation and maximum freshwater input.")
    print("This corresponds to a 'Wet, cold climate with high lake levels'.")
    print("This matches choice G.")

solve_paleoclimate_isotope_question()
