# The G.K. Chesterton story is "The Hammer of God".
# In it, Father Brown discusses the nature of love and pride with the murderer.
# He remarks that certain kinds of love, like for one's wife or home,
# should be intimate and humble, not a matter of competitive pride.
# To illustrate his point, he mentions two London place names.

place1 = "Brixton" # Starts with the paired consonant 'Br'
place2 = "Streatham" # Starts with the paired consonant 'St'

# The relevant quote is: "He might as well be proud of being born in Brixton
# as of being married in Streatham."

print("The two place names mentioned by Father Brown are:")
print(place1)
print(place2)