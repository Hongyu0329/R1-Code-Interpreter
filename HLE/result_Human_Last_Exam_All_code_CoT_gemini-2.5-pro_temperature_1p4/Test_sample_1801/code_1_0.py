def find_enron_laureates():
    """
    This function identifies and prints the names of two Nobel laureates
    who were featured in the infamous 2000 Enron "Ask Why" commercial campaign.
    """
    # At the time of the 2000 commercials, these individuals were Nobel laureates.
    # Myron Scholes and Robert Merton shared the 1997 Nobel Memorial Prize in Economic Sciences.
    laureate1 = "Myron Scholes"
    laureate2 = "Robert Merton"

    # Print the answer in a full sentence.
    print(f"Two Nobel laureates featured in the 2000 Enron 'Ask Why' commercial campaign were {laureate1} and {laureate2}.")

if __name__ == "__main__":
    find_enron_laureates()