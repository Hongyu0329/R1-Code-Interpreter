import math

def get_prime_factorization(n):
    """Computes the prime factorization of a given integer n."""
    factors = {}
    d = 2
    temp_n = n
    while d * d <= temp_n:
        while (temp_n % d) == 0:
            factors[d] = factors.get(d, 0) + 1
            temp_n //= d
        d += 1
    if temp_n > 1:
       factors[temp_n] = factors.get(temp_n, 0) + 1
    return factors

def main():
    """
    Calculates 10! and its prime factorization, then explains why there are no 3-manifolds
    with a fundamental group of this order.
    """
    n = 10
    val_n_factorial = math.factorial(n)
    
    print(f"The order of the fundamental group is {n}! = {val_n_factorial}.")

    factors = get_prime_factorization(val_n_factorial)
    
    equation_parts = []
    for p in sorted(factors.keys()):
        equation_parts.append(f"{p}^{factors[p]}")
    
    print("The prime factorization of this order is:")
    # The final prompt asks for printing each number in the equation.
    # We will print the full equation string, which includes all the numbers.
    equation_str = f"{n}! = " + " * ".join(equation_parts)
    print(equation_str)
    
    # Mathematical Reasoning
    print("\n--- Reasoning ---")
    print("1. A closed orientable 3-manifold with a finite fundamental group G must have the property that every abelian subgroup of G is cyclic.")
    
    order_p5 = 5**factors[5]
    print(f"2. For a group G of order 10!, any Sylow 5-subgroup has order {order_p5}. Since any group of order p^2 is abelian, this subgroup must be cyclic to satisfy the condition.")
    
    print("3. We must check if any group of order 10! satisfies this property. It is a known (though advanced) result that the symmetric group S_10 is the only group of order 10!.")
    
    print("4. Let's examine S_10. Consider the permutations a = (1, 2, 3, 4, 5) and b = (6, 7, 8, 9, 10).")
    print("   - Both 'a' and 'b' are in S_10.")
    print("   - Since they operate on disjoint sets of elements, they commute (ab = ba).")
    print("   - The subgroup H generated by 'a' and 'b' is abelian and has order 25.")
    print("   - This subgroup H is isomorphic to C_5 x C_5, which is not cyclic.")

    print("\n--- Conclusion ---")
    print("Since S_10 contains a non-cyclic abelian subgroup, it cannot be the fundamental group of a closed orientable 3-manifold.")
    print("As S_10 is the only group of order 10!, no such group exists.")
    print("Therefore, the number of such manifolds is 0.")

if __name__ == "__main__":
    main()