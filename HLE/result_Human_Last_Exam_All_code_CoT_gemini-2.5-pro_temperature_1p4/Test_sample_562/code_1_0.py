# The Japanese character is 謄.
# This character can be broken down into two components: 月 (on the left) and 朕 (on the right).
# The radical (bushu) is the component on the left, which is 月.
# When 月 appears as a radical on the left side of a character, it is often a variant of the radical 肉 (meat).
# The Japanese name for this radical form is にくづき (nikuzuki).
# This script will print the Hepburn transcription of "にくづき".

radical_name_hepburn = "Nikuzuki"
print(radical_name_hepburn)