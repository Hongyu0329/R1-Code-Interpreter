def find_french_equivalent():
    """
    This function provides the French equivalent for the Frankish name "Odobeccus".
    """
    # The name is Frankish (Germanic), not Gaulish, though the person lived in Gaul.
    # In French historical sources, the name is typically rendered as "Odobech".
    original_name = "Odobeccus"
    french_equivalent = "Odobech"

    print(f"The French equivalent of the name '{original_name}' is '{french_equivalent}'.")

find_french_equivalent()