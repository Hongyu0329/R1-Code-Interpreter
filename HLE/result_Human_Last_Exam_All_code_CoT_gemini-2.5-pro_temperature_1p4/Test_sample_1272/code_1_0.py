def find_province_of_defeat():
    """
    This function identifies and prints the modern Vietnamese province
    where Ming General Mu Sheng experienced his first major defeat during the Lam Son Uprising.
    """
    # The historical event is the Battle of Khả Lưu Pass in late 1424.
    # General Mu Sheng's relief army was defeated by Lê Lợi's forces.
    # This battle took place in the historical region of Nghệ An.
    province_name = "Nghệ An"

    print(f"The current Vietnamese province where Ming General Mu Sheng experienced his first major defeat is: {province_name}")

find_province_of_defeat()