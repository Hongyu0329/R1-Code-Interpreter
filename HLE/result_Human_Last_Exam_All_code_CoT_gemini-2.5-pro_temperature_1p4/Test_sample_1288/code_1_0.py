def solve_bulgakov_parallel():
    """
    This function identifies and prints the characters and birds
    involved in a parallel scene between Moscow and Jerusalem in
    Bulgakov's "The Master and Margarita".
    """
    
    # The character in Moscow who is the strongest thematic parallel to Pontius Pilate
    # is Rimsky, a man of authority tormented by supernatural events.
    moscow_character = "Rimsky"
    
    # In the Jerusalem chapters, a specific bird torments Pontius Pilate during the trial.
    jerusalem_bird = "barn swallow"
    
    # The character in Jerusalem tormented by the bird is the Procurator of Judea.
    jerusalem_character = "Pontius Pilate"
    
    # Print the final answer in the specified format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

solve_bulgakov_parallel()