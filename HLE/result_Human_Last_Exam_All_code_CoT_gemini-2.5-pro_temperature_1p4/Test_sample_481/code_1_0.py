def find_imprint_numbers():
    """
    This function identifies and prints the different numerical imprints
    on Teva brand fluoxetine 10mg and 20mg capsules.
    """
    # Numerical imprint for the 10mg capsule
    imprint_10mg = "3108"
    
    # Numerical imprint for the 20mg capsule
    imprint_20mg = "3109"
    
    # Print the findings
    print("The Teva brand fluoxetine 10mg capsule is imprinted with the number:")
    print(imprint_10mg)
    
    print("\nThe Teva brand fluoxetine 20mg capsule is imprinted with the number:")
    print(imprint_20mg)

if __name__ == "__main__":
    find_imprint_numbers()