def solve_riddle():
    """
    This function identifies and prints the common literary elements between
    Koji Suzuki's "Key West" and Charlotte Perkins Gilman's "The Yellow Wallpaper".
    """

    # I. Physical confinement plays a key role in the central conflict. (Common)
    # II. The protagonist experiences a progressive detachment from reality. (Common)
    # V. The protagonist experiences an external locus of control throughout the story. (Common)

    common_elements = ["I", "II", "V"]
    
    # Format the output as a string of Roman numerals, in numerical order, separated by commas.
    answer = ", ".join(common_elements)
    
    print(answer)

solve_riddle()
