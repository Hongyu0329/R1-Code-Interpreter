def find_accompanying_book():
    """
    This function provides the name of the book of manners
    preserved alongside the poem 'Sir Launfal'.
    """
    poem_in_question = "Sir Launfal"
    manuscript = "Cotton Caligula A.ii"
    book_of_manners = "The Boke of Curtasye"
    common_name = "The Book of Courtesy"

    print(f"The poem '{poem_in_question}' is preserved in the manuscript {manuscript}.")
    print(f"Alongside it is a book of manners known as '{book_of_manners}' (or '{common_name}').")

find_accompanying_book()