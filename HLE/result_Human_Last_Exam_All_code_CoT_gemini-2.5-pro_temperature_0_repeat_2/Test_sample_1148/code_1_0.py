def find_borges_names():
    """
    This function identifies and prints the names of the three individuals
    Jorge Luis Borges cited in his essay "Our Poor Individualism".
    """
    # The three individuals cited by Borges are Plutarch, Samuel Johnson, and Goethe.
    names = ["Plutarch", "Samuel Johnson", "Goethe"]
    
    # Print the names separated by a comma and a space.
    print(", ".join(names))

find_borges_names()