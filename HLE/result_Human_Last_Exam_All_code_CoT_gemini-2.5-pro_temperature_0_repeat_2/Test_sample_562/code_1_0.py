def find_radical_name():
    """
    This function identifies the radical for the kanji 謄 and prints its name.
    """
    # The kanji in question is 謄.
    # The radical is 言 (word), which is on the left side.
    # The name for the 言 radical when it's on the left is ごんべん (gonben).
    # The Hepburn transcription of ごんべん is "Gonben".
    radical_name = "Gonben"
    print(radical_name)

find_radical_name()