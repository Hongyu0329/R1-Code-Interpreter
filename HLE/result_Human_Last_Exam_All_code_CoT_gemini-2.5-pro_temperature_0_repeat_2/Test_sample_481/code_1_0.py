def find_imprint_numbers():
    """
    This function identifies and prints the different numbers imprinted on
    Teva brand fluoxetine 10mg and 20mg capsules.
    """
    # Imprint number for Teva fluoxetine 10mg
    imprint_10mg = "3105"

    # Imprint number for Teva fluoxetine 20mg
    imprint_20mg = "3106"

