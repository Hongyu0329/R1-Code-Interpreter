def solve_covalency_question():
    """
    This script explains the relationship between orbital overlap and covalency
    to determine which cerium compound is more covalent.
    """

    # Step 1: State the fundamental principle.
    print("Principle: The strength of covalency in a metal-ligand bond is directly proportional to the extent of orbital overlap between the metal and ligand orbitals.")
    print("Explanation: Greater overlap allows for more effective sharing of electron density, which is the definition of a stronger covalent bond.")
    print("-" * 30)

    # Step 2: State the information given in the problem.
    print("Given Information: The orbital overlap between Ce 4f and F 2p in CeF6(2-) is greater than the overlap between Ce 4f and Cl 3p in CeCl6(2-).")
    print("-" * 30)

    # Step 3: Draw the conclusion based on the principle and the given information.
    print("Conclusion: Since CeF6(2-) has greater orbital overlap, it will exhibit stronger covalent character.")
    print("\nTherefore, CeF6(2-) would display stronger covalency compared to CeCl6(2-).")

solve_covalency_question()