def solve_bulgakov_riddle():
    """
    This function identifies and prints the characters and bird
    based on the parallel structure in Bulgakov's "The Master and Margarita".
    """
    # In Chapter 18, "Unlucky Visitors", Andrei Fokich Sokov, the buffet-bar manager,
    # is deeply disturbed by a sparrow that flies into the hallway after his
    # dreadful meeting with Woland's gang.
    moscow_character = "Sokov"

    # The parallel scene occurs in Chapter 26, "The Burial". Pontius Pilate,
    # tormented by his decision, is in his study when a swallow flies in,
    # darts around him, and flies out, a small disturbance in his moment of immense anguish.
    jerusalem_bird = "barn swallow"
    jerusalem_character = "Pontius Pilate"

    # Print the final answer in the specified format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

solve_bulgakov_riddle()