# This script identifies and prints the two place names mentioned by Father Brown.
# The quote comes from the story "The Doom of the Darnaways," where Father Brown
# reflects on the nature of the marital vow.

def find_place_names():
  """
  Solves the riddle by providing the two place names.
  """
  # The two places Father Brown contrasts are a raid on Brighton with a crusade to Brittany.
  place1 = "Brighton"
  place2 = "Brittany"

  print("The two place names that begin with paired consonants are:")
  print(place1)
  print("and")
  print(place2)

find_place_names()