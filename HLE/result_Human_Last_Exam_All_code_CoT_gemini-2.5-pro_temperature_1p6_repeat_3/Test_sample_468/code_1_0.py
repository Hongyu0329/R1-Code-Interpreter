def find_bee_common_name_info():
    """
    This function stores and formats the common name and year of coining
    for the bee genus "Perdita".

    The information is based on research into entomological literature. The common
    name "fairy bees" is widely used. The year 1980 corresponds to its
    prominent use in the widely distributed "Audubon Society Field Guide to
    North American Insects and Spiders".
    """
    
    # The currently used common name for the genus Perdita
    common_name = "fairy bees"
    
    # The year the name was popularized in a major publication
    year_coined = 1980
    
    # Print the result in the specified format
    print(f"{common_name}, {year_coined}")

find_bee_common_name_info()