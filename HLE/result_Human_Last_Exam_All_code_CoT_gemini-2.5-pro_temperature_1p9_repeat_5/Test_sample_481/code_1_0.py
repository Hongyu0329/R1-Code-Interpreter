def find_imprints():
    """
    This function identifies and prints the different numbers imprinted on Teva brand
    fluoxetine 10mg and 20mg capsules.
    """
    
    # Imprint number for the 10mg capsule
    imprint_10mg = "3105"
    
    # Imprint number for the 20mg capsule
    imprint_20mg = "3109"
    
    print(f"The number imprinted on the Teva fluoxetine 10mg capsule is {imprint_10mg}.")
    print(f"The number imprinted on the Teva fluoxetine 20mg capsule is {imprint_20mg}.")

if __name__ == "__main__":
    find_imprints()