# First, we define the modern numbers corresponding to the Roman numerals.
num1 = 8  # This is VIII
num2 = 300 # This is CCC
num3 = 297 # This is CCXCVII

# Next, we perform the calculation as per the equation.
result = num1 / num2 - num3

# Finally, we print the equation in modern numbers along with the solution.
print(f"{num1} / {num2} - {num3} = {result}")