def find_influential_lecturer():
    """
    Identifies the physicist whose lectures and work were a key foundation for
    Einstein's 1905 paper on Brownian motion.
    """
    # The paper built upon the principles of statistical mechanics and the kinetic theory of heat.
    # The primary pioneer in this field, whose published work included lecture notes,
    # was Ludwig Boltzmann. Einstein developed his theory based on Boltzmann's framework.
    physicist_name = "Ludwig Boltzmann"

    print("In his 1905 paper on Brownian motion, Albert Einstein's work was fundamentally based on the principles developed by:")
    print(physicist_name)

find_influential_lecturer()