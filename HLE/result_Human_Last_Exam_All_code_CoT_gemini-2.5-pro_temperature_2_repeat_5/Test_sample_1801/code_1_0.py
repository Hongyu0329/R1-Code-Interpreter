def find_laureates():
    """
    This function identifies and prints the names of the two Nobel laureates
    who were featured in an infamous 2000 Enron commercial.
    """
    # Paul Krugman won the Nobel in Economic Sciences in 2008.
    # Amartya Sen won the Nobel in Economic Sciences in 1998.
    laureate1 = "Paul Krugman"
    laureate2 = "Amartya Sen"

    print(f"The two Nobel laureates featured in the 2000 Enron commercial 'Ask Why' were {laureate1} and {laureate2}.")

find_laureates()