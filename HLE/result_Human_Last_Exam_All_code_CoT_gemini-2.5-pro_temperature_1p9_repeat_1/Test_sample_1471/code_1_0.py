# The equation is Ⅳ/ⅭⅭ-ⅭⅭⅠⅠⅠⅠ=?
# First, let's convert the Roman numerals to modern numbers.
# Ⅳ = 4
# ⅭⅭ = 200
# ⅭⅭⅠⅠⅠⅠ = 204

# Now let's define these numbers as variables in Python.
num1 = 4
num2 = 200
num3 = 204

# Next, we will perform the calculation as per the order of operations (division first, then subtraction).
# Calculation: 4 / 200 - 204
result = (num1 / num2) - num3

# Finally, we will print the equation with the result.
print(f"{num1} / {num2} - {num3} = {result}")