# This script identifies the radical (bushu) for the kanji '謄' and prints its name in Hepburn transcription.
# The character 謄 is composed of the radical 言 (word) on the left and the component 朕 on the right.
# The radical 言 on the left side of a character is called "gonben" (ごんべん).

radical_name = "Gonben"
print(radical_name)