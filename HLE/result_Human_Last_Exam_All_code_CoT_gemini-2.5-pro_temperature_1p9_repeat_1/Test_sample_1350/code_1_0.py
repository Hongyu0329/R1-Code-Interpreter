# This script provides the answer to the literary question.
# The question asks to identify a character in William Morris's "The House of the Wolfings."
# This character visited a Roman city and described what is recognizably the myth of Romulus and Remus.

# The character is Otur, the son of the Wood-Sun of the Laxing-folk.
# In Chapter VI, "They Hear Tidings of the Romans," Otur describes his travels.
# He tells of a statue he saw in a Roman city depicting a she-wolf suckling two male infants,
# and he recounts the city's founding myth associated with them.

character_name = "Otur"

print("In William Morris's The House of the Wolfings, the character who describes a myth akin to Romulus and Remus is:")
print(character_name)
