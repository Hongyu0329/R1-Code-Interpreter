# The separatrix of the system is given by an equation of the form d = a * u**b.
# This script defines the values of the coefficient 'a' and the exponent 'b'
# and then prints the final equation.

# Define the coefficient 'a'
a = -1

# Define the exponent 'b'
b = 2

# Print the values of the parameters.
print(f"The equation for the separatrix is of the form: d = a * u**b")
print(f"The coefficient a is: {a}")
print(f"The exponent b is: {b}")

# Print the final equation for the separatrix.
print(f"The separatrix is given by the equation: d = {a} * u**{b}")
