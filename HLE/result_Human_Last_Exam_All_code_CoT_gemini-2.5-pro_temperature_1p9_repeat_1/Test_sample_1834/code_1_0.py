import math

def calculate_magnetic_field():
    """
    Calculates the magnetic field at a point P due to two infinite wires.
    """
    # Define the point of interest P(x,y,z)
    x, y, z = 1, -1, 0

    print("This script calculates the magnitude of the magnetic field at the point P(x,y,z) = ({}, {}, {}).".format(x, y, z))
    print("The field is generated by two infinite current-carrying wires.\n")
    print("The magnitude of the magnetic field from a single infinite wire is B = (μ₀ * I) / (2 * π * r), where r is the perpendicular distance to the wire.\n")

    # --- Step 1: Analyze Wire 1 (along x-axis, current in +x direction) ---
    print("--- Contribution from Wire 1 (on x-axis) ---")
    # The perpendicular distance r1 from P(x,y,z) to the x-axis is sqrt(y^2 + z^2)
    r1 = math.sqrt(y**2 + z**2)
    print("The distance r1 from point P to the x-axis is: sqrt({}^2 + {}^2) = {}".format(y, z, r1))
    
    # By the right-hand rule, with current in +x, the field at a point with y<0 is in the +z direction.
    print("The magnetic field B1 from this wire points in the +z direction.")
    print("Its magnitude is B1 = (μ₀ * I) / (2 * π * {})\n".format(r1))

    # --- Step 2: Analyze Wire 2 (along y-axis, current in +y direction) ---
    print("--- Contribution from Wire 2 (on y-axis) ---")
    # The perpendicular distance r2 from P(x,y,z) to the y-axis is sqrt(x^2 + z^2)
    r2 = math.sqrt(x**2 + z**2)
    print("The distance r2 from point P to the y-axis is: sqrt({}^2 + {}^2) = {}".format(x, z, r2))

    # By the right-hand rule, with current in +y, the field at a point with x>0 is in the -z direction.
    print("The magnetic field B2 from this wire points in the -z direction.")
    print("Its magnitude is B2 = (μ₀ * I) / (2 * π * {})\n".format(r2))

    # --- Step 3: Combine the fields (Superposition) ---
    print("--- Total Magnetic Field ---")
    print("The total magnetic field B_total is the vector sum of B1 and B2.")
    print("B_total = B1(z-direction) + B2(z-direction)")
    print("B_total = [ (μ₀ * I) / (2 * π * r1) ] - [ (μ₀ * I) / (2 * π * r2) ] in the z-direction.\n")
    
    # --- Step 4: Final Calculation ---
    print("--- Final Magnitude Calculation ---")
    print("Substituting the calculated values for r1 and r2 into the equation:")
    # Here we show the equation with the numbers plugged in.
    final_equation = "Magnitude = | (μ₀*I / (2*π * {})) - (μ₀*I / (2*π * {})) |".format(r1, r2)
    print(final_equation)

    # Since r1 and r2 are equal, the magnitudes are equal, and the fields cancel out.
    total_magnitude = 0.0
    print("Since the two terms are identical, their difference is zero.")
    print("\nFinal Result:")
    print("The magnitude of the total magnetic field at P({}, {}, {}) is: {}".format(x, y, z, total_magnitude))

# Execute the function
calculate_magnetic_field()
