print("This maximizes the number of active interface sites crucial for the reaction.")
print("\nThe ideal Ni/Ce molar ratio is calculated as follows:")
print(f"Ideal Ratio = Moles of Ni / Moles of Ce")
# We explicitly print each number in the final equation as requested.
print(f"Ideal Ratio = {moles_Ni} / {moles_Ce} = {ideal_ratio:.3f}")