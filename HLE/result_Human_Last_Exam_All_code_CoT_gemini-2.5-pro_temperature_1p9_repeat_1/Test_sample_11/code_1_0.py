def get_statute_name():
    """
    This function returns the name of the first statute in modern Israel
    to explicitly introduce the concept of "good faith".
    """
    # The Contracts (General Part) Law, 1973 is widely cited by legal scholars
    # as the landmark legislation that introduced the "good faith" principle
    # (Tom Lev) as a fundamental concept into Israeli law.
    statute = "Contracts (General Part) Law"
    return statute

# Print the name of the statute
print(get_statute_name())
