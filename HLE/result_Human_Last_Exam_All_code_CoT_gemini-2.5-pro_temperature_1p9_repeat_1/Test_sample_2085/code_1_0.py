def find_french_equivalent():
    """
    This function explains and prints the French equivalent of the Gaulish name 'Odobeccus'.
    """
    gaulish_name = "Odobeccus"
    french_equivalent = "Eudes"

    print(f"Finding the French equivalent for the Gaulish name '{gaulish_name}'...")
    print("Through linguistic evolution, the name transformed over time.")
    print(f"The name '{gaulish_name}' is the Latinized form of a Germanic name, which evolved into the modern French name 'Eudes'.")
    print("\n---")
    print(f"The French equivalent of '{gaulish_name}' is:")
    print(french_equivalent)

if __name__ == "__main__":
    find_french_equivalent()