def find_dinosaur_chemical():
    """
    This function identifies and prints the name of a polycyclic hydrocarbon
    named after a creature extinct for over 65 million years.
    """
    # Pterodactyls are flying reptiles that went extinct with the dinosaurs.
    # A synthetic polycyclic hydrocarbon was named after them due to its shape.
    chemical_name = "Pterodactyladiene"
    
    # Print the result to the user.
    print(f"The polycyclic hydrocarbon named after a creature (a Pterosaur) that has been extinct for over 65 million years is: {chemical_name}")

find_dinosaur_chemical()