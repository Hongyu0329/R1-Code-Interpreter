def find_first_card():
    """
    This function stores and prints the name of the first card
    J. Daniel Atlas shows to the camera in the opening scene of
    the movie "Now You See Me" (2013).
    """
    
    # The rank and suit of the card are determined by watching the movie scene.
    card_rank = "Seven"
    card_suit = "Diamonds"
    
    # Format the final answer string.
    final_card = f"{card_rank} of {card_suit}"
    
    print(final_card)

find_first_card()