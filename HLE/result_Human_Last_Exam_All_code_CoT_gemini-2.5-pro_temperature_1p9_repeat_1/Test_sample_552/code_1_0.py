def find_cited_scientist():
    """
    Identifies and prints the name of the scientist whose work and lectures
    were cited by Albert Einstein in his 1905 paper on Brownian motion.
    """
    # Ludwig Boltzmann's work on statistical mechanics and the kinetic theory of gases
    # was the primary foundation for Einstein's analysis. While modern citation
    # style was not used, Einstein built directly upon Boltzmann's principles.
    scientist_name = "Ludwig Boltzmann"
    paper_info = "his 1905 paper on Brownian motion"

    print(f"Albert Einstein cited principles from the work and lectures of {scientist_name} in {paper_info}.")

find_cited_scientist()