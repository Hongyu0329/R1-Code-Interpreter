def find_chesterton_place_names():
    """
    This function identifies and prints the two place names from a G.K. Chesterton
    work that begin with paired consonants (consonant digraphs) and are mentioned
    in a reflection on modern relationships.
    """
    # The place names are from the novel "The Man Who Was Thursday".
    # 'Ch' and 'Sh' are the paired consonants (digraphs).
    place_name_1 = "Chiswick"
    place_name_2 = "Shepherd's Bush"
    
    print(f"The first place name is: {place_name_1}")
    print(f"The second place name is: {place_name_2}")

find_chesterton_place_names()