def find_laureates():
    """
    This function identifies and prints the names of the two Nobel laureates
    featured in Enron's 2000 "Ask Why" commercial.
    """
    # The two Nobel laureates were Amartya Sen and Paul Krugman.
    # Amartya Sen won the Nobel Memorial Prize in Economic Sciences in 1998.
    # Paul Krugman won the same prize in 2008, after the commercial aired.
    laureate_1 = "Amartya Sen"
    laureate_2 = "Paul Krugman"

    print("The two Nobel laureates featured in the infamous 2000 Enron 'Ask Why' commercial were:")
    print(f"1. {laureate_1}")
    print(f"2. {laureate_2}")

find_laureates()