# In Maria Khoreva's 2017 Paquita pas de trois variation, the cabrioles
# are performed as the final step in a repeating sequence on a diagonal.

# Number of cabrioles performed on the diagonal.
diagonal_cabrioles = 4

# Number of cabrioles performed in other parts of the variation.
other_cabrioles = 0

# Calculate the total number of cabrioles.
total_cabrioles = diagonal_cabrioles + other_cabrioles

# Print the final calculation and result.
print(f"The total number of cabrioles devants is calculated as follows:")
print(f"Number of cabrioles on the diagonal: {diagonal_cabrioles}")
print(f"Number of cabrioles in other sections: {other_cabrioles}")
print(f"Final Equation: {diagonal_cabrioles} + {other_cabrioles} = {total_cabrioles}")