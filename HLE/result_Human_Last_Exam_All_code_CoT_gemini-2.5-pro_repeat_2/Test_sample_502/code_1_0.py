import math

def solve_invariant_theory_problem():
    """
    This script solves the problem by explaining the relevant mathematical theory
    and constructing an example to show the maximum value is attainable.
    """

    # Problem parameters
    n_vars = 10  # Dimension of the vector space C^n
    group_order = 10000  # Order of the finite group G

    print("Step 1: Understand the problem setup.")
    print(f"We are given a finite group G of order |G| = {group_order}.")
    print(f"G acts on the space C^{n_vars}, and on the polynomial ring R = C[x_1, ..., x_{n_vars}].")
    print("We want to find the maximum possible dimension of the quotient ring R/I, where I is the ideal generated by positive degree homogeneous G-invariant polynomials.")
    print("This quotient ring R/I is known as the coinvariant ring.\n")

    print("Step 2: State the key theorem from Invariant Theory.")
    print("A theorem by Chevalley states that for any finite group G acting on C^n, the dimension of the coinvariant ring is bounded by the order of the group.")
    print("Theorem: dim(R/I) <= |G|")
    print(f"In our case, this means dim(R/I) <= {group_order}.\n")

    print("Step 3: Determine when the maximum is achieved.")
    print("The same theorem provides a condition for when this inequality becomes an equality.")
    print("Equality holds, i.e., dim(R/I) = |G|, if and only if G is a complex reflection group.")
    print("A complex reflection is an element of GL_n(C) that fixes a hyperplane (a subspace of dimension n-1) pointwise.")
    print("A complex reflection group is a group generated by such elements.\n")

    print("Step 4: Check for the existence of a suitable complex reflection group.")
    print(f"So, the largest possible dimension for R/I is {group_order}, provided that a complex reflection group G acting on C^{n_vars} with |G| = {group_order} exists.")
    print("Let's see if we can construct such a group.")
    print(f"Consider the cyclic group C_k of order k={group_order}.")
    print(f"Let's define a representation of this group in GL_{n_vars}(C) = GL_{10}(C).")
    zeta = f"exp(2*pi*i / {group_order})" # Symbolic representation
    print(f"Let the generator of C_{group_order} act on C^{10} by the diagonal matrix:")
    print(f"g = diag({zeta}, 1, 1, 1, 1, 1, 1, 1, 1, 1)")
    print("The group G generated by g is isomorphic to C_10000 and has order 10000.")
    print("Let's check if g is a complex reflection.")
    print("The fixed points of g are vectors (x_1, ..., x_10) such that g * v = v.")
    print(f"This means ({zeta}*x_1, x_2, ..., x_10) = (x_1, x_2, ..., x_10).")
    print(f"Since {zeta} is not 1, this requires x_1 = 0.")
    print("The fixed space is the set of vectors where x_1=0, which is a subspace of dimension 10 - 1 = 9.")
    print("A subspace of dimension 9 is a hyperplane in C^10.")
    print("Therefore, g is a complex reflection, and the group G it generates is a complex reflection group.\n")

    print("Step 5: Conclusion.")
    print("We have established the following:")
    print(f"1. The dimension of R/I is at most |G| = {group_order}.")
    print(f"2. This maximum value is achieved if G is a complex reflection group.")
    print(f"3. We have constructed a complex reflection group G of order {group_order} in GL_{n_vars}(C).")
    print("Therefore, the largest possible dimension for R/I is the order of the group.")

    final_answer = group_order
    print("\nFinal Answer Equation:")
    print(f"max(dim(R/I)) = |G|")
    print(f"max(dim(R/I)) = {final_answer}")


if __name__ == '__main__':
    solve_invariant_theory_problem()