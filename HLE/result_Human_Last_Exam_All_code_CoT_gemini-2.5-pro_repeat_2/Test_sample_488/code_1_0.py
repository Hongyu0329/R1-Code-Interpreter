def analyze_metaphor():
    """
    Analyzes the conceptual metaphor in the phrase "my love for humanity".
    """
    phrase = "my love for humanity"
    
    print(f"Analyzing the phrase: \"{phrase}\"\n")
    
    print("Step 1: Understanding the types of metaphors.")
    print(" - Orientational Metaphor: Relates concepts to spatial orientation (e.g., UP/DOWN, IN/OUT). Example: 'HAPPY IS UP' as in 'I'm feeling up today.'")
    print(" - Structural Metaphor: Structures one abstract concept in terms of another, more concrete concept. Example: 'ARGUMENT IS WAR' as in 'He won the argument.'\n")
    
    print("Step 2: Analyzing the phrase itself.")
    print(f"The phrase \"{phrase}\" treats the abstract emotion 'love' as if it were a physical substance or object.")
    print("Specifically:")
    print(" - It can be possessed: 'MY love'. This treats love like property.")
    print(" - It can be contained and directed: 'love FOR humanity'. This frames love as a substance held by a person and directed at a target.\n")

    print("Step 3: Conclusion.")
    print("This act of treating an abstract concept (love) as a physical entity (a possessable substance) is a way of giving it structure.")
    print("This falls under the category of a Structural Metaphor, as it imposes the structure of a physical object/substance onto the abstract concept of love.")
    print("It is not an Orientational Metaphor because it does not rely on spatial orientation like up/down or in/out.")

if __name__ == '__main__':
    analyze_metaphor()