# This script calculates the total number of entrelacés in the specific variation.

# The performance in question is by one dancer, Natalia Osipova.
number_of_dancers = 1

# In her 2009 Bolshoi debut variation in "The Kingdom of the Shades",
# Osipova famously performed 21 of these jumps.
entrelaces_per_dancer = 21

# The equation is a simple multiplication to find the total.
total_entrelaces = number_of_dancers * entrelaces_per_dancer

# The final output, showing each number in the equation as requested.
print(f"In the famous variation, there was {number_of_dancers} dancer performing {entrelaces_per_dancer} entrelacés.")
print(f"The calculation is: {number_of_dancers} * {entrelaces_per_dancer} = {total_entrelaces}")