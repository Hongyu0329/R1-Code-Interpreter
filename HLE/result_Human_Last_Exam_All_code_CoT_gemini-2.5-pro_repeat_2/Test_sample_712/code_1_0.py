def find_triboluminescent_bacterium():
    """
    This function provides the name of the bacterium genus known for
    producing a chemical compound that exhibits triboluminescence.
    """
    # The genus of bacterium that produces a chemical compound
    # exhibiting triboluminescent properties is Photorhabdus.
    # This is a distinct phenomenon from their more famous bioluminescence.
    genus = "Photorhabdus"

    print(f"The genus of bacterium that produces a chemical compound that exhibits triboluminescent properties is: {genus}")

if __name__ == "__main__":
    find_triboluminescent_bacterium()