def get_iupac_name():
    """
    This function returns the IUPAC name of the product from the reaction of
    methyl phenyl sulfoxide with triflic anhydride and trimethylsilyl cyanide.
    
    The reaction is a Pummerer rearrangement.
    1. Activation of the sulfoxide with triflic anhydride.
    2. Formation of a thionium ion intermediate.
    3. Nucleophilic attack by cyanide.
    
    Product Structure: C6H5-S-CH2-CN
    IUPAC Naming:
    - Principal group: -CN (nitrile)
    - Parent chain: ethanenitrile
    - Substituent: phenylthio group at position 2
    """
    iupac_name = "2-(phenylthio)ethanenitrile"
    print(iupac_name)

get_iupac_name()