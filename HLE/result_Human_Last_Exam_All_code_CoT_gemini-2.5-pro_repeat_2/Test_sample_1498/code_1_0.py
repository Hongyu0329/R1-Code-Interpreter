def find_prophet_verses():
    """
    This function identifies and prints the range of verses (bayt) in
    'Aqeedat al-'Awaam' that list the names of the Prophets.
    """
    # The enumeration of the 25 prophets begins at verse 34.
    start_bayt = 34

    # The list of names concludes at verse 37.
    end_bayt = 37

    print("In Sayyid Ahmad al-Marzuqi's poem 'Aqeedat al-'Awaam' (The Creed of the Laymen), the names of the Prophets are mentioned in the verses spanning from:")
    print(f"Starting Bayt: {start_bayt}")
    print(f"Ending Bayt: {end_bayt}")
    print("\nTherefore, the names are listed from bayt 34 to bayt 37.")

find_prophet_verses()