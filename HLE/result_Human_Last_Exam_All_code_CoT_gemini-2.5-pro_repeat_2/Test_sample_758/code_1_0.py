def find_common_elements():
    """
    This function identifies and prints the common elements between
    "Key West" by Koji Suzuki and "The Yellow Wallpaper" by Charlotte Perkins Gilman.
    """
    # Based on literary analysis, the common elements are:
    # I. Physical confinement plays a key role in the central conflict.
    # II. The protagonist experiences a progressive detachment from reality.
    # V. The protagonist experiences an external locus of control throughout the story.
    
    common_elements = ["I", "II", "V"]
    
    # Format the output string as requested
    output_string = ", ".join(common_elements)
    
    print(output_string)

find_common_elements()