def find_prophet_verses_range():
    """
    This function identifies and prints the range of bayts (verses) in
    the poem 'Aqeedat al-'Awaam' where the names of the Prophets are listed.
    """
    # The starting bayt where the names of the prophets are mentioned.
    start_bayt = 20

    # The ending bayt where the list of prophets' names concludes.
    end_bayt = 23

