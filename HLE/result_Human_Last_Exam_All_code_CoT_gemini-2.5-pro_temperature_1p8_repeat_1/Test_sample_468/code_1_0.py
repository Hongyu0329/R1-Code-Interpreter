# The task is to find the common name for the bee genus "Perdita" and the year it was established.

# 1. Identify the common name.
# Based on entomological resources, the most widely accepted common name for this genus of tiny bees is "Fairy bees".
common_name = "Fairy bees"

# 2. Identify the "coining" year.
# While the term may have been used informally earlier, its usage was solidified
# in the highly influential scientific reference "The Bee Genera of North and Central America"
# by Michener, McGinley, and Danforth. This book was published in 1994.
year = 1994

# 3. Print the final answer in the specified format.
print(f"{common_name}, {year}")