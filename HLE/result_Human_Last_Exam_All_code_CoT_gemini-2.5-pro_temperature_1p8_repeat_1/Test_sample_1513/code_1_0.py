def solve_riddle():
    """
    This function provides the answer to the geographical riddle.
    """
    # Clues:
    # 1. More than 500 km from another inhabited island.
    # 2. Sits on a bay formed by a volcanic caldera.
    # 3. The town and the bay share the same name.
    
    # The answer is Hanga Roa on Easter Island (Rapa Nui).
    island_town = "Hanga Roa"
    
    print(f"The island town is: {island_town}")

solve_riddle()