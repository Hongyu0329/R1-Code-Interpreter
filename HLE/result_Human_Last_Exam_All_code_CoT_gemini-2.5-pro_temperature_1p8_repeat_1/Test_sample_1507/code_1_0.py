# The great icosahedron is a non-convex polyhedron.
# Its convex hull is a regular icosahedron.
# The number of visible triangles on the outer-hull is equal to the number of faces of a regular icosahedron.

# A regular icosahedron has 20 faces, which are all triangles.
number_of_triangles = 20

# Print the result as a descriptive equation.
print(f"Number of triangles = {number_of_triangles}")