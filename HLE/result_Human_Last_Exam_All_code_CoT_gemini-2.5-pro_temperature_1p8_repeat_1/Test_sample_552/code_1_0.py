# molekularkinetischen Theorie der Wärme geforderte Bewegung von in ruhenden
# Flüssigkeiten suspendierten Teilchen") famously contained no citations, his work
# was deeply rooted in the principles of statistical mechanics.
#
# The primary figure whose work and published lectures on statistical mechanics
# and gas theory were a direct influence on Einstein's thinking for this paper
# was Ludwig Boltzmann. Einstein's paper is considered a major validation of
# Boltzmann's statistical interpretation of thermodynamics.

foundational_figure = "Ludwig Boltzmann"
paper_subject = "Brownian motion"
year = 1905

# Output the answer in a full sentence.
print(f"Albert Einstein's {year} paper on {paper_subject} did not formally cite any sources.")
