def find_dinosaur_hydrocarbon():
    """
    This function identifies and prints the name of a polycyclic hydrocarbon
    named after a famous dinosaur.
    """
    # The hydrocarbon is designed to mimic the shape of a T-Rex.
    hydrocarbon_name = "T-rexanene"

    # The creature it's named after.
    creature_name = "Tyrannosaurus rex"

    # The time in millions of years since this creature has been extinct.
    extinction_age_in_millions = 65

    # Print the answer.
    print(f"The polycyclic hydrocarbon is named '{hydrocarbon_name}'.")
    print(f"It is named after the {creature_name}, which has been extinct for over {extinction_age_in_millions} million years.")

find_dinosaur_hydrocarbon()