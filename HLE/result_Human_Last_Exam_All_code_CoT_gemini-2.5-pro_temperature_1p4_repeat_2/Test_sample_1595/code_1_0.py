def find_speaker():
    """
    This function identifies and prints the name of the person who spoke a specific
    quote to G.K. Chesterton in Jerusalem.
    """

    # The person is identified in G.K. Chesterton's 1920 book, "The New Jerusalem".
    speaker = "his wife, Frances Chesterton"

    quote = "Well, anyhow, it must be obvious to anybody that the doctrine of the Fall is the only cheerful view of human life"

    # Print the full answer in a clear sentence.
    print(
        f"The person who told G.K. Chesterton, '{quote}', while they stood on the Mount of Olives was {speaker}."
    )

find_speaker()