def solve_vienna_poet_riddle():
    """
    This function solves the riddle by explaining the multi-layered pun
    connecting a Russian essay, Viennese boulevards, and an English poet.
    """
    print("Solving the riddle step-by-step:")
    
    print("\nStep 1: Identify the source.")
    print("The essay is Joseph Brodsky's 'A Guide to a Renamed City'. In it, he compares St. Petersburg's avenues to other cities' boulevards, including the 'Ring' in Vienna.")

    print("\nStep 2: Identify the key location and feature.")
    print("The 'wide boulevards of Vienna' refers to the Ringstrasse. The key word used by Brodsky is 'Ring'.")

    print("\nStep 3: Uncover the pun.")
    print("Vienna has tram lines running on the Ringstrasse, designated by letters. A famous line is the 'B' line.")

    print("\nStep 4: Combine the clues.")
    print("Combining the tram line 'B' with the boulevard 'Ring' gives the phonetic clue: 'B-Ring'.")

    print("\nStep 5: Connect to the English poet.")
    print("The sound 'B-Ring' is a pun on the surname of the English poet Robert Browning.")
    
    print("\nTherefore, the surname is:")
    surname = "Browning"
    print(surname)

solve_vienna_poet_riddle()
