def solve_trilogy():
    """
    This script deciphers the clues and prints the final trilogy of cities.
    """

    # City 1: From Irving Wallace's "The Miracle", set in Lourdes.
    city_1 = "Lourdes"

    # City 2: From Yuri Krasnokutsky's poem about the Neva River in Saint Petersburg.
    city_2 = "Saint Petersburg"

    # City 3: From the "Belly of Zagreb" analogue, the "Belly of Paris".
    city_3 = "Paris"

    # Print the final trilogy in the specified format.
    print(f"{{{city_1}, {city_2}, {city_3}}}")

solve_trilogy()