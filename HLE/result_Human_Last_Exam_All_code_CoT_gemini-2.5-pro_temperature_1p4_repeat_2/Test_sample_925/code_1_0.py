def get_original_owner_family_name():
    """
    This function provides the family name of the original owner of the lucky ballpoint pen
    as revealed in the Odd Taxi audio drama 13.3.
    """
    # In the audio drama, the pen's history is traced back.
    # It is revealed that the nurse Miho Shirakawa received it from her senior colleague, Natsumi Satou.
    # Natsumi Satou is therefore the original owner in the story's timeline.
    family_name = "Satou"
    print(f"The family name of the original owner of the lucky ballpoint pen is: {family_name}")

get_original_owner_family_name()