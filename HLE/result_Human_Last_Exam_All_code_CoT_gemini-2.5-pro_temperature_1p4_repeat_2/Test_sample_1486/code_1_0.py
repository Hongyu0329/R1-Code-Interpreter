# This script calculates the total number of cabrioles devants performed by
# Maria Khoreva in the first female solo of the Act I pas de trois
# from the 2017 Mariinsky production of Paquita.

# The choreography features a diagonal sequence containing these jumps.
# Let's represent the count with a simple equation.
cabrioles_part1 = 2
cabrioles_part2 = 2

# Calculate the total
total_cabrioles = cabrioles_part1 + cabrioles_part2

# Print the final equation and the result.
print(f"The total number of cabrioles devants is calculated as:")
print(f"{cabrioles_part1} + {cabrioles_part2} = {total_cabrioles}")