def display_force_formula():
    """
    Prints the derived formula for the force F required to lift the rope.
    """
    print("The final equation to calculate the force F is derived from the Work-Energy Theorem.")
    print("\nThe general formula is:")
    print("Work done by F = Change in Kinetic Energy + Change in Potential Energy")
    print("F * l = (1/2 * m * v**2) + (m * g * l / 2)")
    
    print("\nSolving for F, we get the final equation for the mystical force:")
    # The numbers in this equation are the '2's in the denominators.
    print("F = (m * v**2) / (2 * l) + (m * g) / 2")
    
    print("\nWhere:")
    print("F = The force required")
    print("m = mass of the rope")
    print("l = length of the rope")
    print("v = speed of the rope when it just leaves the ground")
    print("g = acceleration due to gravity")

display_force_formula()