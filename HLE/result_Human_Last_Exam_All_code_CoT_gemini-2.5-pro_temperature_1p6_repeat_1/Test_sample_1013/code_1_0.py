# The question asks for the largest possible cardinality of an antichain
# of nonprincipal ultrafilters below a fixed ultrafilter V.
# This is a known result in set theory. The ordering described is the
# Tukey order, and the size of the largest such antichain is the
# cardinality of the continuum, which is 2^{\aleph_0}.

# The final answer can be expressed as an equation for the cardinality C:
# C = 2^{\aleph_0}
# The numbers in this expression are 2 and 0.

# The base of the power
base = 2
# The index of the Aleph number
index = 0

# We print a string representation of this value.
print(f"{base}**aleph_{index}")
