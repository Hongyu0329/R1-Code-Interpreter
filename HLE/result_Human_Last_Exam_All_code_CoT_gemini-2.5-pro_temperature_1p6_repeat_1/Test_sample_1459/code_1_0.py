import math

# The Gromov-Hausdorff distance between the interval [0,1] and the unit circle
# is given by the formula pi / 2.

# Define the components of the equation
numerator = math.pi
denominator = 2

# Calculate the result
distance = numerator / denominator

# Output the equation and the final answer
print(f"The Gromov-Hausdorff distance is given by the equation: pi / 2")
print(f"Let pi = {numerator}")
print(f"Let the denominator be {denominator}")
print(f"Final equation: {numerator} / {denominator} = {distance}")