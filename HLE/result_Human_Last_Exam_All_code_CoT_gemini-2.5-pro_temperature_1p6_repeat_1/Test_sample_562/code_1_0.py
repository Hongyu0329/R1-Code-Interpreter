# The Japanese character in question is 謄.
# This character is composed of a left part and a right part.
# The left part is the radical, which is 月.
# When the radical 月 is on the left side of a character, it is known as a "hen" (へん).
# The Japanese name for this radical form is つきへん (tsukihen).
# This script will print the Hepburn transcription of the radical's name.

radical_name_hepburn = "Tsukihen"
print(radical_name_hepburn)