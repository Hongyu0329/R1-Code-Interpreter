def print_evaluation():
    """
    This function prints Liu Ban's detailed evaluation of Zhang Ji's seven-character poems,
    based on his comments in "Zhongshan Shihua" (《中山诗话》).
    """

    evaluation = """根据北宋刘攽在其著作《中山诗话》中的评价，他对张籍七言诗的确切看法如下：

原文为：“然其七言诗，句虽精苦，然颇伤于气格。前辈云：‘张籍诗骨，韩愈诗肉。’非知言也。籍诗正是肌理细腻，而无有骨格尔。”

这段评价可以解读为：

1.  **“句虽精苦，然颇伤于气格”**：刘攽首先承认张籍七言诗的诗句（句）是经过精心锤炼的，写得非常精致、深刻（精苦）。然而，他认为这些诗在整体的气势、风格和风骨（气格）上有所欠缺和损害。

2.  **“籍诗正是肌理细腻，而无有骨格尔”**：刘攽进一步反驳了前人“张籍的诗是骨头，韩愈的诗是血肉”的观点。他认为，恰恰相反，张籍的诗歌特点是细节描写得非常精细，如同肌肤的纹理（肌理细腻），但却缺乏强有力的内在结构和骨力（无有骨格）。

总结来说，刘攽认为张籍的七言诗虽然字句优美，但在篇章的整体力量感和风骨上是薄弱的，是一种细节上乘但骨架不强的诗风。"""

    print(evaluation)

if __name__ == "__main__":
    print_evaluation()