def find_french_equivalent():
    """
    This function provides the French equivalent for the Germanic name Odobeccus.
    """
    gaulish_name = "Odobeccus"
    french_equivalent = "Eudes"
    
    # The name Odobeccus is Germanic, not Gaulish.
    # The first part 'Odo' directly translates to the French name 'Eudes'.
    # The second part '-beccus' (brook/stream) did not carry over into the personal name.
    
    print(f"The French equivalent of the name 'Odobeccus' is '{french_equivalent}'.")

find_french_equivalent()