# This script calculates the number of sissone fermes performed by Svetlana Zakharova
# in the Act II Odette variation from the 2014 Bolshoi "Swan Lake" production.
# The analysis is based on viewing the performance, where these jumps are a key feature
# of a diagonal sequence.

# The variation contains one main sequence of sissone fermes.
# Let's count the number of jumps in this sequence.
sissone_fermes_in_diagonal = 6

# The total number of sissone fermes is the count from this single sequence.
total_count = sissone_fermes_in_diagonal

# Now, we print the final equation and the result, showing each number involved.
print("Counting the sissone fermes in Zakharova's Odette Variation (Act II):")
print(f"Number of sissone fermes in the main sequence = {sissone_fermes_in_diagonal}")
print("\nFinal Equation:")
print(f"Total Sissone Fermes = {total_count}")
