def get_borges_names():
    """
    Identifies the three figures from Borges's essay "Our Poor Individualism."
    The essay explicitly mentions a Greek (Plutarch) and an Englishman (Swift).
    It refers to the German as "a professor from Trier," a well-understood allusion
    to the scholar Ernst Robert Curtius. This function prints the three names.
    """
    greek_figure = "Plutarch"
    english_figure = "Jonathan Swift"
    german_figure = "Ernst Robert Curtius"
    
    print(f"{greek_figure}, {english_figure}, {german_figure}")

get_borges_names()