# Explanation:
# The genomic mutation rate is understood to be at an equilibrium.
# There is a natural tendency, or "mutational pressure," for the rate to increase,
# because mutations are more likely to degrade DNA repair systems than improve them.
# This upward pressure is counteracted by natural selection. A high mutation rate
# is harmful because it increases the number of deleterious mutations an organism carries (the "mutational load"),
# which reduces overall fitness.
# Therefore, natural selection for optimal fitness acts as a powerful stabilizing force,
# pushing the mutation rate down and maintaining it at a relatively constant, low level.

