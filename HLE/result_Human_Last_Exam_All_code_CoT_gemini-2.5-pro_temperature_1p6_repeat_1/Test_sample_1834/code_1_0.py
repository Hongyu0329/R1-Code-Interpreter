import math

def solve_magnetic_field_magnitude():
    """
    Calculates and explains the magnitude of the magnetic field at a specific point
    due to two perpendicular infinite wires carrying current I.
    The final answer is presented as a symbolic expression.
    """
    
    # Define the coordinates of the point of interest
    px, py, pz = 1, -1, 0
    
    print("This script calculates the magnitude of the magnetic field at the point (x,y,z) = ({}, {}, {}).".format(px, py, pz))
    print("The field is generated by two infinite wires, each carrying a current I.")
    print("  - Wire 1 is on the x-axis with current in the +x direction.")
    print("  - Wire 2 is on the y-axis with current in the +y direction.\n")
    print("We apply the principle of superposition: B_total = B1 + B2.")
    print("The magnetic field magnitude from a long wire is B = (mu_0 * I) / (2 * pi * r).\n")

    # --- Calculation for Wire 1 ---
    print("Step 1: Magnetic Field from Wire 1 (on the x-axis)")
    
    # The perpendicular distance from a point (px, py, pz) to the x-axis is sqrt(py^2 + pz^2)
    r1 = math.sqrt(py**2 + pz**2)
    
    print(f"The perpendicular distance (r1) from point ({px}, {py}, {pz}) to the x-axis is:")
    print(f"r1 = sqrt(({py})^2 + ({pz})^2) = {r1}")
    print("By the right-hand rule, with current in +x, the magnetic field at a point with y<0 points in the +z direction.")
    print(f"The magnetic field vector from Wire 1 is B1 = [ (mu_0 * I) / (2 * pi * {r1}) ] in the +z direction.\n")

    # --- Calculation for Wire 2 ---
    print("Step 2: Magnetic Field from Wire 2 (on the y-axis)")

    # The perpendicular distance from a point (px, py, pz) to the y-axis is sqrt(px^2 + pz^2)
    r2 = math.sqrt(px**2 + pz**2)
    
    print(f"The perpendicular distance (r2) from point ({px}, {py}, {pz}) to the y-axis is:")
    print(f"r2 = sqrt(({px})^2 + ({pz})^2) = {r2}")
    print("By the right-hand rule, with current in +y, the magnetic field at a point with x>0 also points in the +z direction.")
    print(f"The magnetic field vector from Wire 2 is B2 = [ (mu_0 * I) / (2 * pi * {r2}) ] in the +z direction.\n")

    # --- Total Field Calculation ---
    print("Step 3: Total Magnetic Field (Superposition)")
    print("The total magnetic field B_total is the vector sum B1 + B2.")
    print("Since both B1 and B2 point in the same direction (+z), we add their magnitudes directly.")
    print("Equation for the total magnitude:")
    print(f"|B_total| = [ (mu_0 * I) / (2 * pi * {r1}) ] + [ (mu_0 * I) / (2 * pi * {r2}) ]")
    print(f"|B_total| = 2 * (mu_0 * I) / (2 * pi * {r1})")
    print("Simplifying the expression, we get:")
    print("|B_total| = (mu_0 * I) / pi\n")

    # --- Final Result ---
    final_numerator = 1
    final_denominator = 1
    print("The final equation for the magnitude is:")
    print(f"Magnitude = ({final_numerator} * mu_0 * I) / ({final_denominator} * pi)")

solve_magnetic_field_magnitude()