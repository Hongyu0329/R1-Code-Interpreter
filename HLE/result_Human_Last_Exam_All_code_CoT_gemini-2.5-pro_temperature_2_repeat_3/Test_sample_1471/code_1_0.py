# The equation is VI / CCC - CCCIX = ?

# Convert Roman numerals to modern numbers
num1 = 6  # VI
num2 = 300 # CCC
num3 = 309 # CCCIX

# Calculate the result following the order of operations
result = num1 / num2 - num3

# Print the full equation in modern numbers along with the solution
print(f"The equation in modern numbers is:")
print(f"{num1} / {num2} - {num3} = {result}")