def find_radical_name():
    """
    This function identifies and prints the Hepburn transcription of the radical
    for the Japanese character 謄.
    """
    character = "謄"
    radical_kanji = "言"
    radical_name_japanese = "ごんべん"
    radical_name_hepburn = "Gonben"

    print(f"The character is: {character}")
    print(f"The radical is '{radical_kanji}', which is called '{radical_name_japanese}' when it appears on the left side of a character.")
    print(f"The Hepburn transcription for the radical is: {radical_name_hepburn}")

if __name__ == "__main__":
    find_radical_name()
