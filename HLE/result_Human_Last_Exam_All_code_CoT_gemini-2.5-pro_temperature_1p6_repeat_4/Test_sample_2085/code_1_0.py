def find_french_equivalent():
    """
    This function finds and prints the French equivalent of the Gaulish/Germanic name "Odobeccus".
    """
    # The name in question is of Germanic origin, recorded in Latin as "Odobeccus".
    original_name = "Odobeccus"

    # The root "Odo-" evolved into the French name "Eudes".
    french_equivalent = "Eudes"

    print(f"The French equivalent of the name '{original_name}' is '{french_equivalent}'.")

find_french_equivalent()