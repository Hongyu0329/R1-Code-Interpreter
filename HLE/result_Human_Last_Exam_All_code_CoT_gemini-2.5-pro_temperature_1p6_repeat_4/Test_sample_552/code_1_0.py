def find_cited_lecturer():
    """
    This function identifies and prints the name of the scientist whose lectures
    Albert Einstein cited in his 1905 paper on Brownian motion.
    """
    # The paper is "Über die von der molekularkinetischen Theorie der Wärme geforderte Bewegung von in ruhenden Flüssigkeiten suspendierten Teilchen"
    # (On the Movement of Small Particles Suspended in a Stationary Liquid Required by the Molecular-Kinetic Theory of Heat).
    # In this paper, Einstein cites the work of several scientists, but specifically refers to the
    # "Vorlesungen über Gastheorie" (Lectures on Gas Theory) by Ludwig Boltzmann.
    
    scientist_name = "Ludwig Boltzmann"
    
    print(f"In his 1905 paper on Brownian motion, Albert Einstein cited the lectures of {scientist_name}.")

# Execute the function to provide the answer.
find_cited_lecturer()