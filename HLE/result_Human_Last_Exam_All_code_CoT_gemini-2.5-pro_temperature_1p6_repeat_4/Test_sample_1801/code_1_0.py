# This script identifies and prints the names of the two Nobel laureates
# who were featured in an Enron "Ask Why" commercial in 2000.

def find_laureates():
    """
    Prints the names of the two Nobel laureates from the Enron commercial.
    """
    laureate_1 = "Paul Krugman"
    laureate_2 = "Kenneth Arrow"
    
    print("The two Nobel laureates featured in the infamous 2000 Enron commercial were:")
    print(f"1. {laureate_1}")
    print(f"2. {laureate_2}")

if __name__ == "__main__":
    find_laureates()