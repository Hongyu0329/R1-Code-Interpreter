def solve_bulgakov_puzzle():
    """
    This function identifies and prints the solution to the literary puzzle
    from Bulgakov's "The Master and Margarita".
    """
    # The character in Moscow tormented by a house sparrow (in Chapter 14) is Rimsky.
    moscow_character = "Rimsky"

    # The corresponding bird in the Jerusalem chapters is the barn swallow.
    jerusalem_bird = "barn swallow"

    # The character tormented by the barn swallow is Pontius Pilate.
    jerusalem_character = "Pontius Pilate"

    # Print the answer in the required "character; bird; character" format.
    print(f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}")

solve_bulgakov_puzzle()