# Step 1: Define the given financial values from the problem description.
revenue = 50  # Revenue from selling the product in dollars
water_cost = 10  # Cost of water in dollars
energy_cost = 15  # Cost of energy in dollars

# Step 2: Calculate the total environmental cost.
total_environmental_cost = water_cost + energy_cost

# Step 3: Calculate the Sustainable Value Added (SVA).
# SVA = Revenue - Total Environmental Costs
sva = revenue - total_environmental_cost

# Step 4: Print the final equation with each number.
print("Sustainable Value Added (SVA) = Revenue - (Water Cost + Energy Cost)")
print(f"SVA = {revenue} - ({water_cost} + {energy_cost})")
print(f"SVA = {sva}")