def find_philosopher():
    """
    This function identifies and prints the name of the Lviv-Warsaw School
    philosopher who decomposed the notion of a sign or hallmark.
    """
    philosopher = "Kazimierz Ajdukiewicz"
    general_concept = "hallmark (or more generally, a sign)"
    component_1 = "symptoms"
    component_2 = "signals"

    print(f"The philosopher from the Lviv-Warsaw School who decomposed the notion of a '{general_concept}' is {philosopher}.")
    print(f"He distinguished between:")
    print(f"1. {component_1}: Natural signs that are causally connected to what they signify (e.g., smoke is a symptom of fire).")
    print(f"2. {component_2}: Conventional signs established by rules or agreement (e.g., a traffic light is a signal to stop).")

find_philosopher()