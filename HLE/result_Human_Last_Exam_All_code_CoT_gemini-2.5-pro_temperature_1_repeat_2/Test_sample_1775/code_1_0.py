def solve_displacement_field():
    """
    This function calculates and prints the formula for the effective displacement
    field in a dual-gate FET.
    """
    # Symbolic representation of the given variables
    C_tg = "C_{tg}"
    C_bg = "C_{bg}"
    V_tg = "V_{tg}"
    V_bg = "V_{bg}"

    # The displacement field D is the sum of the effects from the top and bottom gates.
    # D = C_tg * V_tg + C_bg * V_bg
    # The code below will print this equation.

    print("The effective displacement field (D) through the transistor is determined by the combined influence of the top and back gates.")
    print("It is calculated as the sum of the displacement fields generated by each gate capacitor.")
    print("\nThe final equation is:")
    print(f"D = ({C_tg}) * ({V_tg}) + ({C_bg}) * ({V_bg})")
    print("\nWhere:")
    print(f"  D: The effective displacement field (in Coulombs per square meter, C/m^2)")
    print(f"  {C_tg}: Top gate capacitance per unit area (in Farads per square meter, F/m^2)")
    print(f"  {V_tg}: Voltage applied to the top gate (in Volts, V)")
    print(f"  {C_bg}: Back gate capacitance per unit area (in Farads per square meter, F/m^2)")
    print(f"  {V_bg}: Voltage applied to the back gate (in Volts, V)")

# Execute the function to get the solution
solve_displacement_field()