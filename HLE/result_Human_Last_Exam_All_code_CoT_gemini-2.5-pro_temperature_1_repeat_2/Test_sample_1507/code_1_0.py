# A great icosahedron is a complex, self-intersecting polyhedron.
# The "outer-hull" or "convex hull" of a great icosahedron is a regular icosahedron.
# The "visible triangles from the outside" are the faces of this regular icosahedron.

# A regular icosahedron is a Platonic solid with 20 faces.
# Each face is an equilateral triangle.

# Therefore, the number of visible triangles is 20.
num_visible_triangles = 20

# The final equation is simply stating this number.
print("The number of visible triangles on the outer-hull of the great icosahedron = " + str(num_visible_triangles))