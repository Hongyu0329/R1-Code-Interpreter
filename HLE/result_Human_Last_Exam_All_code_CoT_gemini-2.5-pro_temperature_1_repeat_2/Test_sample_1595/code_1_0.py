def solve_chesterton_riddle():
    """
    This function identifies and prints the name of the person who spoke a specific quote to G.K. Chesterton.
    """
    # The person who spoke the quote, as documented in Chesterton's book "The New Jerusalem".
    speaker_name = "Hilaire Belloc"

    # The quote in question.
    quote = "Well, anyhow, it must be obvious to anybody that the doctrine of the Fall is the only cheerful view of human life"

    # Print the answer in a full sentence.
    print(f"It was {speaker_name} who told G.K. Chesterton, '{quote}', while they were on the Mount of Olives.")

# Execute the function to provide the answer.
solve_chesterton_riddle()