def solve_geographical_riddle():
    """
    This function solves a geographical riddle based on a set of clues.
    """

    # --- Clues from the riddle ---
    # 1. An island town.
    # 2. More than 500 kilometers from another inhabited island.
    # 3. Sits on a bay formed by a volcanic caldera.
    # 4. The town, bay, and caldera share the same name.

    # --- Analysis ---
    # The town of Taioha'e, on Nuku Hiva in French Polynesia, fits the clues best.
    # - Town: Taioha'e
    # - Bay: Taioha'e Bay
    # - Bay Type: Flooded volcanic caldera
    # This matches the geographical and naming clues perfectly.
    # The isolation clue is ambiguous. While the Marquesas archipelago is extremely
    # remote, Nuku Hiva is near other inhabited islands within its group.
    # However, no other location on Earth matches the other specific clues.

    the_answer = "Taioha'e"

    print(f"The island town that sits on a bay formed by a volcanic caldera sharing its name is: {the_answer}")

solve_geographical_riddle()