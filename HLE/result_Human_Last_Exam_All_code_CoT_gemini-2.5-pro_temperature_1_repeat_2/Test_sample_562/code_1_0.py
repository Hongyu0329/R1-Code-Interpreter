def find_radical_name():
    """
    This function identifies and prints the radical (bushu) name for the Kanji '謄'.
    """
    kanji_character = '謄'
    # The radical of 謄 is 言. When it is on the left side of a character (a position known as 'hen'),
    # its common name is ごんべん (gonben).
    radical_name_hepburn = "Gonben"

    print(f"The bushu / radical in the Japanese character '{kanji_character}' is '{radical_name_hepburn}'.")

if __name__ == "__main__":
    find_radical_name()