def find_dinosaur_molecule():
    """
    This function provides the name of the polycyclic hydrocarbon
    named after a creature extinct for over 65 million years.
    """
    # The number of years mentioned in the prompt
    years_extinct = 65

    # The formal name of the polycyclic hydrocarbon
    formal_name = "Dibenzo[fg,op]naphthacene"

    # The creature it is named after
    creature_nickname = "dinosauroid"

    # Print the answer, including the number from the prompt.
    print(f"The polycyclic hydrocarbon is named {formal_name}.")
    print(f"It is nicknamed '{creature_nickname}' because its shape resembles a dinosaur.")
    print(f"Dinosaurs went extinct over {years_extinct} million years ago.")

find_dinosaur_molecule()