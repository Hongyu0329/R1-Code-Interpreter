def find_prophet_verses():
    """
    Identifies and prints the range of verses (bayt) in 'Aqeedat al-'Awaam'
    that mention the names of the Prophets.
    """
    start_bayt = 39
    end_bayt = 42

    print("In the poem 'Aqeedat al-'Awaam' by Sayyid Ahmad al-Marzuqi, the names of the Prophets are mentioned from bayt:")
    print(f"Start: {start_bayt}")
    print(f"End:   {end_bayt}")
    print(f"\nTherefore, the names are listed from bayt {start_bayt} to bayt {end_bayt}.")

find_prophet_verses()