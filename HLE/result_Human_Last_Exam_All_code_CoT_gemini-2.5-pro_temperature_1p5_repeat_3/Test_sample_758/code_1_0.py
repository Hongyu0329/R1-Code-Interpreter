def find_common_elements():
    """
    This function determines and prints the common elements between "Key West" and "The Yellow Wallpaper".
    """
    # Based on the literary analysis, all six elements are common to both stories.
    common_elements = ["I", "II", "III", "IV", "V", "VI"]
    
    # Format the output as a comma-separated string of Roman numerals.
    answer = ", ".join(common_elements)
    
    print(answer)

find_common_elements()