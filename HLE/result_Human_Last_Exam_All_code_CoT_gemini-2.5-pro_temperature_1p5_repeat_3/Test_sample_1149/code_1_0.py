# The number of depressions in the lacrimal recess of Irritator challengeri is a known anatomical detail.
# According to paleontological studies of the skull, this recess contains two large depressions.
# This script will display this fact using a simple equation.

# Define two numbers that will add up to the correct total.
first_number = 1
second_number = 1

# Calculate the total number of depressions.
total_depressions = first_number + second_number

# Print the information and the resulting equation.
print("The number of depressions in the lacrimal recess of Irritator challengeri can be represented by the following equation:")
print(f"{first_number} + {second_number} = {total_depressions}")