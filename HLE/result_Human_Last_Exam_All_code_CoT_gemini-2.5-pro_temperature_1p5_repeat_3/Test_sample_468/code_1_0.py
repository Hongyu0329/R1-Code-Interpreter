def get_bee_common_name_info():
    """
    This function provides information about the common name of the bee genus Perdita.
    """
    # The common name for the genus "Perdita" is "Fairy bees".
    name = "Fairy bees"

    # The name was popularized in the landmark 1996 book "The Forgotten Pollinators"
    # by Stephen L. Buchmann and Gary Paul Nabhan, establishing a firm date for its usage.
    year = 1996

    # The final output needs to be in the format "<name>, <year>"
    print(f"{name}, {year}")

if __name__ == "__main__":
    get_bee_common_name_info()