def poetry_and_art_info():
  """
  This function prints information about the poet and artwork
  associated with the provided verses.
  """
  poet = "Rafael Alberti"
  artwork = "Pablo Picasso's mural, 'Guernica'"
  details = "The poem is from Alberti's collection 'A la pintura' (To Painting)."

  print(f"The Spanish poet who wrote these verses is: {poet}")
