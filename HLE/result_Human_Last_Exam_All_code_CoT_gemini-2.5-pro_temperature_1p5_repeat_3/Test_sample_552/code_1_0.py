def find_cited_lectures():
    """
    Prints the name of the scientist whose lectures influenced Einstein's 1905 Brownian motion paper.
    """
    scientist = "Ludwig Boltzmann"
    work_title = "Lectures on Gas Theory"
    
    # Einstein's 1905 paper on Brownian motion was built upon the foundations of
    # statistical mechanics and the kinetic theory of heat. While the paper itself has very few citations,
    # it is well-documented by historians of science that Einstein had studied and was deeply influenced
    # by the work of Ludwig Boltzmann, particularly his "Lectures on Gas Theory".
    
    print(f"In his work on Brownian motion, Albert Einstein's thinking was significantly influenced by the lectures (published as a book) of {scientist}, specifically his \"{work_title}\".")

