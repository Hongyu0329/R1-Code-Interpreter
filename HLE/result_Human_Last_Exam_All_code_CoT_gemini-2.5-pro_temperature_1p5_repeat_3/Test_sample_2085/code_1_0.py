def find_french_equivalent():
    """
    This function provides the French equivalent for the historical name "Odobeccus".
    """
    # The original Latinized Germanic name
    historical_name = "Odobeccus"
    
    # The modern French equivalent
    french_equivalent = "Eudes"
    
    print(f"The historical name was: {historical_name}")
    print(f"The modern French equivalent is: {french_equivalent}")
    
    # Displaying the relationship as an "equation" as requested
    print("\nTherefore, the equivalence can be shown as:")
    print(f"{historical_name} = {french_equivalent}")

if __name__ == "__main__":
    find_french_equivalent()