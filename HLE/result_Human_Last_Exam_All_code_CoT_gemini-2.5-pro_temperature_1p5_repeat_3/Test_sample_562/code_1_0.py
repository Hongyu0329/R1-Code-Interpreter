# The Japanese character is 謄.
# This character can be broken down into two main components: 言 (gon) on the left and 朕 (chin) on the right.
# The radical, used for dictionary classification, is the left part: 言.
# The name for the 言 radical when it's on the left side of a character is "gonben" (ごんべん).
# The Hepburn transcription for "gonben" is "Gonben".
print("Gonben")