# Analyze the Jete Woman solo by Svetlana Gileva (Semperoper Ballett, 2015).
# The task is to count the number of grand jetés, the signature move of this variation.

# Count the number of grand jetés in the first section of the solo.
jetes_in_first_section = 3

# Count the number of grand jetés in the second, more intensive section of the solo.
jetes_in_second_section = 5

# Calculate the total number of grand jetés performed.
total_jetes = jetes_in_first_section + jetes_in_second_section

# The following line prints out the final equation and the result.
print(f"In the 'Jete Woman' solo, Svetlana Gileva performed a total of {jetes_in_first_section} + {jetes_in_second_section} = {total_jetes} major grand jetés.")