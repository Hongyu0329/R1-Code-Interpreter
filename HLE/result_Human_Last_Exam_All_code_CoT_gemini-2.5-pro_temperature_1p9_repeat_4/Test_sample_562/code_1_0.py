def find_radical_name():
    """
    This function identifies the radical for the kanji 謄
    and provides its name in Hepburn transcription.
    """
    kanji = '謄'
    # The radical for 謄 is 言, which is called "gonben" when on the left.
    radical_name_hepburn = "Gonben"
    
    print(f"The bushu / radical for the character '{kanji}' is called: {radical_name_hepburn}")

find_radical_name()