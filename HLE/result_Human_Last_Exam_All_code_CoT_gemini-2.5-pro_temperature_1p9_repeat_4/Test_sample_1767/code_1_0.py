# Define the Chern numbers for the two insulators
c1 = 1
c2 = 1

# The Chern number is an additive quantity.
# The total Chern number of the junction is the sum of the individual Chern numbers.
c_total = c1 + c2

# Print the final equation showing all the numbers involved
print(f"The Chern number of the junction is the sum of the individual Chern numbers:")
print(f"C_total = C1 + C2")
print(f"C_total = {c1} + {c2} = {c_total}")