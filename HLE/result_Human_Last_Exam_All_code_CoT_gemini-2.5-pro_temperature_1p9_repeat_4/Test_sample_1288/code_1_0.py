def solve_bulgakov_parallel():
    """
    This function identifies and prints the solution to the literary parallel
    between the Moscow and Jerusalem sections of Bulgakov's 'The Master and Margarita'.

    The parallel involves a character being tormented by a small bird.

    - In Chapter 18, "Unlucky Visitors," the buffet manager Andrei Fokich Sokov,
      after his terrifying encounter with Woland's gang, is tormented by a persistent house sparrow.
    - In Chapter 2, "Pontius Pilate," the Procurator is suffering from a terrible migraine
      and is repeatedly distracted and annoyed by a barn swallow flying in and out of the colonnade.
    """
    
    # Identify the character in the Moscow chapters tormented by a sparrow in Ch. 18
    moscow_character = "Sokov"
    
    # Identify the corresponding bird in the Jerusalem chapters
    jerusalem_bird = "barn swallow"
    
    # Identify the character in Jerusalem tormented by the bird
    jerusalem_character = "Pontius Pilate"
    
    # Format the output as requested: "character; bird; character"
    final_answer = f"{moscow_character}; {jerusalem_bird}; {jerusalem_character}"
    
    print(final_answer)

solve_bulgakov_parallel()