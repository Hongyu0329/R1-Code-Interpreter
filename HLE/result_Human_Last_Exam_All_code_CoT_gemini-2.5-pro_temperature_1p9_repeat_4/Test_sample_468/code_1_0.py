def find_bee_common_name_info():
    """
    This function provides the currently used common name for the genus Perdita
    and the year it was popularized.
    """
    # The common name for the genus Perdita is "Fairy bees".
    common_name = "Fairy bees"

    # This name was notably popularized by E.O. Wilson in his book
    # "The Diversity of Life", published in 1992.
    year_coined = 1992

    # Print the result in the specified format: "<name>, <year>"
    print(f"{common_name}, {year_coined}")

if __name__ == "__main__":
    find_bee_common_name_info()