# In Maria Khoreva's first solo in the pas de trois of the 2017 Paquita,
# she performs one main diagonal sequence of jumps. This sequence
# consists of cabrioles devants.

cabrioles_in_diagonal_sequence = 6

# The total is the sum of all cabrioles performed in the variation.
# Since there is only one sequence, the total is the number from that sequence.
total_cabrioles_devants = cabrioles_in_diagonal_sequence

print(f"Number of cabrioles devants in the main sequence: {cabrioles_in_diagonal_sequence}")
print(f"Total = {total_cabrioles_devants}")
