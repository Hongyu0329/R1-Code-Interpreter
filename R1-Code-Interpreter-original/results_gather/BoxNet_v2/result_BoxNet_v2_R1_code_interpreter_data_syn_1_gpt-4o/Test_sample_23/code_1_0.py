# Initial and goal states
initial_state = {
    "box1": "C1,2",
    "box2": "C2,4",
    "box3": "C2,3",
    "box4": "C1,4"
}

goal_state = {
    "box1": "C3,1",
    "box2": "C2,3",
    "box3": "C3,2",
    "box4": "C1,5"
}

# Adjacency list
adjacency = {
    "C1,1": ["C1,2", "C2,1"],
    "C1,2": ["C1,1", "C1,3", "C2,2"],
    "C1,3": ["C1,2", "C1,4", "C2,3"],
    "C1,4": ["C1,3", "C1,5", "C2,4"],
    "C1,5": ["C1,4", "C1,6", "C2,5"],
    "C1,6": ["C1,5", "C2,6"],
    "C2,1": ["C2,2", "C1,1", "C3,1"],
    "C2,2": ["C2,1", "C2,3", "C1,2", "C3,2"],
    "C2,3": ["C2,2", "C2,4", "C1,3", "C3,3"],
    "C2,4": ["C2,3", "C2,5", "C1,4", "C3,4"],
    "C2,5": ["C2,4", "C2,6", "C1,5", "C3,5"],
    "C2,6": ["C2,5", "C1,6", "C3,6"],
    "C3,1": ["C3,2", "C2,1"],
    "C3,2": ["C3,1", "C3,3", "C2,2"],
    "C3,3": ["C3,2", "C3,4", "C2,3"],
    "C3,4": ["C3,3", "C3,5", "C2,4"],
    "C3,5": ["C3,4", "C3,6", "C2,5"],
    "C3,6": ["C3,5", "C2,6"]
}

# Plan generation
plan = [initial_state.copy()]

# Function to find the next move for a box
def find_next_move(current, goal, occupied):
    if current == goal:
        return current
    for neighbor in adjacency[current]:
        if neighbor == goal:
            return neighbor
    for neighbor in adjacency[current]:
        if neighbor not in occupied:
            return neighbor
    return current

# Move boxes towards their goals
while plan[-1] != goal_state:
    current_state = plan[-1].copy()
    occupied_cells = set(current_state.values())
    moved = False
    for box, current_location in current_state.items():
        if current_location != goal_state[box]:
            next_location = find_next_move(current_location, goal_state[box], occupied_cells)
            if next_location != current_location:
                current_state[box] = next_location
                moved = True
                break
    if not moved:
        break
    plan.append(current_state)

print(plan)