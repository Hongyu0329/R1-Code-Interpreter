# List of penguin names
penguin_names = ["Louis", "Bernard", "Vincent", "Gwen", "James"]

# List of giraffe names
giraffe_names = ["Jody", "Gladys", "Marian", "Donna"]

# Combine both lists
all_names = penguin_names + giraffe_names

# Sort the combined list alphabetically
all_names_sorted = sorted(all_names)

# Get the last name in the sorted list
last_name = all_names_sorted[-1]

# Print the last name
print(last_name)