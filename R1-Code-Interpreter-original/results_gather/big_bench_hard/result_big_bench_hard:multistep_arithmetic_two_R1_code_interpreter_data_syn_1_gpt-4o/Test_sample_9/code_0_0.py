# Step 1: Evaluate the multiplications
multiplication_1 = 1 * 5 * -5
multiplication_2 = -1 * -7

# Step 2: Simplify the expressions inside the parentheses
parentheses_1 = -9 - multiplication_1
parentheses_2 = 6 + -3 - multiplication_2

# Step 3: Subtract the results of the two parentheses expressions
result = parentheses_1 - parentheses_2

print(result)