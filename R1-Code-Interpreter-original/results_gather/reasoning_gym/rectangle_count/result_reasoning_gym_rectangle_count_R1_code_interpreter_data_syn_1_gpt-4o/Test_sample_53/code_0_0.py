# Function to count rectangles
def count_rectangles(grid):
    rectangles = 0
    in_rectangle = False

    for row in grid:
        if '#' in row:
            if not in_rectangle:
                rectangles += 1
                in_rectangle = True
        else:
            in_rectangle = False

    return rectangles

