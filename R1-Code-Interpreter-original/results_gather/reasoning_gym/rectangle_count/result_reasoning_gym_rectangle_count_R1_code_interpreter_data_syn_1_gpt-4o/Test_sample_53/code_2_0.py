# Function to count rectangles
def count_rectangles(grid):
    rectangles = 0
    in_rectangle = False

    for i, row in enumerate(grid):
        if '#' in row:
            if not in_rectangle:
                # Check if this is the start of a new rectangle
                if row.strip().startswith('#') and row.strip().endswith('#'):
                    rectangles += 1
                    in_rectangle = True
        else:
            in_rectangle = False

    return rectangles

# Count the rectangles
rectangles_count = count_rectangles(grid)
