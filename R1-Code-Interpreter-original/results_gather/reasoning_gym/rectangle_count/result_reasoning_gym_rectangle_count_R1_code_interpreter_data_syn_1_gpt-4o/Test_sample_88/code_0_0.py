def count_rectangles(grid):
    rows = len(grid)
    cols = len(grid[0]) if rows > 0 else 0
    rectangles = set()

    for r in range(rows):
        for c in range(cols):
            if grid[r][c] in ['#', '█']:
                # Find the bottom-right corner of the rectangle
                br_r, br_c = r, c
                while br_r + 1 < rows and grid[br_r + 1][c] in ['#', '█']:
                    br_r += 1
                while br_c + 1 < cols and grid[r][br_c + 1] in ['#', '█']:
                    br_c += 1
                
                # Check if this is a valid rectangle
                if all(grid[br_r][cc] in ['#', '█'] for cc in range(c, br_c + 1)) and \
                   all(grid[rr][br_c] in ['#', '█'] for rr in range(r, br_r + 1)):
                    rectangles.add((r, c, br_r, br_c))
    
    return len(rectangles)

