# Given angles
angles = [95.0, 69.0, 52.0, 108.0, 157.0]

# Total sum of interior angles for a hexagon
total_sum = 720.0

# Sum of the given angles
sum_of_given_angles = sum(angles)

# Calculate the remaining angle
remaining_angle = total_sum - sum_of_given_angles

# Output the remaining angle
print(remaining_angle)