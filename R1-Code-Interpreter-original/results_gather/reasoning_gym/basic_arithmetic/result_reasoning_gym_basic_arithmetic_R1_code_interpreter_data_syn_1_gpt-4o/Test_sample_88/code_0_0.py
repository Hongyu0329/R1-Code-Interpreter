# Step 1: Evaluate the innermost parentheses
inner1 = -45 + -35
inner2 = -44 + 29 + -46

# Step 2: Negate the results
neg_inner1 = -inner1
neg_inner2 = -inner2

# Step 3: Multiply the negated results
product = neg_inner1 * neg_inner2

# Step 4: Subtract the result of the multiplication from -75
result = product - -75

print(result)