def analyze_vital_point(move):
    board = [
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', 'X', '.', '.', '.'],  # 13
        ['X', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 12
        ['.', 'O', 'X', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 11
        ['.', '.', '.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.'],  # 10
        ['.', '.', '.', '.', '.', 'O', 'X', 'X', '.', '.', 'X', '.', '.'],  # 9
        ['.', '.', '.', '.', '.', 'X', 'O', 'O', 'X', 'O', '.', '.', '.'],  # 8
        ['.', '.', '.', '.', '.', 'X', 'O', '.', '.', '.', '.', '.', '.'],  # 7
        ['.', '.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.', '.'],  # 6
        ['O', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 5
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 4
        ['.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.', '.', '.'],  # 3
        ['.', '.', '.', 'O', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 2
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.']   # 1
    ]
    
    col = ord(move[0]) - ord('A')
    row = 13 - int(move[1:])
    
    board[row][col] = '*'
    
    print("   A B C D E F G H I J K L M")
    for i in range(13):
        print(f"{13-i:2d} {' '.join(board[i])}")
    
    print(f"\nAnalyzing move at {move}:")
    if move == 'I7':
        print("This move is the vital point:")
        print("1. It cuts off the white stones from potential escape")
        print("2. Creates a shortage of liberties for the white group")
        print("3. White cannot save the stones at F8, G8, H8, and G7")

analyze_vital_point('I7')