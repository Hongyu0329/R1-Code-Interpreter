def analyze_position(move):
    board = [
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', 'X', '.', '.', '.'],  # 13
        ['X', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 12
        ['.', 'O', 'X', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 11
        ['.', '.', '.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.'],  # 10
        ['.', '.', '.', '.', '.', 'O', 'X', 'X', '.', '.', 'X', '.', '.'],  # 9
        ['.', '.', '.', '.', '.', 'X', 'O', 'O', 'X', 'O', '.', '.', '.'],  # 8
        ['.', '.', '.', '.', '.', 'X', 'O', '.', '.', '.', '.', '.', '.'],  # 7
        ['.', '.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.', '.'],  # 6
        ['O', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 5
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 4
        ['.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.', '.', '.'],  # 3
        ['.', '.', '.', 'O', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 2
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.']   # 1
    ]
    
    col = ord(move[0]) - ord('A')
    row = 13 - int(move[1:])
    
    board[row][col] = '*'
    
    print("   A B C D E F G H I J K L M")
    for i in range(13):
        print(f"{13-i:2d} {' '.join(board[i])}")
    
    print(f"\nAnalyzing move at {move}:")
    if move == 'G9':
        print("This move creates a snapback situation:")
        print("1. If White captures at H8, Black can recapture the entire group")
        print("2. If White doesn't capture, Black threatens to capture White's stones")

analyze_position('G9')