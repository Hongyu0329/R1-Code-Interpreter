def print_board_with_highlight(move):
    board = [
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', 'X', '.', '.', '.'],  # 13
        ['X', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 12
        ['.', 'O', 'X', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 11
        ['.', '.', '.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.'],  # 10
        ['.', '.', '.', '.', '.', 'O', 'X', 'X', '.', '.', 'X', '.', '.'],  # 9
        ['.', '.', '.', '.', '.', 'X', 'O', 'O', 'X', 'O', '.', '.', '.'],  # 8
        ['.', '.', '.', '.', '.', 'X', 'O', '.', '.', '.', '.', '.', '.'],  # 7
        ['.', '.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.', '.'],  # 6
        ['O', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 5
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 4
        ['.', '.', '.', '.', '.', 'X', '.', '.', '.', '.', '.', '.', '.'],  # 3
        ['.', '.', '.', 'O', '.', '.', '.', '.', '.', '.', '.', '.', '.'],  # 2
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.']   # 1
    ]
    
    # Convert move to coordinates (e.g., 'H7' to (7,6))
    col = ord(move[0]) - ord('A')
    row = 13 - int(move[1:])
    
    # Mark the suggested move with '*'
    board[row][col] = '*'
    
    # Print the board
    print("   A B C D E F G H I J K L M")
    for i in range(13):
        print(f"{13-i:2d} {' '.join(board[i])}")
    
    # Analyze the position
    print(f"\nSuggested move: {move}")
    print("This move threatens to capture the white stones at F8-H8")

print_board_with_highlight('H7')