# Check the liberties of the group at (8, 5), (8, 6), (8, 7), (7, 6)
def check_liberties(board, group_positions):
    liberties = set()
    for x, y in group_positions:
        # Check all four directions
        for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
            nx, ny = x + dx, y + dy
            if 0 <= nx < len(board) and 0 <= ny < len(board[0]) and board[nx][ny] == '.':
                liberties.add((nx, ny))
    return liberties

# Group positions
