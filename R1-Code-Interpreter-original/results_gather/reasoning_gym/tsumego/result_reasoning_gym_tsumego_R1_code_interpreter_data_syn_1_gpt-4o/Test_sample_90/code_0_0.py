# Function to check if a move captures a group
def captures_group(move, group):
    for stone in group:
        x, y = stone
        if board[x][y] != 'O':
            return False
    return True

# Check each potential move
for move, group in potential_moves.items():
    if captures_group(move, group):
        print(move)
        break