# Define the real and imaginary parts of the complex numbers
real1, imag1 = 3.0, 7.0
real2, imag2 = 4.0, 8.0

# Calculate the sum of the real parts and the imaginary parts
real_sum = real1 + real2
imag_sum = imag1 + imag2

# Print the result in the form of a complex number
print(f"({real_sum} + {imag_sum}i)")