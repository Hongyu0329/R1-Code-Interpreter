# Define the real and imaginary parts of the complex numbers
real_part1 = 6.0
imaginary_part1 = 5.0

real_part2 = 8.0
imaginary_part2 = -5.0

# Calculate the sum of the real parts and the imaginary parts
real_sum = real_part1 + real_part2
imaginary_sum = imaginary_part1 + imaginary_part2

# Print the result as a complex number
print(f"{real_sum} + {imaginary_sum}i")