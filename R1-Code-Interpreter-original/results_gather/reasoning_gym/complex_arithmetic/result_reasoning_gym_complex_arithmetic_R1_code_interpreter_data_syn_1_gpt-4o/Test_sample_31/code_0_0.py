# Define the complex numbers
z1_real = -1.0
z1_imag = -2.0
z2_real = 5.0
z2_imag = -8.0

# Subtract the real parts
real_part = z1_real - z2_real

# Subtract the imaginary parts
imaginary_part = z1_imag - z2_imag

# Form the resulting complex number
result = complex(real_part, imaginary_part)

# Print the result
print(result)