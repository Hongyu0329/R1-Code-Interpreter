# Define the real and imaginary parts of the complex numbers
real_part_1 = -7.0
imaginary_part_1 = -5.0
real_part_2 = -8.0
imaginary_part_2 = 9.0

# Perform the subtraction
real_result = real_part_1 - real_part_2
imaginary_result = imaginary_part_1 - imaginary_part_2

# Print the result in the form of a complex number
print(f"{real_result} + {imaginary_result}i")