# Define the complex numbers
z1_real = -4.0
z1_imag = 4.0
z2_real = 6.0
z2_imag = 5.0

# Subtract the real parts and the imaginary parts
result_real = z1_real - z2_real
result_imag = z1_imag - z2_imag

# Format the result as a complex number
result = complex(result_real, result_imag)

# Print the result
print(result)