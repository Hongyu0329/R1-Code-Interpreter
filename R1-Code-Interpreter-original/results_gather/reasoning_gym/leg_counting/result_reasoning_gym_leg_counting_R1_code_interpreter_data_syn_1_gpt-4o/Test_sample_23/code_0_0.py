# Define the number of legs for each type of animal
legs_per_horse = 4
legs_per_dog = 4
legs_per_starfish = 0
legs_per_chicken = 2

# Define the number of each type of animal
num_horses = 12
num_dogs = 3
num_starfish = 5
num_chickens = 2

# Calculate the total number of legs
