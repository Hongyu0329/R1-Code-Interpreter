# Define the number of legs for each type of animal
legs_per_horse = 4
legs_per_elephant = 4
legs_per_wasp = 6

# Define the number of each type of animal
num_horses = 6
num_elephants = 1
num_wasps = 12

# Calculate the total number of legs
total_legs = (num_horses * legs_per_horse) + (num_elephants * legs_per_elephant) + (num_wasps * legs_per_wasp)

# Output the total number of legs
print(total_legs)