# Number of animals
cows = 14
cats = 13
wasps = 12
praying_mantises = 8

# Legs per animal
legs_per_cow = 4
legs_per_cat = 4
legs_per_wasp = 6
legs_per_praying_mantis = 6

# Total legs calculation
