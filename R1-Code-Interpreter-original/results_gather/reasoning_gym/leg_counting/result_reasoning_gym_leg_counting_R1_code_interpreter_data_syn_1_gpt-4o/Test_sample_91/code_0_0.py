# Number of animals
num_deers = 5
num_chickens = 2
num_grasshoppers = 14

# Legs per animal
legs_per_deer = 4
legs_per_chicken = 2
legs_per_grasshopper = 6

# Calculate total legs
total_legs = (num_deers * legs_per_deer) + (num_chickens * legs_per_chicken) + (num_grasshoppers * legs_per_grasshopper)

# Output the result
print(total_legs)