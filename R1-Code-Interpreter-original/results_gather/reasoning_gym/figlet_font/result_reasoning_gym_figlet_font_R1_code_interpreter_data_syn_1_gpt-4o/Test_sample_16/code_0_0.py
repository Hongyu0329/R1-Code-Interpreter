# Function to extract each letter from the pattern
def extract_letters(pattern):
    letters = []
    for i in range(0, len(pattern[0]), 8):  # 7 characters for letter + 1 space
        letter = [line[i:i+7] for line in pattern]
        letters.append(letter)
    return letters

# Function to match extracted letter to known ASCII letters
def match_letter(letter):
    for char, ascii_art in ascii_letters.items():
        if letter == ascii_art:
            return char
    return "?"

# Extract letters from the pattern
extracted_letters = extract_letters(pattern)

# Decode the word
decoded_word = ''.join(match_letter(letter) for letter in extracted_letters)

print(decoded_word)