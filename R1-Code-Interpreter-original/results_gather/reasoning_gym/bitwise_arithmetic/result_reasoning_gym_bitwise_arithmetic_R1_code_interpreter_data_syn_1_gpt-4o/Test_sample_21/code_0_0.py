# Initial values
value1 = 0xf0de
shift_amount = 0x2
value2 = 0xdcb2
value3 = 0xeed8

# Perform left shift
shifted_value = value1 << shift_amount

# Perform addition
added_value = value2 + value3

# Perform subtraction
result = shifted_value - added_value

# Output the result in hexadecimal format
print(hex(result))