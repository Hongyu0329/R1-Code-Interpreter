# Convert hexadecimal to decimal and perform operations
multiplication_result = 0xbf4f * 0x6e6f
subtraction_result = 0xe39c - 0xa7c5

# Add the results
final_result = multiplication_result + subtraction_result

# Convert the final result to hexadecimal
hex_result = hex(final_result)

# Print the final result
print(hex_result)