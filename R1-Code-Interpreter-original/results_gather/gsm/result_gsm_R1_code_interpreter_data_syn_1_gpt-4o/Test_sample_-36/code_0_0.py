# Define the calories burned per activity
calories_aerobics = 2 * 500  # 2 hours of aerobics
calories_running = 1 * 600   # 1 hour of running

# Total calories burned
total_calories = calories_aerobics + calories_running

# Calculate the water needed
water_needed = (total_calories / 200) * 100

# Output the result
print(water_needed)