# Calculate the measure of angle LOQ in a regular hexagon
# Each interior angle of a regular hexagon is 120 degrees
# The angle LOQ is the exterior angle of one of the equilateral triangles formed by the diagonals

# Calculate the measure of angle LOQ
angle_LOQ = 360 / 6  # Since the hexagon can be divided into 6 equilateral triangles

print(angle_LOQ)