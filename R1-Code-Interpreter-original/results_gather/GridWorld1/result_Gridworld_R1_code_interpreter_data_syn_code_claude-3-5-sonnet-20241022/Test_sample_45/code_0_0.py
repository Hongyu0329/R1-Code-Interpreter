import json

# Initialize the problem
initial_pos = "C4,4"
goals = ['C4,2', 'C5,1', 'C5,4', 'C3,2', 'C3,3', 'C5,3']
obstacles = ['C1,2', 'C2,5', 'C5,2', 'C1,5', 'C3,5', 'C1,3', 'C1,4', 'C4,3']

# Adjacency dictionary
adjacency = {
    "C1,1": ["C1,2", "C2,1"], "C1,2": ["C1,1", "C1,3", "C2,2"],
    "C1,3": ["C1,2", "C1,4", "C2,3"], "C1,4": ["C1,3", "C1,5", "C2,4"],
    "C1,5": ["C1,4", "C2,5"], "C2,1": ["C2,2", "C1,1", "C3,1"],
    "C2,2": ["C2,1", "C2,3", "C1,2", "C3,2"],
    "C2,3": ["C2,2", "C2,4", "C1,3", "C3,3"],
    "C2,4": ["C2,3", "C2,5", "C1,4", "C3,4"],
    "C2,5": ["C2,4", "C1,5", "C3,5"], "C3,1": ["C3,2", "C2,1", "C4,1"],
    "C3,2": ["C3,1", "C3,3", "C2,2", "C4,2"],
    "C3,3": ["C3,2", "C3,4", "C2,3", "C4,3"],
    "C3,4": ["C3,3", "C3,5", "C2,4", "C4,4"],
    "C3,5": ["C3,4", "C2,5", "C4,5"], "C4,1": ["C4,2", "C3,1", "C5,1"],
    "C4,2": ["C4,1", "C4,3", "C3,2", "C5,2"],
    "C4,3": ["C4,2", "C4,4", "C3,3", "C5,3"],
    "C4,4": ["C4,3", "C4,5", "C3,4", "C5,4"],
    "C4,5": ["C4,4", "C3,5", "C5,5"], "C5,1": ["C5,2", "C4,1"],
    "C5,2": ["C5,1", "C5,3", "C4,2"], "C5,3": ["C5,2", "C5,4", "C4,3"],
    "C5,4": ["C5,3", "C5,5", "C4,4"], "C5,5": ["C5,4", "C4,5"]
}

def find_path():
    def dfs(current, path, visited_goals):
        # If we've visited all goals, we're done
        if all(goal in visited_goals for goal in goals):
            return path
        
        # Try each adjacent cell
        for next_pos in adjacency[current]:
            # Skip if it's an obstacle
            if next_pos in obstacles:
                continue
                
            # Skip if we've already visited this cell in our current path
            if next_pos in path:
                continue
            
            # Create new visited goals set
            new_visited_goals = visited_goals | {next_pos} if next_pos in goals else visited_goals
            
            # Try this path
            new_path = dfs(next_pos, path + [next_pos], new_visited_goals)
            if new_path:
                return new_path
        
        return None

    # Start the search from initial position
    initial_visited_goals = {initial_pos} if initial_pos in goals else set()
    path = dfs(initial_pos, [initial_pos], initial_visited_goals)
    return path

# Find and print the path
path = find_path()
print(f"<<<{json.dumps(path)}>>>")