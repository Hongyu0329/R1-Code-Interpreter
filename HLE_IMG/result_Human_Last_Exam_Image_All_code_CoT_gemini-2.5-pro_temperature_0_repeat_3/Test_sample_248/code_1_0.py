def find_indicator_species():
    """
    This function identifies the species that indicates the photo was taken
    on the Atlantic coast and prints its scientific name.
    """
    # The common periwinkle, Littorina littorea, is native to Europe but is
    # now one of the most common intertidal snails on the North American Atlantic coast.
    # It is not native to the Pacific coast and its presence there is very limited.
    # Therefore, its appearance in this photo is a strong indicator of the Atlantic coast.
    scientific_name = "Littorina littorea"
    print(scientific_name)

find_indicator_species()