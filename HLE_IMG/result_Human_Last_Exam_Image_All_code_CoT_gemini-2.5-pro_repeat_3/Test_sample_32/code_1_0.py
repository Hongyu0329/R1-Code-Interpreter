def distinguish_grimmia_species():
    """
    Explains the main difference in the sporophyte between Grimmia montana and Grimmia curviseta.
    The difference is primarily in the seta (the stalk supporting the capsule).
    """

    # Define the characteristics of each species' sporophyte
    grimmia_montana_seta = "short and straight"
    grimmia_curviseta_seta = "longer and distinctly curved (often S-shaped)"

    # Print the explanation
    print("The main difference in the sporophyte between Grimmia montana and Grimmia curviseta is the seta (the stalk that supports the capsule):\n")
    print(f"1. Grimmia montana has a {grimmia_montana_seta} seta.")
    print("   This causes the capsule to be immersed or only slightly emergent among the perichaetial leaves.\n")
    print(f"2. Grimmia curviseta has a {grimmia_curviseta_seta} seta.")
    print("   This causes the capsule to be clearly exserted (raised above the leaves) and to appear nodding or drooping.\n")
    print("This difference in the seta's shape and length is the most reliable feature to distinguish the two species at a glance when sporophytes are present.")

distinguish_grimmia_species()