def get_watch_inscription():
  """
  This function stores and prints the famous inscription on Paul Newman's Rolex Daytona.
  """
  # The message engraved on the back of Paul Newman's Rolex Daytona by his wife, Joanne Woodward.
  message = "DRIVE CAREFULLY ME"

  # Convert the message to lowercase as requested.
  formatted_message = message.lower()

  # Print the final message. The original message contains no punctuation.
  print(formatted_message)

get_watch_inscription()