# Elevation of Jacumba Hot Springs, CA in feet, according to GNIS
elevation_feet = 2828

# Conversion factor from feet to meters
feet_to_meters_conversion = 0.3048

# Calculate the elevation in meters
elevation_meters = elevation_feet * feet_to_meters_conversion

# Print the equation and the result
print(f"The elevation of Jacumba Hot Springs is {elevation_feet} feet.")
print("To convert feet to meters, we use the conversion factor of 0.3048.")
print(f"Calculation: {elevation_feet} feet * {feet_to_meters_conversion} m/ft = {elevation_meters} meters")
print(f"The elevation in meters is {elevation_meters}.")