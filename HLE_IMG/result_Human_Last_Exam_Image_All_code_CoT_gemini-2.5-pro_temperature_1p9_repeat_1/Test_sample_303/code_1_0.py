def print_poem():
    """
    This function prints the complete text of the famous Tang poem "出塞" (Chū Sài)
    by Wang Changling.
    """
    title = "《出塞二首·其一》"
    author = "【唐】王昌龄"
    line1 = "秦时明月汉时关，"
    line2 = "万里长征人未还。"
    line3 = "但使龙城飞将在，"
    line4 = "不教胡马渡阴山。"

    print("这幅书法作品的内容是唐代诗人王昌龄的著名诗作《出塞》，其完整内容如下：")
    print("\n" + title)
    print(author)
    print(line1)
    print(line2)
    print(line3)
    print(line4)

print_poem()