def find_indicator_species():
    """
    Identifies the species in the image that indicates an Atlantic coast location.
    
    The image contains several small, dark, rounded snails known as the common periwinkle.
    This species, Littorina littorea, is abundant on the North Atlantic coasts of both
    Europe and North America (where it is an introduced species).
    It is not found on the Pacific coast of North America.
    Therefore, its presence is a definitive indicator of an Atlantic location.
    """
    
    # The scientific name of the Common Periwinkle.
    scientific_name = "Littorina littorea"
    
    print(f"The scientific name of the indicator species is: {scientific_name}")

find_indicator_species()