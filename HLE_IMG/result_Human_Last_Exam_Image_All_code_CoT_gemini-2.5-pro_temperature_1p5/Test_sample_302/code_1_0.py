def print_seal_characters():
    """
    This function prints the simplified Chinese characters found on the seal.
    The characters are read from right to left, top to bottom.
    Right column: 长 (cháng), 庚 (gēng)
    Left column:  不 (bù), 老 (lǎo)
    The full phrase is 长庚不老 (Cháng Gēng Bù Lǎo).
    """
    characters = "长庚不老"
    print(f"The characters on the seal are: {characters}")

print_seal_characters()