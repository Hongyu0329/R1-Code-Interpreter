def solve():
  """
  This function identifies and prints the scientific name of the species
  that indicates the photo was taken on the Atlantic rather than Pacific Coast.
  """
  # The Common Periwinkle, Littorina littorea, is an invasive species on the
  # North American Atlantic coast but is not widespread on the Pacific coast,
  # making its presence a key indicator.
  scientific_name = "Littorina littorea"
  print(scientific_name)

