def find_medallion_century():
    """
    This function determines and prints the century of the Ptolemaic medallion.
    The medallion, a gold octodrachm of Ptolemy II and Arsinoe II,
    was first minted around 272 B.C.
    """
    # The year 272 B.C. is in the 3rd century B.C.
    century_number = 3

    # We format the output to state the century clearly.
    # The equation here is simple: we are just presenting the number '3' in the final string.
    print(f"{century_number}rd century B.C.")

