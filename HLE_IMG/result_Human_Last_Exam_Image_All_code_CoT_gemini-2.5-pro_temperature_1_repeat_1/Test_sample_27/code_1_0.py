def get_artist_name():
  """
  This function identifies and prints the full name of the artist.
  """
  # The signature in the bottom right corner of the painting reads "manguin".
  # This signature belongs to the French Fauvist painter Henri Manguin.
  artist_first_name = "Henri"
  artist_last_name = "Manguin"
  
  full_name = f"{artist_first_name} {artist_last_name}"
  
  print(full_name)

get_artist_name()