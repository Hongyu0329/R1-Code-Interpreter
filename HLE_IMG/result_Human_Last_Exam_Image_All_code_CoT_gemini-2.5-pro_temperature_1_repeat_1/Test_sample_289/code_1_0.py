# The identified location is Jacumba Hot Springs, California.
# According to the Geographic Names Information System (GNIS), its elevation is 2831 feet.
# This script converts the elevation from feet to meters.

# Elevation in feet
elevation_in_feet = 2831

# Conversion factor: 1 foot = 0.3048 meters
feet_to_meters_factor = 0.3048

# Calculate the elevation in meters
elevation_in_meters = elevation_in_feet * feet_to_meters_factor

# Print the final equation with all the numbers
print(f"The elevation of the unincorporated community is {elevation_in_feet} feet.")
print(f"The conversion from feet to meters is {elevation_in_feet} * {feet_to_meters_factor}.")
print(f"The elevation in meters is: {elevation_in_meters}")
