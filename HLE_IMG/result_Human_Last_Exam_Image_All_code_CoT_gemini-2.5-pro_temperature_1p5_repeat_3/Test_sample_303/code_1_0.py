def print_poem():
    """
    This function prints the full text of the famous Tang dynasty poem
    identified from the calligraphic artwork.
    """
    title = "《独坐敬亭山》"
    author = "【唐】李白"
    line_1 = "众鸟高飞尽，"
    line_2 = "孤云独去闲。"
    line_3 = "相看两不厌，"
    line_4 = "只有敬亭山。"

    print(title)
    print(author)
    print(line_1)
    print(line_2)
    print(line_3)
    print(line_4)

print_poem()