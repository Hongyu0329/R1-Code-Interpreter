def explain_difference():
    """
    Explains the main difference in the sporophyte between Grimmia montana and Grimmia curviseta.
    """
    title = "Distinguishing Grimmia montana and Grimmia curviseta by Sporophyte"
    print(title)
    print("=" * len(title))

    difference = """
The main difference in the sporophyte between Grimmia montana and Grimmia curviseta, which allows for distinguishing them at a glance, is the seta (the stalk supporting the capsule).

1.  Grimmia montana:
    - Has a very short and straight seta.
    - This makes the capsule appear immersed or only slightly emergent from the leaves.

2.  Grimmia curviseta:
    - As its name 'curviseta' (curved seta) suggests, it has a longer, distinctly curved or hooked (arcuate) seta.
    - This curvature causes the capsule to be exserted (projecting beyond the leaves) and often nodding or oriented to the side.

Therefore, the presence of a long, curved seta in Grimmia curviseta versus a short, straight seta in Grimmia montana is the key distinguishing feature.
"""
    print(difference)

if __name__ == "__main__":
    explain_difference()