def find_indicator_species():
  """
  This function identifies and prints the scientific name of the species
  that indicates the photo was taken on the Atlantic Coast.
  """
  # The most identifiable species in the image that is exclusive to the
  # Atlantic coast of North America is the Common Periwinkle.
  species_name = "Littorina littorea"
  print(f"The scientific name of the indicator species is: {species_name}")

find_indicator_species()