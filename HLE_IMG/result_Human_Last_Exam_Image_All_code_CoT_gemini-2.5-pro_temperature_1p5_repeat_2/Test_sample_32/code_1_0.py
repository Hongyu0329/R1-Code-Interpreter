def explain_difference():
    """
    Explains the main difference in the sporophyte between Grimmia montana and Grimmia curviseta.
    """
    # The main difference lies in the seta (the stalk supporting the capsule).
    
    # Description for Grimmia montana
    species1 = "Grimmia montana"
    seta1 = "short and straight"
    capsule_orientation1 = "erect and typically immersed among the leaves"

    # Description for Grimmia curviseta
    species2 = "Grimmia curviseta"
    seta2 = "distinctly curved or S-shaped (cygneous)"
    capsule_orientation2 = "nodding or hanging down, often exserted beyond the leaves"

    print(f"The main distinguishing feature in the sporophyte between {species1} and {species2} is the seta (the stalk holding the spore capsule).")
    print("\n")
    print(f"1. In {species1}:")
    print(f"   - The seta is {seta1}.")
    print(f"   - This results in the capsule being held {capsule_orientation1}, as seen in the provided image.")
    print("\n")
    print(f"2. In {species2}:")
    print(f"   - The seta is {seta2}.")
    print(f"   - This causes the capsule to be {capsule_orientation2}.")
    print("\n")
    print("This difference in the seta's curvature and the resulting capsule orientation is the most reliable feature to distinguish the two species at a glance.")

explain_difference()