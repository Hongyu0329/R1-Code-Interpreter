carbon_atom_2 = 4

# This elimination process creates a new double bond between the two carbons.
print(f"In the product of the intramolecular Heck reaction, a new alkene (carbon-carbon double bond) is formed between C{carbon_atom_1} and C{carbon_atom_2}.")
