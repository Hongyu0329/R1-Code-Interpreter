def solve():
  """
  This function prints the scientific name of the species that indicates
  the photo was taken on the Atlantic rather than Pacific Coast.
  """
  # The key indicator species is the Common Periwinkle.
  # Its scientific name is Littorina littorea.
  # This species is widespread on the North Atlantic coasts (both Europe and North America)
  # but is not found on the Pacific coast of North America.
  scientific_name = "Littorina littorea"
  print(scientific_name)

solve()