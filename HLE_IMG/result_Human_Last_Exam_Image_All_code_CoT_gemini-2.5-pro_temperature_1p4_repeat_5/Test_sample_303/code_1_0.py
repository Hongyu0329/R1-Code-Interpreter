def display_poem():
    """
    This function prints the details and content of the famous Chinese poem.
    """
    title = "《黄鹤楼送孟浩然之广陵》"
    author = "（唐）李白"
    line1 = "故人西辞黄鹤楼，"
    line2 = "烟花三月下扬州。"
    line3 = "孤帆远影碧空尽，"
    line4 = "唯见长江天际流。"
    
    print("这幅书法作品出自一首著名的唐诗：")
    print("\n" + title)
    print(author + "\n")
    print(line1)
    print(line2)
    print(line3)
    print(line4)

display_poem()