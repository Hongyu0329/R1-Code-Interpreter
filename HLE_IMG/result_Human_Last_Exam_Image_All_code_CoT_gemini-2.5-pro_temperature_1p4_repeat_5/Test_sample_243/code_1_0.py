def get_endemic_island():
  """
  This function stores and prints the name of the island
  to which the Saint Helena Olive (Nesiota elliptica) was endemic.
  """
  # The plant shown is Nesiota elliptica, the Saint Helena Olive.
  # This species was endemic to the island of Saint Helena.
  island_name = "Saint Helena"
  print(f"The plant, Nesiota elliptica, was endemic to the island of: {island_name}")

get_endemic_island()