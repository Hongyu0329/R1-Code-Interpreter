def distinguish_grimmia_species():
  """
  Prints the main sporophyte difference between Grimmia montana and Grimmia curviseta.
  """
  explanation = """The primary difference in the sporophyte that helps distinguish Grimmia montana and Grimmia curviseta at a glance is the seta (the stalk supporting the spore capsule).

1.  Grimmia montana: This species has a very short and straight seta. As a result, the capsule is held erect and is typically immersed or just emergent from the surrounding leaves.

2.  Grimmia curviseta: The specific epithet 'curviseta' means 'curved seta'. This species is characterized by a seta that is distinctly curved or arched, especially when moist. This causes the capsule to be cernuous (nodding) or to hang downwards."""
  print(explanation)

distinguish_grimmia_species()