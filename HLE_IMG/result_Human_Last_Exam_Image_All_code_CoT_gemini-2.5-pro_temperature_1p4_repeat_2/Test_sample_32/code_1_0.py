def explain_difference():
    """
    This function explains the main sporophytic difference between
    Grimmia montana and Grimmia curviseta.
    """
    
    species1 = "Grimmia montana"
    species2 = "Grimmia curviseta"
    
    # The key distinguishing feature is the seta (the stalk supporting the capsule).
    
    description = (
        f"The main difference in the sporophyte between {species1} and {species2} that helps distinguish them at a glance is the seta.\n\n"
        f"In {species1}, the seta is straight, holding the capsule erect.\n\n"
        f"In {species2}, the seta is distinctly curved or arched (its name 'curviseta' means 'curved stalk'), causing the capsule to be nodding or cernuous."
    )
    
    print(description)

explain_difference()