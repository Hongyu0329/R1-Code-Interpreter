def find_checkmate():
  """
  This function prints the solution to the mate-in-2 chess puzzle.
  """
  move1_white = "Re3+"
  move1_black = "Kxe3"
  move2_white = "Qd1#"

  print("The solution for the mate in 2 is:")
  print(f"1. {move1_white} {move1_black}")
  print(f"2. {move2_white}")

find_checkmate()