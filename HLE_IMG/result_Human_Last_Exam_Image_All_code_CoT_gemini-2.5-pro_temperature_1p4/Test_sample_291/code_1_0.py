def find_cities():
  """
  This function identifies the city where the picture was taken
  and the city in the background.
  """
  # The picture is taken from Sausalito, looking across the bay.
  city_picture_taken_in = "Sausalito"

  # The city in the background, featuring Coit Tower, is San Francisco.
  background_city = "San Francisco"

  # Print the result in the specified tuple format.
  print(f"({city_picture_taken_in}, {background_city})")

find_cities()