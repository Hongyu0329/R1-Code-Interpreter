def find_indicator_species():
  """
  This function identifies the species that indicates the photo's location
  and prints its scientific name.
  """
  # The most prominent and geographically distinct species in the image is the
  # Common Periwinkle. This snail is abundant on the Atlantic coasts of North
  # America and Europe but is not found on the Pacific coast of North America.
  scientific_name = "Littorina littorea"
  print("The scientific name of the species that definitively indicates the photo was taken on the Atlantic rather than Pacific Coast is:")
  print(scientific_name)

find_indicator_species()