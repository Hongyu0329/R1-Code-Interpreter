def distinguish_grimmia_species():
    """
    This function explains the primary distinguishing characteristic
    between the sporophytes of Grimmia montana and Grimmia curviseta.
    """
    species1 = "Grimmia montana"
    species2 = "Grimmia curviseta"

    # Define the key characteristic for each species' sporophyte
    feature_montana = "a short, straight seta (stalk)"
    feature_curviseta = "a strongly curved or hooked seta"

    # Define the resulting appearance
    appearance_montana = "The capsule is held erect and immersed among the leaves."
    appearance_curviseta = "The capsule is bent over or nodding, often hidden beneath the leaves."

    # Print the explanation
    print(f"The main difference in the sporophyte between {species1} and {species2} that helps distinguish them at a glance is the seta (the stalk supporting the capsule).\n")
    print(f"1. {species1}: It has {feature_montana}. As a result, {appearance_montana}")
    print(f"2. {species2}: It has {feature_curviseta}. This causes the capsule to be {appearance_curviseta}")
    print("\nTherefore, the curvature of the seta is the most reliable feature to tell them apart quickly.")

distinguish_grimmia_species()