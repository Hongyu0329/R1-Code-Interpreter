def solve_riddle():
    """
    This function prints the Russian idiom identified from the image.
    """
    # The idiom is "Нашла коса на камень"
    # Нашла (Nashla) - Found (feminine past tense, agrees with коса)
    # коса (kosa) - Scythe
    # на (na) - on/onto
    # камень (kamen') - Stone
    # The literal translation is "The scythe found the stone."
    # The meaning is a clash of two stubborn, unyielding opponents.
    russian_idiom = "Нашла коса на камень"
    print(russian_idiom)

solve_riddle()