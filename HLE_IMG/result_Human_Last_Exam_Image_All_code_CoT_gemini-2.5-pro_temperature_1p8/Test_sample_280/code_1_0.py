# The Piet program is designed to solve the equation (2 * 3 * 4) + 5.
# This script will perform the same calculation.

# Define the numbers involved in the equation.
a = 2
b = 3
c = 4
d = 5

# Calculate the result following the order of operations.
result = a * b * c + d

# Print the full equation along with its final result.
# The original Piet program would output only the final number.
print(f"{a} * {b} * {c} + {d} = {result}")