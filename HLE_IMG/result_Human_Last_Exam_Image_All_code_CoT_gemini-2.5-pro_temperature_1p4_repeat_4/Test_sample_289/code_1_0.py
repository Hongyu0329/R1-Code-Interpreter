# The elevation of Dulzura, California, in feet from the Geographic Names Information System (GNIS).
elevation_feet = 935

# The conversion factor from feet to meters.
conversion_factor = 0.3048

# Calculate the elevation in meters.
elevation_meters = elevation_feet * conversion_factor

# Print the final equation with all the numbers.
print(f"{elevation_feet} feet * {conversion_factor} meters/foot = {elevation_meters} meters")