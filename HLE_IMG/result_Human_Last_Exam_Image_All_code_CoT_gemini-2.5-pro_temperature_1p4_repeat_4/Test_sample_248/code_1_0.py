def solve_riddle():
    """
    This function identifies the species that indicates the photo's location
    and prints its scientific name.
    """
    # The common periwinkle snail, Littorina littorea, is a species native to the
    # northeastern Atlantic. It was introduced to the North American Atlantic coast
    # and is now ubiquitous there, but it is not a characteristic species of the Pacific coast.
    # Its presence in this photo is a strong indicator of an Atlantic location.
    scientific_name = "Littorina littorea"
    print(f"The scientific name of the indicator species is: {scientific_name}")

solve_riddle()