def distinguish_grimmia_species():
  """
  This function prints the main difference in the sporophyte structure
  between Grimmia montana and Grimmia curviseta.
  """
  difference = "The main difference at a glance between the sporophytes of Grimmia montana and Grimmia curviseta is the curvature of the seta (the stalk supporting the spore capsule)."

  montana_feature = "Grimmia montana has a short, straight seta, which holds the capsule in an upright (erect) position."
  curviseta_feature = "Grimmia curviseta, as its name suggests (curvi-seta), has a strongly curved or arcuate seta. This causes the capsule to be oriented downwards or sideways (it is cernuous)."

  print(difference)
  print(f"- {montana_feature}")
  print(f"- {curviseta_feature}")

distinguish_grimmia_species()