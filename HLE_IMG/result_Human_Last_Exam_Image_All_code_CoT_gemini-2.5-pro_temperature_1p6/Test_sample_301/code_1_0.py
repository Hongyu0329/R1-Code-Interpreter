def identify_artists():
    """
    Identifies and prints the author of each of the 9 artworks.
    """
    artists = [
        "1. (Top-Left): Feng Zikai (丰子恺)",
        "2. (Top-Middle): Qi Baishi (齐白石)",
        "3. (Top-Right): Huang Zhou (黄胄)",
        "4. (Middle-Left): Feng Chengsu (冯承素)",
        "5. (Middle-Center): Wu Guanzhong (吴冠中)",
        "6. (Middle-Right): Chu Suiliang (褚遂良)",
        "7. (Bottom-Left): Huaisu (怀素)",
        "8. (Bottom-Middle): Jin Shangyi (靳尚谊)",
        "9. (Bottom-Right): Zhang Daqian (张大千)"
    ]

    print("From left to right, top to bottom, the authors are:")
    for artist in artists:
        print(artist)

identify_artists()
