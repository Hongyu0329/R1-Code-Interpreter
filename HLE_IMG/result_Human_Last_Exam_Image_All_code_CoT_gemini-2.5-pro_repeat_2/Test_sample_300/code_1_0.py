def solve():
    """
    Identifies the author of each of the 9 artworks from left to right, top to bottom.
    """
    authors = [
        "1. Huang Zhou (黄胄)",
        "2. Fu Baoshi (傅抱石)",
        "3. Mao Zedong (毛泽东)",
        "4. Xu Beihong (徐悲鸿)",
        "5. Jin Shangyi (靳尚谊)",
        "6. Lin Fengmian (林风眠)",
        "7. Wu Guanzhong (吴冠中)",
        "8. Li Keran (李可染)",
        "9. Qi Baishi (齐白石)"
    ]

