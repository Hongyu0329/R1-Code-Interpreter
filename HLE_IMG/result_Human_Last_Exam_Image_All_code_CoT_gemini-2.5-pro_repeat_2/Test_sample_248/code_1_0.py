def solve_riddle():
  """
  This function identifies and prints the scientific name of the species
  that indicates the photo was taken on the Atlantic Coast.
  """
  # The key indicator species is the Common Periwinkle.
  # This species is abundant on the North Atlantic coasts of both Europe and North America
  # but is not found on the Pacific Coast of North America.
  scientific_name = "Littorina littorea"
  print(scientific_name)

solve_riddle()