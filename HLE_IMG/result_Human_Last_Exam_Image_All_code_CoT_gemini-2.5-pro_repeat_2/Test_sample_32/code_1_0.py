def explain_grimmia_difference():
    """
    Explains the main difference in the sporophyte between Grimmia montana and Grimmia curviseta.
    """
    species1 = "Grimmia montana"
    species2 = "Grimmia curviseta"
    
    # Sporophyte characteristics
    seta_montana = "very short and straight"
    capsule_position_montana = "immersed or emergent (sitting within or just above the leaves)"
    
    seta_curviseta = "distinctly long and curved (arcuate)"
    capsule_position_curviseta = "exserted (held up and away from the leaves)"
    
    print(f"The main difference between the sporophytes of {species1} and {species2} lies in the seta (the stalk supporting the capsule).")
    print("-" * 80)
    
    print(f"1. {species1}:")
    print(f"   - Seta: The seta is {seta_montana}.")
    print(f"   - Capsule Position: This results in the capsule being {capsule_position_montana}.")
    print("\n")
    
    print(f"2. {species2}:")
    print(f"   - Seta: As its name 'curviseta' suggests, the seta is {seta_curviseta}.")
    print(f"   - Capsule Position: This holds the capsule in a(n) {capsule_position_curviseta} position.")
    print("-" * 80)

    print("\nAt a glance, the most distinguishing feature is the seta: straight and short in G. montana versus long and curved in G. curviseta.")

explain_grimmia_difference()

# The final answer summarises the key point.
final_answer = "The main difference is the seta (stalk). In Grimmia montana, the seta is short and straight, making the capsule appear immersed in the leaves. In Grimmia curviseta, the seta is longer and distinctly curved (arcuate), holding the capsule up and away from the leaves."
print(f"\n<<<ANSWER>>>\n{final_answer}")