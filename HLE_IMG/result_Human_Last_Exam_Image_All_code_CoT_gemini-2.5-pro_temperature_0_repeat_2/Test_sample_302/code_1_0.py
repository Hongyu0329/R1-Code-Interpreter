def seal_characters():
  """
  This function prints the simplified Chinese characters found on the seal.
  """
  # The characters on the seal are 長樂未央 (cháng lè wèi yāng) in traditional script.
  # The simplified version is 长乐未央.
  simplified_chars = "长乐未央"
  print(f"The characters on the seal in simplified Chinese are: {simplified_chars}")
  print("This is a traditional Chinese idiom meaning 'Eternal happiness without end'.")

seal_characters()