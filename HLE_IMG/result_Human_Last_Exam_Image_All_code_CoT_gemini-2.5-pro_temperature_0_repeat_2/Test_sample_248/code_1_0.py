def find_indicator_species():
  """
  This function identifies and prints the scientific name of the species
  that indicates the photo was taken on the Atlantic Coast.
  """
  # The common periwinkle, Littorina littorea, is native to the European Atlantic coast
  # and is a very common introduced species on the North American Atlantic coast.
  # It is not established on the Pacific Coast.
  scientific_name = "Littorina littorea"
  print(f"The scientific name of the indicator species is: {scientific_name}")

find_indicator_species()