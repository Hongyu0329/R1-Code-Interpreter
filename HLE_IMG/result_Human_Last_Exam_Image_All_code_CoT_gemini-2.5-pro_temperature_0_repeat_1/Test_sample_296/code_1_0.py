# The first drawing (in red) provides a visual pun for the Russian word "ломовой" (lomovoy).
# It shows a man with a crowbar, which is "лом" (lom) in Russian, performing hard labor.
# The second drawing illustrates the concept of an "извозчик" (izvozchik), which means a cabman or carter,
# by showing both a modern taxi driver and an old-fashioned horse-cab driver.
# Combining these two parts gives us the term "ломовой извозчик" (a drayman for heavy loads).
# The full idiom is a common rhetorical question based on this term.

print("Что, я тебе ломовой извозчик?")