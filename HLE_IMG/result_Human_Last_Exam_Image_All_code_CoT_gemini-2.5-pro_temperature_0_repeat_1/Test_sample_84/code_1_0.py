def find_city_with_similar_coat_of_arms():
  """
  This function identifies and prints the name of the city
  whose coat of arms is very similar to the one described.
  """
  # The key elements in the image's shield are:
  # - A shield divided horizontally (per fess).
  # - Top field: Blue (azure) with a crescent moon.
  # - Bottom field: Yellow/Gold (or) with a crab.
  # This design is the coat of arms of the city of Ochakiv, Ukraine.
  city_name = "Ochakiv"
  print(f"The city with a very similar coat of arms is: {city_name}")

