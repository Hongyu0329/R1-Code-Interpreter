def solve():
    """
    Identifies the author of each of the 9 artworks and prints the list.
    The order is from left to right, top to bottom.
    """
    authors = [
        "1. Feng Zikai (丰子恺)",
        "2. Wu Changshuo (吴昌硕)",
        "3. Ai Xuan (艾轩)",
        "4. Dong Qichang (董其昌)",
        "5. Chu Teh-Chun (朱德群)",
        "6. Chu Suiliang (褚遂良)",
        "7. Sun Guoting (孙过庭)",
        "8. Jin Shangyi (靳尚谊)",
        "9. He Jiaying (何家英)"
    ]

    print("The authors of the 9 works are:")
    for author in authors:
        print(author)

