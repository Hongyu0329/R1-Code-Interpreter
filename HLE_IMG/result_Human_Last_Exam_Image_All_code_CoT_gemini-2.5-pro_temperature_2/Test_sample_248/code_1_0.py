def solve():
    """
    This function identifies the species that indicates the photo was taken
    on the Atlantic rather than Pacific coast and prints its scientific name.
    """
    # The image contains several organisms common to rocky intertidal zones.
    # The most definitive indicator species is the Common Periwinkle snail.
    # This species (Littorina littorea) is abundant on the North American Atlantic coast
    # but is not found on the Pacific coast.
    scientific_name = "Littorina littorea"
    print(f"The scientific name of the indicator species is: {scientific_name}")

solve()