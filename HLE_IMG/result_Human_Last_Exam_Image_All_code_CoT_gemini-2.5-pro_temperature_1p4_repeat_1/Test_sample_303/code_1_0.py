def print_poem_content():
    """
    This function prints the full content of the famous poem "Jiang Xue"
    by the Tang dynasty poet Liu Zongyuan, which is depicted in the calligraphy.
    The output is in Simplified Chinese.
    """
    title = "《江雪》"
    author = "〔唐〕柳宗元"
    line1 = "千山鸟飞绝，"
    line2 = "万径人踪灭。"
    line3 = "孤舟蓑笠翁，"
    line4 = "独钓寒江雪。"

    print("这幅书法作品的内容是唐代著名诗人柳宗元的《江雪》。")
    print("其完整内容如下：")
    print("\n" + title)
    print(author + "\n")
    print(line1)
    print(line2)
    print(line3)
    print(line4)

print_poem_content()