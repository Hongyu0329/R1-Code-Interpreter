def get_watch_inscription():
  """
  This function stores and formats the inscription from Paul Newman's Rolex Daytona.
  """
  # The message engraved on the back of Paul Newman's Daytona
  inscription = "DRIVE CAREFULLY ME"

  # Convert to lowercase as requested
  formatted_inscription = inscription.lower()

  # Print the final message
  print(formatted_inscription)

get_watch_inscription()