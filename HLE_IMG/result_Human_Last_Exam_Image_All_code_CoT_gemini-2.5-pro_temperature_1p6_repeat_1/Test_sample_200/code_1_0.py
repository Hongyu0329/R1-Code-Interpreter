# The geological sequence shows a transition from a deeper-water rock (A) to a
# shallower-water rock (B), which indicates a marine regression (a fall in relative sea level).
# We need to identify which of the given events cause a marine regression.

# Let's analyze each option:
# i. Onset of glaciation: Traps ocean water in ice sheets, causing global sea level to fall. This leads to a REGRESSION. Plausible.
# ii. Coastal subsidence: The land sinks, causing relative sea level to rise. This leads to a TRANSGRESSION. Not plausible.
# iii. Global warming: Melts ice sheets and causes thermal expansion of water, causing global sea level to rise. This leads to a TRANSGRESSION. Not plausible.
# iv. Decreased Mid-Ocean Ridge Activity: Oceanic crust at ridges cools and contracts, increasing the ocean basin's volume and causing global sea level to fall. This leads to a REGRESSION. Plausible.

# The plausible explanations are i and iv.
# The required format is a comma-separated list with no spaces.

plausible_options = ['i', 'iv']
answer = ",".join(plausible_options)
print(answer)