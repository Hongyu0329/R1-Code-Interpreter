# The artist is identified by the signature "Picasso" in the bottom-right corner of the artwork.

def get_artist_full_name():
  """
  Returns the full name of the artist Pablo Picasso.
  """
  full_name = "Pablo Diego José Francisco de Paula Juan Nepomuceno María de los Remedios Cipriano de la Santísima Trinidad Martyr Patricio Clito Ruíz y Picasso"
