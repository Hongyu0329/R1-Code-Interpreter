def get_scientific_name():
    """
    This function returns the scientific name of the large milkweed bug.
    """
    # The scientific name for the large milkweed bug is Oncopeltus fasciatus.
    genus = "Oncopeltus"
    species = "fasciatus"
    scientific_name = f"{genus} {species}"
    print(f"The scientific name of the animal is: {scientific_name}")

if __name__ == "__main__":
    get_scientific_name()