def get_watch_inscription():
    """
    This function returns the famous inscription on the back of Paul Newman's Rolex Daytona.
    """
    # The message inscribed on the back of Paul Newman's Daytona by his wife, Joanne Woodward.
    message = "DRIVE CAREFULLY ME"

    # Convert the message to lowercase and remove any potential punctuation (though there is none).
    formatted_message = message.lower()

    # Print the formatted message.
    print(formatted_message)

get_watch_inscription()