def explain_difference():
    """
    Explains the main sporophyte difference between Grimmia montana and Grimmia curviseta.
    """
    species1 = "Grimmia montana"
    feature1 = "a short and straight seta (stalk)"
    result1 = "an erect capsule held upright among the leaves"

    species2 = "Grimmia curviseta"
    feature2 = "a distinctly curved or S-shaped seta"
    result2 = "a nodding or hanging capsule"

    print(f"The main difference in the sporophyte between Grimmia montana and Grimmia curviseta is the seta (the stalk supporting the spore capsule).\n")
    print(f"1. In {species1}:")
    print(f"   - The sporophyte has {feature1}.")
    print(f"   - This results in {result1}.\n")
    print(f"2. In {species2}:")
    print(f"   - The sporophyte has {feature2}.")
    print(f"   - This results in {result2}.\n")
    print("This difference in seta curvature and capsule orientation is the most reliable feature to distinguish them at a glance when sporophytes are present.")

if __name__ == "__main__":
    explain_difference()