# The characters on the seal are read from top to bottom, right to left.
# The characters are identified as follows:
# 1. Top Right: 君 (jūn)
# 2. Bottom Right: 子 (zǐ)
# 3. Top Left: 不 (bù)
# 4. Bottom Left: 器 (qì)
#
# These characters form a well-known phrase from the Analects of Confucius.
# This script will print the identified phrase in simplified Chinese.

def get_seal_content():
  """
  Returns the characters on the seal in simplified Chinese.
  """
  return "君子不器"

# Print the final result
print(get_seal_content())