def distinguish_grimmia_species():
  """
  Explains the main visual difference in the sporophytes of
  Grimmia montana and Grimmia curviseta.
  """
  species1_name = "Grimmia montana"
  species1_feature = "a short, straight seta, which results in an erect capsule."

  species2_name = "Grimmia curviseta"
  species2_feature = "a strongly curved or arcuate seta, which causes the capsule to be nodding or pendent (hanging down)."

  print("The main difference in the sporophyte between Grimmia montana and Grimmia curviseta lies in the seta (the stalk) and the resulting orientation of the capsule:")
  print("\n" + "="*70)
  print(f"{species1_name}:")
  print(f"  - The sporophyte has {species1_feature}")
  print("\n" + "="*70)
  print(f"{species2_name}:")
  print(f"  - The sporophyte has {species2_feature}")
  print("\n" + "="*70)
  print("\nIn summary, you can distinguish them at a glance by looking for an erect capsule (G. montana) versus a hanging or nodding capsule (G. curviseta).")

distinguish_grimmia_species()