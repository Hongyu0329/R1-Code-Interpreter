def solve_riddle():
  """
  This function solves the riddle by identifying the two parts of the clue
  and printing the final answer.
  """
  x = "Felt"
  y = "hat"

  print(f"The first word, X, is: {x}")
  print(f"The second word, Y, is: {y}")
  print(f"The complete term X Y is: {x} {y}")

solve_riddle()