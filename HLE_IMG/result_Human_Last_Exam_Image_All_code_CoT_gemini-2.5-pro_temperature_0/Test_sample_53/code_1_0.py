def identify_figure():
    """
    This function identifies the figures in the mythological scene depicted on the vase.
    The scene is the chariot race between Pelops and Oenomaus.
    """
    driver = "Pelops"
    passenger = "Hippodamia"
    fallen_man = "King Oenomaus"
    winged_figure = "Nike (Victory)"

    print("The scene on the vase depicts the myth of Pelops and Hippodamia.")
    print(f"The driver of the chariot is the hero, {driver}.")
    print(f"The man defeated on the ground is {fallen_man}, Hippodamia's father.")
    print(f"The winged figure celebrating the win is {winged_figure}.")
    print("-" * 20)
    print(f"The figure being carried away in the chariot is: {passenger}")

identify_figure()