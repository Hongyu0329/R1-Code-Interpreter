def solve_riddle():
    """
    This function solves the riddle by breaking it down into two parts and connecting them.
    """

    # Clue 1: "X Y is pictured at an event on June 20, 2019."
    # The image shows hockey player Nikita Kucherov at the 2019 NHL Awards.
    # In hockey, scoring three goals is a "hat trick". A player who achieves this can be called a "hatter".
    clue1_subject = "A hockey player (a 'hatter')"

    # Clue 2: "Another X Y has almost ceased to be used due to the toxicity of mercury salts."
    # This refers to the historical profession of a "hatter" (a hat maker), who used mercury
    # salts to make felt, a practice now obsolete due to its toxicity.
    clue2_subject = "A hat maker (also a 'hatter')"

    # The common word connecting both clues is "Hatter".
    answer = "Hatter"

    print("Thinking Process:")
    print(f"1. The image shows a famous hockey player at the 2019 NHL awards. In hockey, a player who scores three goals gets a 'hat trick'. This person can be described as a 'hatter'.")
    print(f"2. The second clue refers to the old profession of a 'hatter' (a hat maker), which used toxic mercury salts to make felt.")
    print(f"3. The word that fits both clues is '{answer}'.")
    print("\nFinal Answer:")
    print(answer)

solve_riddle()