def find_author():
    """
    This function identifies and prints the author of the altarpiece detail.
    The artwork is a terracotta relief from the high altar of the Badia Fiesolana.
    Based on art historical records, the author is known.
    """
    author = "Benedetto Buglioni"
    artwork_title = "The Communion of the Apostles (Sacrament of the Altar)"
    location = "Badia Fiesolana, Fiesole"

    print(f"The author of this altarpiece detail is: {author}")
    print(f"Artwork: {artwork_title}")
    print(f"Location: {location}")

find_author()