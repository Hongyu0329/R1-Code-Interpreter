def explain_difference():
    """
    Explains the main difference in the sporophyte between Grimmia montana and Grimmia curviseta.
    """
    species1 = "Grimmia montana"
    species2 = "Grimmia curviseta"
    
    # The primary distinguishing feature is the seta (the stalk supporting the capsule).
    
    # Feature for Grimmia montana
    feature1_seta = "straight"
    feature1_capsule_orientation = "erect and typically immersed or emergent among the leaves"
    
    # Feature for Grimmia curviseta
    feature2_seta = "strongly curved or cygneous (swan-necked)"
    feature2_capsule_orientation = "cernuous (nodding) or pendulous (hanging down)"
    
    print(f"The main difference in the sporophyte between {species1} and {species2} that helps distinguish them at a glance is the seta (the stalk holding the capsule):")
    print("\n")
    print(f"1. {species1}: Has a {feature1_seta} seta, which makes the capsule {feature1_capsule_orientation}.")
    print(f"2. {species2}: Has a {feature2_seta} seta, which causes the capsule to be {feature2_capsule_orientation}.")

explain_difference()