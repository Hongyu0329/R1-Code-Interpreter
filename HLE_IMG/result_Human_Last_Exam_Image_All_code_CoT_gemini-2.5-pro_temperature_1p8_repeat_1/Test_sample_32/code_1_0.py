def explain_grimmia_difference():
    """
    This function explains the primary visual difference between the sporophytes
    of Grimmia montana and Grimmia curviseta.
    """
    species_1_name = "Grimmia montana"
    species_1_seta = "a short and straight seta"
    species_1_capsule = "upright and immersed among the leaves"

    species_2_name = "Grimmia curviseta"
    species_2_seta = "a short but strongly curved seta"
    species_2_capsule = "nodding or hanging"

    print("The main difference in the sporophyte between Grimmia montana and Grimmia curviseta lies in the seta (the stalk supporting the capsule):\n")
    print(f"1. {species_1_name}:")
    print(f"   - Seta: Has {species_1_seta}.")
    print(f"   - Capsule Position: As a result, its capsule is {species_1_capsule}.\n")

    print(f"2. {species_2_name}:")
    print(f"   - Seta: Has {species_2_seta} (the name 'curviseta' means 'curved seta').")
    print(f"   - Capsule Position: This causes its capsule to be {species_2_capsule}.\n")

    print("Therefore, at a glance, you can distinguish them by checking if the capsule is upright (Grimmia montana) or nodding (Grimmia curviseta).")

# Execute the function to provide the explanation.
explain_grimmia_difference()