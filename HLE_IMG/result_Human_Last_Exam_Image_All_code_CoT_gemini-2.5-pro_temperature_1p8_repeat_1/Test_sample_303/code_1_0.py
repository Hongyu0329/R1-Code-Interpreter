def display_poem():
    """
    This function stores and prints the famous Chinese poem "Birdsong in the Ravine" by Wang Wei.
    """
    title = "《鸟鸣涧》"
    author = "（唐）王维"
    line1 = "人闲桂花落，"
    line2 = "夜静春山空。"
    line3 = "月出惊山鸟，"
    line4 = "时鸣春涧中。"

    poem_text = f"{title}\n{author}\n{line1}\n{line2}\n{line3}\n{line4}"
    
    print(poem_text)

display_poem()