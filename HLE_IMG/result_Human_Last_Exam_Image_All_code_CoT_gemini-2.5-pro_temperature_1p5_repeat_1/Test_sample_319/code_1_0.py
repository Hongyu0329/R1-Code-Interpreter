# The image displays a visual representation (spectrogram and waveform)
# of a famous auditory illusion. In this illusion, a sound like a pure
# tone is interrupted and replaced by a burst of noise.
# The human brain, however, perceives the tone as continuous,
# effectively "restoring" the part of the tone that was obscured by the noise.
# This perceptual phenomenon, discovered by Richard M. Warren, is known as
# the Phonemic restoration effect. Although the name refers to phonemes (speech sounds),
# it is also the standard term for the effect when demonstrated with simple tones.

phenomenon_name = "Phonemic restoration effect"
print(phenomenon_name)